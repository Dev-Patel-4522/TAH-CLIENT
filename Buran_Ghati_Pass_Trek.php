                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 01- Shimla to Janglik</h3>
                                    <p>
                                        Janglik is the base camp for Buran Ghati trek from where you'll begin your proceedings. You will have to drive from Shimla passing Rohru which is one scenic drive along the way. On the way, you'll go past 
                                        pretty villages which have a charm of Himachal culture that catches your eyes instantly. On the journey, you'll also come across forests of Oak and Pine trees which look artistic with their sharp edges. 
                                        We will be having our lunch on our way to Diude. It will approximately take you two hours to get to Tonglu as the motorable road at Tikri becomes a dirt track for the rest of the journey. After reaching 
                                        Diude, hot dinner will be served at our campsite/guesthouse. Please make a note about the last ATM point which is at Rohru, so if you want to withdraw any cash make sure that you visit the ATM. Rohru is 
                                        also the last place where you'll find the network on your phone.
                                    </p>
                                    <h3> Day 02- Janglik to Dayara Thatch</h3>
                                    <p>
                                        Wake up in the morning to a bright view of the valley. Breakfast and tea will be served before embarking ahead. Today's walk is one of the most scenic ones as you will walk past the beautifully crafted wheat 
                                        fields that are set in front of wooden farmhouses. The artistic wooden houses with their attractive roofs look captivating and are sure to catch your attention. You will be able to spot the entire view of Diude, 
                                        Tonglu and other small villages after climbing for an hour.
                                    </p> 
                                    <p>
                                        After that, we'll take some time for refreshments with a setting of pine trees surrounding us. The forest trail is not a treacherous one and will surely help you relax. You'll be amazed to see the trees surrounding you 
                                        and the small streams which have a cooling effect on your senses. When such beauty surrounds you, it takes some time to soak in what mother nature has to offer so take your time and get a taste of your surroundings.
                                    </p> 
                                    <p>
                                        While deteriorating out of the tree line almost after an hour you will be greeted by the calming trees of Pine and Oak which are much more thicker and denser than the earlier ones. The dried leaves which are fallen on 
                                        the floor make it look like a carpet of leaves which is put for you to walk on. The trail climbs gradually before coming out again. The Dayara meadows will be our spot for camping tonight. In the evening we'll climb the 
                                        nearby hillock for better acclimatization. It is important for your body to be properly acclimatized to these conditions as you will be gaining more altitude. After coming back from the acclimatization walk, dinner will 
                                        be served at your campsite.


                                    </p>
                                    <h3> Day 03- Dayara Thatch to Litham</h3>
                                    <p>
                                        You will start your day exploring the Dayara meadows after having your breakfast and hot tea that will be served at your campsite. You can take a walk just to soak in the freshness that the morning has to offer. 
                                        The first rays of sunlight make the meadows look more artistic.
                                    </p>           
                                    <p>
                                        The trail which follows from Dayara to Litham takes you through dense forest, green meadows and gushing streams. Throughout the trail, there are streams where you can refill your water bottles. Before reaching the Dayara campsite you'll 
                                        be able to see the snow-capped mountains of the Dhauladhars. You will come across the forest of Bhoj which has a beautiful setting. From here you'll be able to spot a clear view of Litham. After crossing the Chandranahan stream you will 
                                        reach the Litham campsite. You will camp here for the night.
                                    </p>           
                                    <h3>Day 04- Acclimatization day, visit to Chandranahan lake</h3>   
                                    <p>
                                        We'll start early in the morning as today there is a steep ascent and descent. After having your breakfast we'll be visiting the Chandranahan lake for an acclimatization walk and will be coming back to Litham campsite. It is a very exciting and 
                                        satisfying walk where you'll be getting a chance to click a lot of pictures along the way. We will continue walking upstream towards the waterfall from where it emerges.
                                    </p>  
                                    <p>
                                        You will also get good amount of snow in the month of June. As you reach the lake you will notice that the lake is a bit smaller in size and is fed by the snowfields around it. You can spend some time around the lake and click pictures of the panoramic 
                                        view it has to offer before heading to Litham again. It will be a steep descent to Litham, therefore, you'll be starting early in the morning so that you reach Litham by lunch.
                                    </p>  
                                    <h3>Day 05- Litham to Dhunda</h3>   
                                    <p>
                                        You will have to climb for 2 hours to Dhunda which is at an elevation of 13,300 ft. You will be able to see the Dhauladhar range which looms over the trail with Gunas pass sitting pretty on top of the snowy flanks of the grey mountains. It is a combination of grey 
                                        and white mountains with the grassland of Litham in the left and Rupin Valley in the right. It has a sharp ascent which gives you the first view of Buran Ghati to your left. Spend some time at this spot to click some wonderful pictures.
                                    </p>  
                                    <p>
                                        You will have to start the climb to Dhuna taking the shepherd trail that leads to the pass. From here you get a clear view of the Buran Ghati trail to leads to it. You will be camping in the midst of snow-capped mountains which will encircle you from all four sides. 
                                        When it comes to camping in the alpine mountains the entire campsite of Dhunda is one of the most picturesque ones.
                                    </p>  
                                    <h3>Day 06 – Dhunda to Munirang (River camp) via Buran Ghati Pass</h3>   
                                    <p>Today we'll be trekking for almost 10-11 hours which will be both ascending and descending. In September, the snow melts and it is not difficult to climb the pass. Out of the Dhunda campsite follow the ridge and hop over boulders towards the base of the pass. From the base, 
                                        it is an hour long climb to the top, mostly on snow. Buran Ghati at exactly 15,000 ft is a thrilling climb. It is a narrow ledge on the top. The drop on the other side is sharp and steep. Without the rope, the descent can be risky even with an ice axe. Getting down to the first 
                                        snowfield about 100 metres below the pass is your first objective. Thereafter it is a series of descents over smaller ledges that you can slide down!
                                    </p>  
                                    <p>
                                        After taking the ridge to follow the pass you will reach the base of the pass. It will take you an hour-long climb to the top of the pass which is situated at an elevation of 15,000 ft. You will have to be careful as it a narrow ledge on the top with a steep drop on the other side.
                                    </p>  
                                    <h3>Day 07 – Munirang (River camp) to Barua village, Drive to Shimla</h3>   
                                    <p>
                                        On the last day of the trek, you will descend to the Barua village which is known for the vegetation. The trail is a beautiful one and a real treat for all the shutterbugs. You will be astonished to see the beauty this trek offers while descending. The trail goes quickly through the 
                                        high altitude pines with a blend of other trees surrounding.
                                    </p>  
                                    <p>
                                        After descending for an hour and half the trail takes you to a large stream which meets the Barua Khud. As soon as you come out of the stream, you'll be able to spot a lot of shrubs which are lined on either side. You will also get a chance to spot trees of apricot and peaches along the way. 
                                        After an hour, you'll be greeted by apple orchards and you'll spot signs of cultivation. When you reach the village the descent is more rapid. In half an hour you'll reach the road bridge. We will pick-up you by 1:00 pm for Shimla and you will reach there in between 10:00 to 11:00 pm, Please 
                                        make your hotel booking at Shimla accordingly
                                    </p>  
