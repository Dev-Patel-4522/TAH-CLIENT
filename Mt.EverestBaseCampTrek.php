                                <h4>About the trek</h4>
                                <p>The Everest Base Camp Trek, is one of the most famous Trek in Nepal, which is famous for its fantastic mountain peaks and the beauty on the hills. Most of the places to this spot go through ridges and trails, but you would have ample space to rest and have meal on your way. 
                                    And one of the best things about this place is that you don’t have to worry about being lost, as you can ask anybody for the next village. The people here are very friendly and offer help and support even to strangers. The people who live here are mostly shepherds and understand English; some even speak it fluently.
                                </p>

                                <p>You can come to this destination all around the year as people trek in all seasons; however the best time to come to this area is between March to mid may and between Septembers to mid November. 
                                    Many people do not prefer coming here in winters as the weather is very cold and the snow all around may make your trekking experience difficult. In such conditions, it would not be possible to go to higher altitudes. 
                                    At higher altitudes, the lodges may even be closed due to the weather and you might have a difficult time in finding a place to rest and spend the night. Therefore it becomes a mandate to carry your tents along. 
                                    Not to mention, the nights may get very cold and you would have to carry some extra clothes to keep you warm.
                                </p>
                                
                                <p>
                                    During the summers, the season is wet and the peaks are lost in the clouds. Therefore April and May might just prove a good time to go around this place. You can enjoy the tree and shrubs blossoming in this season, with Rhododendrons in particular. This adds a spectacular colour and beauty to the surrounding. 
                                    However the views are much better if you plan a post monsoon trek than a pre monsoon trek because the rain and wind clear the dust which blows from the terrain. However while trekking to this area; it is mandatory to visit the Trekker's Information Management System (TIMS), particularly if you are going to Khumbu region.
                                </p>
                               
