                                            </li>
                                        </ul>
                                    </p>
                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1: Arrive Kathmandu </h3>
                                    <p>
                                        Upon arrival in Kathmandu-Take cab from Airport and reach booked hotel in Kathmandu City. Enjoy local cuisine in the evening and do trek gears shopping or any last minute purchase from this place.
                                        <p>
                                            Attend briefing session in the evening –know about trek do and don’t. If you have any questions then it’s good time to get all the answers. Also make a note- from April 2019 most flights have been diverted to Ramecchap from Kathmandu due to construction and crowd at Kathmandu Airport. 
                                            Ramechhap is the small airport in manali and 4 hrs drive away from Kathmandu. Your flight will be booked for next day so take early night and get ready by 1 to leave Kathmandu city for taking early morning flight from Ramechhap Airport.
                                        </p>
                                        <p>
                                            As per the Nepal government, foreigners(including OCN, NRN) need to obtain a special permit to trek in Nepal. This permit is issued in Kathmandu and therefore, 
                                            foreigners need to come one day before in Lukla. This process requires your signed ILP, therefore it cannot be processed in advance and can be processed only 
                                            when you enter Nepal. The charges of obtaining the permit will be Rs.2, 000 per person. TAH will cooperate with you in getting the permit. You have to pay this 
                                            amount to our representative and we shall get the permit, once you have arrived. Please make a note that your expenses for the additional day are not included in 
                                            our costs.
                                        </p>
                                        <p>
                                            Citizens of China, Myanmar, and Bangladesh are not permitted to trek in Nepal.
                                        </p>
                                    </p>
                                    <h3> Day 2: Fly to Lukla, trek to Phakding </h3>
                                    <p>
                                        Early morning next day, you would have a flight from Kathmandu to Lukla. You should keep your pen- knifes, lighter, matchboxes and other related stuff in the bigger baggage than in your hand-bag to avoid any security breach and unnecessary delay. 
                                        The planes are generally small for these flights but the journey is panoramic. On arrival to Lukla, you would be received by your trekking guide, if you have booked one. You will have some time to see Lukla and enjoy its beauty. Your trekking in Nepal would begin from this point. 
                                        <p>
                                            The walk from Lukla to Phakding is gradual and has few ups and downs on the way. You would have a superb view of Kusum-Kangru, while you are crossing the short suspension bridge on your way. After a short climb, you would reach the Ghat village and another one and a half hours of gradual walk brings you to Phakdng village. 
                                        </p>
                                        
                                    </p>
                                    <h3>Day 3: Phakding to Namche bazaar </h3>
                                    <p>
                                        From Phakding, after crossing the Dudh-koshi suspension bridge, the walk is easy and pleasant with short uphill and down-hill climb. You would be re-crossing the same river again. You would have a magnificient view of Thermasarkhu which is 6,608 meters from the village of Benkar. 
                                        Now you would cross the long suspension bridge over the Bhote- Koshi River. This is a busy area and is full f trekkers and porters. After here you would finally arrive to the small river of Monjo, after a climb of 20 minutes from this area.
                                    <p>
                                        Here, you can see some interesting water mills which are used for grinding corns and barley. It is one of the major staple diet of the Sherpas. Monjo is situated below the magnificent peak of Thermasharkhu. Close to this you have the entrance to the Sagarmatha national park. 
                                        At this point, your trekking permit is checked and the officials kleep a record of all the trekkers going ahead. After a gradual walk you would reach Namche bazaar. You can also get prepared for some high altitude trekking.
                                    </p>
                                    </p>
                                    <h3> Day 4: Namche Bazaar </h3>
                                    <p>
                                        This day is reserved for acclimatization and going around the place. Namche Bazaar is a good place to shop and see magnificent views of the area. You would cherish the view of Kwangde peak and its sister peaks in the east.
                                        The houses in Namche Bazaar when looked from a distance appear to be in a U shaped bowl. 
                                    </p>
                                    <p>
                                        You can have a short up- hill walk towards the Sangmatha National Park headquarter. This spot is on the top of Namache Bazaar with breath-taking view of the surrounding areas. You would have a look at the giant snow- covered peaks and mountains. 
                                        There is also a local museum which gives you a fair detail of the history of Himalayas, its geographic and cultural aspects, mountaineering history and various other information on the flora and fauna of the area. 
                                    </p>
                                    <p>
                                        The overnight stay would be in guest houses. You would be highly acclimatized to the area by this time. 
                                    </p>
                                    <h3>Day 5: Namche Bazaar to Tengboche </h3>
                                    <p>
                                        The first half of the walk is fairly simple and easy. You can reach the top of Namche Bazaar after a walk of 20 minutes. After a pleasant walk of 2 hours, you come to a small place at Shanasa. Here you can see the colorful Danphe pheasant, which is the national bird of Nepal and might also get a chance to have a look at the musk-deer and also the Himalayan Thar, 
                                        which is a mountain goat that looks like an antelope. With a gradual descent, you would reach the Imjatse River at Phungitenga which is at a height of 3250 meters. After a strenuous walk from here for an hour, you would reach the religious Buddhist entrance which lies just before Tengboche.
                                        <p>
                                            Here, your walk is over for the day after 5 hours and you can admire and enjoy this religious place. The overnight stay is in guest houses.
                                        </p>
                                    </p>
                                    <h3>Day 6: Tengboche to Dingboche</h3>
                                    <p>
                                        After you have relaxed for the day, you begin the trek towards Dingboche. The path goes through a forest that has trees like birch, juniper, fir, rhododendron etc. Through this forest you arrive to a lovely place i.e. Deboche which is at 3,650 meters. This place gives you a magnificent view of Mt. Ama Dblam, Mt. Everest, Nuptse and Lhotse. 
                                        From here, the walk is mostly up-hill and continues to be so until you reach a large mani- wall gate. From here the way to Dingboche is divided into two paths. One of the paths towards Dingboche also goes through Pangboche. You can take this route to see more views and natural beauty. 
                                    <p>
                                        Dingboche is a lovely place and you will find stone- walls protecting the agricultural fields. The walls are built to protect the crops from cold winds and grazing animals. Your over night stay would again be n guest houses. 
                                    </p>
                                    </p>
                                    <h3>Day 07: Dingboche. </h3>
                                    <p>
                                        Dingboche is a lovely and green place. The greenery is spread all around the village and you would find farms and crop- fields all around. The natives here grow crops like barley, buckwheat and potatoes. 
                                        Dingboche has more than tea- houses so you would also like to have a cup of the same. This day is reserved for acclimatization to the weather and place.
                                    </p>
                                    <p>
                                        You can go for a short walk around the village or might trek to Chukung, which is 3 or 5 hours trek and at a height of 4,730 meters. Chukung is towards the east and lies within the Imjatse valley. Thought the trek is gradual, you would still feel the thin air developing around this place. At Chukung, 
                                        again there are about five tae-houses and give a superb and over- looking view. You can also explore the valley in the surrounding area and later return to your guest house. 
                                    </p>
                                    <h3>Day 8: Dingboche to Lobuche </h3>
                                    <p>
                                        From Dingboche the trail diverts towards the north, and the path is gradual and easy for the first 40 – 50 minutes. After this easy walk you would come to a mani- prayer stupa. From here the trail s slightly down towards Pheriche village. 
                                        On this day of your walking, you would have a beautiful view of the Mt. Tawache, Ama Dablam, and Pokalde, which is approximately 5,741 meters towards the north. You would also have a view of the great wall of Nuptse. 
                                    <p>
                                        You would have a pleasant walk for the next one and a half to two hours from Pheriche till you reach the small wooded bridge over the river of Khumbu glacier. This place is called Thugla and is the only place which has tea- houses before Lobuche. 
                                        From here another 3 hours of strenuous trekking time brings you to Lobuche which lies at one corner of the hill. The overnight stay would be in guest houses. 
                                    </p>
                                    
                                    </p>
                                    <h3>Day 9: Lobuche to Gorakshep to EBC and back to Gorakshep </h3>
                                    <p>
                                        Today will be an exciting day for you as you are very close to your final destination. Today you will have an adventurous journey and a great trekking experience. The first walk of an hour is gradual up to the valley and leads to a grassy field. 
                                        A walk of nearly 20 minutes takes you to the rocky moraine dunes. As you go ahead on your journey you can see the icy- glacial ponds and icebergs which lie below the Khumbu glacier. After a few more rocky moraine dunes and a gradual downhill walk, you reach Gorakshep.
                                    </p>
                                    <p>
                                        After this, the trek to EBC can be misleading among the high- altitude trekking which is a strenuous climb. You must follow the lead Sherpa as the paths change every season due to the movement of the glaciers. But once you reach to EBC after a total of 7 hours walk, a grand view of the Khumbu ice fall awaits you. EBC is a lively place during the high expedition season.
                                    </p>
                                    <h3>Day 10: Hike upto Kalapatthar and retreat trek to Pheriche. </h3>
                                    <p>
                                        After you have trekked to the EBC and returned back to Gorakshep and an over night stay in Guest houses, you are now prepared to climb up to Kalapatthar. Besides, Gorakshep also is a good place to go around. It has a couple of tea- houses and it also used to be the base camp for trekking the Everest in the 1950’s. 
                                        The walk towards Kalapather from Gorakshep starts with a few minutes of easy walk. All this while, you will be walking in the sandy fields. After here, there is a steep climb for about 45 minutes. Post this tiresome and strenuous mountaineering, you would come to the level fields and the walk from here is easy ad gradual. 
                                        You would have the comfort of walking for the next one hour, post which there is again a steep trek to the top of Kalapatthar for about half an hour. The Everest from this point looks closer than you will imagine.
                                    </p>
                                    <p>
                                        AAfter here, you trek back to Pheriche. 
                                    
                                    </p>
                                    <h3>Day 11: Pheriche to Namche bazaar. </h3>
                                    <p>
                                        After you have already reached your final destination the previous day, your cumbersome trekking time is almost over. You have done the Everest base camp trek and a lot of Himalayan treks. From this day onwards, you would have pleasant and easy walks throughout. You will be heading downwards to lower elevations. The walk is similar all the while to Thugla. 
                                        After you reach the ridge, you would follow the stream and start traveling towards Pheriche valley. After a walk of around 40 minutes you reach the valley floor. You can take another one hour of easy and simple walk to reach Pheriche which is at 4,243 meters. You would have a wonderful the treks in Nepal by now. You trek down the slope to reach Namche bazaar. 
                                        Once you reach here, the walk for the day is over and you would spend the night in guest houses and enjoy the local meals. 
                                   
                                    </p>
                                    <h3>Day 12: Namche Bazar to Phakding to Lukla </h3>
                                    <p>
                                        Once you wake up to the lovely weather in Namche Bazaar, you would like to visit the place and go around a little bit, before you start your downhill journey. You can visit the local markets of Namche Bazaar and have a look at the local art crafts and handicrafts. You can buy a lot of stuff from here to make your journey memorable. 
                                        You would like to have a small walk in and around the Sagarmatha National Park. It is a good place for photography and collecting memories. You would also enjoy the local food which has a distinct taste. 
                                    <p>
                                        The trek from Namche Bazaar to Phakding is again a simple and easy walk. You would be trekking down the slope most of the time. The beauty of the forest and distant mountains with several water- falls add to your journey a great delight. This is the beauty of Nepal trekking. 
                                        <b>There will be a few uphill climbs but you will manage it very easily. The lush green beauty and forests will take your pain away. After this easy walk you would reach Lukla. This will be your last day with the Sherpas and other crew members, so you would like to enjoy the day with a delicious dinner and a few drinks.</b>
                                    </p>
                                    </p>
                                    <h3>Day 13: Fly back to Kathmandu </h3>
                                    <p>
                                        On this day you would have to wake up early as the flights to Kathmandu are generally scheduled in the morning. This is because during daytime, the weather is generally windy and there might be a delay in the flight timings. However you expect a slight delay in you r early morning flight as well if the weather is not suitable. 
                                        As you make your way through the security checks and your flight takes off from Lukla airport, you would have a magnanimous view of the surrounding. Drive back to Kathmandu from Ramechhap Airport and overnight stay in Hotel. 
                                    
                                    </p>
