                                       since Tsokha to Phedang is 4.5km and Phedang to Dzongri is also the same. The hikers must have packaged lunch and enough water for the trek since on the way there is no place to refill 
                                       the same. The trail is a steep ascent so proper precautions are mandatory as it is not an easy journey for the trekkers. Within minutes of climbing, trekkers can find the trail leaves 
                                       Tsokha, way behind and moves forward amidst red rhododendrons which fill the heart with happiness. Especially if the visit is conducted in spring season the trail remains filled with 
                                       red blossoms making the pathway quite romantic and passionate. The rocky terrain at the beginning of the day slowly transforms to a paved one with wooden logs. However even though there 
                                       are fascinating pathways in midst, the rocky and steep climb continues giving a hard time to the legs and knees of the trekkers. However the hard time is not done as the trail continues
                                       through stones ad pebbles till it reaches Phedang. This is an excellent scenic location to have rest and enjoy lunch with the scintillating views of nature entertaining all. After having 
                                       rest and regaining some fresh energy, the trek continues for Dzongri. High steep climbing is found very common in the next phase of climbing and proper protection should be taken for it. 
                                       After almost an hour of travelling, the hikers can get the sight of the worshipper flags host here for praying. With so much altitude gain one may suffer from height sickness, headache and 
                                       loss of oxygen. But soon the same will be adjusted, and if the problem persists it is better to consult the tour guide. The trail continues amidst in line trees and stretched meadows with 
                                       the background of snowy mountains making it quite a heartfelt experience for the onlookers. After reaching about 12,980 feet, campsite, place the tents and have a much needed rest for the 
                                       night.
