                                    <h3> Day 2: Yuksom to Sachen</h3>
                                    <p>
                                        The destination for the first trekking day is Sachen based at an altitude of 7200 feet. The total distance from Yuksom to Sachen is about 8kms and takes approximately 
                                        6 hours to reach the destination. Though many tour operators believe in travelling till Tshoka in one go, but it can be quite tiring and the enjoyment of the trek could 
                                        be lost with too much physical exhaustion. In this trail one can find absolutely no shops and even the telecom network gets quite erratic as the elevation increases. 
                                        Packed lunch should be carried since it’s a long day. The beautiful trail starts with sights of fascinating small huts as the traveller’s move forward leaving the villages 
                                        behind. Enjoying the fresh air the trail continues crossing the vast landscapes and soon the trail lands to the valley of the serene River Rathong.
                                        <p>
                                            Crossing the valley, the trekking trail takes a sharp turn to the forested lands which with each step becomes denser with thick growth of trees. After sometime, 
                                            the travellers may feel difficulty since sunlight only peeks in few deserted areas but the rest of the trail through the deep tropical forest of oak, fir, bamboo 
                                            and pine trees making the route quite dark. Many hikers use torches in this place to have a clear vision since the trail often remains congested with mountain yaks 
                                            and mules. It is advisable to give way to the animals and stay close to the mountains. Trekking through the forest for around 30 mins, the trekkers ca reach the first 
                                            bridge which is actually in absolute darkness except some occasional sunlight. Continuing on the trail the trekkers moves forward to experience the spectacular beauty 
                                            of Tshushay Khola waterfalls which again flows to meet the Perk River and then crosses the second iron bridge. With each step in this region there is a sharp altitude 
                                            gain. From here the next iron bridge comes after an hour of trekking over Mentogang Khola River. As soon as this 3rd or the last bridge of the day is reached, one can 
                                            be assure that the altitude of 7100 feet is conquered. From here it’s another 20 minutes trekking to the campsite zone. Since it is a forested area, tents are set for 
                                            sleeping for the night.
                                        </p>
                                        <p>
                                            It can be quite an exhausting day so it better to sleep early listening to the gushing waters of Perk River.
                                        </p>
                                    </p>
                                    <h3>Day 3: Sachen to Tshokha via Bakhim</h3>
                                    <p>
                                        Welcome the refreshing clear morning the next day and get prepared for a journey to Tshokha at 9,700 feet via Bakhim located at an altitude of 8600 feet. The scheduled trek for 
                                        the day is not long, considering its only 7kms trek, but the time taken can be way long around 5 to 7 hours, since the elevation becomes steep from this point and hiking also 
                                        gets much difficult. The hiking trail for the day starts through the forested areas towards Bakhim. One can find the presence of beautiful magnolias and Rhododendrons here. 
                                        It’s about 1 km walk from Sachen before the next iron bridge over River Perk is reached. It’s fascinating to visualise the crystal clear waters of River Perk and savouring 
                                        the mesmerizing ambience that characterises the location. From the bridge itself hikers can see the waving Prayer flags which marks the beginning of the Goechala trail. 
                                        From here one can feel the altitude gain with each step till the destination is reached. After this point there remains a steep ascent through oak forest towards Bakhim. 
                                        On the way the hikers can spot the sparkling views of Mount Pandim and Mount Tenzinkhay. The trail elevates fast here and the temperature also become much cooler. Soon 
                                        the first destination for the day Bakhim will be reached. A short break can be arranged here. There is a forest rest house located which often trekkers use for long halts 
                                        to enjoy the scenic extravaganza of Yuksom valley. Here are some excellent tea stalls and snack joints which can be enjoyed after the tiring ascent. One can refill water 
                                        in Bakhim. After having some rest, the trek towards Tsokha begins for another 2kms. The next trail is a mix of steep ascents and descents and with full of slopes.
                                    <p>
                                        Conquering 9000 feet the trekkers can cherish the view of excellent Rhododendrons and alpine berries spread across the area. Soon one can visualise the plateau of Tsokha 
                                        and the campsite for the day. There are trekkers hut in this region, which are huge wooden huts with large rooms which can accommodate at least twenty trekkers. Apart from 
                                        this there remain flat plains where tents can be placed for stay. This is a wonderful location with fascinating views of snow clad Himalayan peaks with the sight of Kanchenjunga 
                                        and Pandim dominating the view. Bathe in the serene beauty of nature and have a pleasant sleep at night in the tents.
                                    </p>
                                    </p>
