                                        mesmerizing and very rejuvenating which also helps in relaxing and gaining fresh energy for further trekking the next day. Here trekkers can find a whole lot Tibetan prayer flags that covers the 
                                        area and keeps fluttering fanatically with the breeze. The greatest highlight of this location is the sight of the impressive snow peaks of the Gigantic Himalaya range that is absolutely awe striking. 
