                                        hikers can drink in the natural beauty and refill some water for the rest of the trip. Though there are trekkers hut in Kokcharang, ther place for setting up tents is very small and congested. So without 
                                        wasting time it’s a straight walk down towards the bridge of the Prekchu River.
