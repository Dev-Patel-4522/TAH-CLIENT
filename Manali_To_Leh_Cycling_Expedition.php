                                    <p>
                                        From Marhi our next stop is Sissu via Rohtang pass. We start off our day by having our breakfast and then finishing 17km ascent to Rohtang pass. You will come across some spectacular views of snow-covered mountain peaks of Spiti Valley. 
                                        We will take a halt at Rohtang pass for some refreshment after which we'll have to take the descent to Gramphu. Sissu is also known as Khagling which is a small town in Lahaul valley on the banks of the river Chandra. 
                                        At Sissu, we'll be camping for the night where you will be served your dinner. Relax after a tiring day because you'll be up fresh the next day for new adventures.
                                    </p>
                                    <h3>Day 4 Sissu to Jispa</h3>
                                    <p>
                                        Today we'll be riding all the way up to Gondla which is at a higher elevation from Sissu. A gradual route from sissu to around 8-10 km, where it gets climb for around 20 km or so later it stretches to Jispa. 
                                        From Gondla we'll be descending all the way to Tandi. After crossing Tandi we will go past Keylong which is a low gradient all the way up to Jispa. 
                                    </p>
                                    <p>
                                        It is advisable to drink lots of water at high altitude which helps to maintain the blood circulation. There will also be a support vehicle so that you can refill your bottles when needed. 
                                        On the highway, you will come across freshwater streams which are cool and refreshing. Keylong is a village which is situated halfway between Sissi and Jispa. We'll take some rest at Keylong where you will be having your lunch and then moving forward. 
                                        As soon as you cross Keylong you will notice the change in the mountain ranges where the vegetation part is taken over by dry desert. Watch out for stones which are rolling down the road. 
                                        After a few kilometres of uphill, we finally reach Jispa. On the banks of the river Bhaga, Jispa is a peaceful village in the Lahaul valley which is located 20km north of Keylong. We will be camping here for the night. 
                                    </p>
                                    <h3>Day 5 Jispa to Zing Zing Bar</h3>
                                    <p>
                                        After having your breakfast we will be heading to Darcha which is about 10km from Jispa. The road climbs steadily on this route on the way to Patseo. You will have to be a bit careful on this road as it is rocky so it important that you maintain the speed that you are comfortable with. 
                                        You will start noticing less of civilization from here on and it will strike you that how disconnected this place is from other places. You will start seeing glimpses of the higher Himalayan range which offers a view of snow covered peaks.
                                    </p>
                                    <p>
                                        We will start gaining altitude from here on and you will feel the cold so it is important to have proper layering of clothes. The final ascent is to Baralacha-La starts at Zing Zing bar. Your campsite will be pitched at some distance from ZZ bar where you will stay for the night. 
                                        Hot soup will be provided to you before you have your dinner. It is important to get the much-needed rest before you set out for the following day.
                                    </p>
                                    
                                    <h3>Day 6 Zing Zing Bar to Brandy Nallah via Baralacha La</h3>
                                    <p>
                                        After having your breakfast we'll have to start our day early today as it is a 65km stretch. You will sense that the entire surrounding changing into a dry desert and there no signs of green. At a height of 15,900 ft Baralacha pass feels cold with strong winds blowing from time to time. 
                                        The serene flow of the river and the majestic mountains surrounding will amaze you for the rest of the journey. Take some time to click pictures and soak in the moment
                                    </p>
                                    <p>
                                        After having your lunch we'll be heading to Sarchu marks as the border between Himachal Pradesh and Jammu & Kashmir. The landscape still remains the same as earlier with tall barren peaks surrounding you. 
                                        The entire route then descends gradually to the wide Sarchu Plains. We will be camping at Brandy Nala for the night which is at some distance from Sarchu.
                                    </p>
                                    
                                    <h3>Day 7 Brandy Nallah to Pang via Nakee La, Lachulung La</h3>
                                    <p>
                                        Brandy Nala to Pang is perhaps the most difficult day when we will be riding past the Gata loops which is a series of 21 hairpin bends that cover a distance of 7km. 
                                        We'll be crossing Nakee la and Lachung la both on the same day and it would be one adventurous ride of your life. You will come across a board which marks as the beginning of Gate Loops.
                                    </p>
                                    <p>
                                        As you climb higher the loops below are visible which look flat. You will be relived after crossing the loops as it takes a huge toll on your body and your fitness is tested as well. 
                                        After having your lunch at we will go past Nakee la where the mercury level dips down. Pang will be our camping spot for the night.
                                    </p>
                                    <h3>Day 8 Pang to Debring via More Plains</h3>
                                    <p>
                                        Your day will start with a 7km ascent to Taglang La which is followed by a long descent along a steep gorge to Pang. Moray Plains which is at a height of 4,700 m is an 8km climb from Pang which known for extreme temperatures both in the morning and in the evening. 
                                        You will come across some nomadic Changpas who with their yaks. Apart from them, there is no sign of civilisation. Tso Kar lake is the first choice of camping but in case the route is close we will have to camp at We will be camping at Debring which is at Tanglang La which is the world's second highest motorable pass.
                                    </p>
                                    <h3>Day 9 Debring to Rumtse via Tanglang la</h3>
                                    <p>
                                        Today, you will be facing a challenging climb which will lead to the world's second highest motorable pass in the world, Tanglang La which. These are surely testing times and you will have to put in more effort than ever. 
                                        But what we can surely promise you is that after reaching the top, there are few feelings that can match this. Before we start our descent to Lato village, you can take some time to click pictures. 
                                    </p>
                                    <p>
                                        The first view of Lato is amazing and it will surely leave you spellbound. This will be our stop for camping tonight. You can also pay a visit to the gompas in Lato to get an insight into their culture.
                                    </p>
                                    <h3>Day 10 Rumtse to Leh</h3>
                                    <p>
                                        We will leave Rumtse after having breakfast and our final destination which is Leh will be at a distance of 75km. As we'll start approaching Leh, a stretch of dusty brown mountains will start appearing from a distance. 
                                        You have now reached the land of lamas. As you come down from Rumtse you will start noticing some vegetation before Upshi. After a while, you will start getting first glimpses of the Indus river which is quite a pleasing view.
                                    </p>
                                    <p>
                                        We will be taking a halt at Thiksey for lunch which is at a distance of 20km from Leh. After reaching the guesthouse at Leh you will actually get a sense of satisfaction that you've completed the task which you started on 
                                        Day 1 from Manali. We are sure that you will come back with lots of memories that cannot be expressed in words but will last a lifetime.
                                    </p>
                                    <h3>Day 11 Leh to Khardung La and back to Leh</h3>
