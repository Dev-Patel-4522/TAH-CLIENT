                                <h3>Overview</h3>

                                <p>Region :- Uttarakhand</p>
                                <p>Duration :- 7 Days</p>
                                <p>Grade :- Moderate </p>
                                <p>Max Altitude :- 14,000 Ft.</p>
                                <p>Approx Trekking Km :- 44 Kms.</p>
                                <p>Adorning the Lord Curzon circuit- one of Garhwal Himalaya’s most fabled trails, Pangarchulla- the chimney peak soars to vision. Perched 
                                    at 14, 700 ft., for mountaineering enthusiasts, the peak offers a perfect test of non-technical climbing skills. Though this Uttarakhand 
                                    trek is accessible all though the year, it is the winter snow that makes it truly unsurpassable. If thrill is directly proportionate to 
                                    the gauge of challenge, Pangarchulla Peak Trek in the winters would be a journey brave hearts just cannot afford to miss!</p>

                                <p>Spanning over 7 days, the trek will take you along the pilgrim vein of Uttarakhand, from Haridwar to Joshimath, where we have our base 
                                    camp. A little further away, from Dhak village, starts our walking trajectory up the rocky tracks. With ascent, the horizons open up to 
                                    cliff side villages characterized by terraced farmland and slant roofed houses, moving on to green meadows surrounded by the signature 
                                    peaks of Garhwal Himalayas. Notable are—Nanda Devi, Chaukhamba, Hati-Ghori, and Barmal among others. Part of the Nanda Devi Sanctuary, 
                                    the trail to Pangarchulla never loses sight of these celebrated peaks and culminates finally at the summit with a gala of glittering snow 
                                    views.</p>
                                <h4>Highlights of the Trek</h4>
                                <P>
                                    The Trek to Pangarchulla - the chimney peak is full of the riches of Himalayan grandeur; here is what to look forward to—
                                    <p>
                                        • A Variety of Flora and Fauna- Until the snowline starts; the track will be strewn with blood-red rhododendrons. Plenty of Oaks, 
                                        silver birch, blue pine, and bhojpatra- the bark of which is known to source papyrus, will be lining the way. Commonly found wildlife 
                                        here consists of—musk deer, black bear, and Himalayan tahr.
                                    </p>
                                    <P>
                                        • A Ridge Climb on Summit Day- A steep ridge taking you on an ascent to the peak on summit day is a painstaking but highly gratifying 
                                        feature of this trek. The climb becomes arduous and sated with adrenalin rush. The overwhelming snow on the trail will add to this 
                                        incomparable feeling of exploring the unsurpassable.
                                    </P>
                                    <P>
                                        • Unbound Snow on Risk-Free Slopes- The one dominant characteristic of your winter adventure to the Pangarchulla Peak will be the treat of 
                                        snow. In most parts on the last lap of the climb, the slopes covered in powdery snow will offer you an ascent without the risk of a deathly 
                                        fall, by providing a cushioning. These gently curving inclines have the typical quality of skiing slopes. It is ideal for you to enjoy the 
                                        delights of a snow saturated wintry Himalaya sans the risk of tumbling over fatally. It is also ideal for your enjoyment if you happen to 
                                        have experience in skiing.
                                    </P>
                                </P>
