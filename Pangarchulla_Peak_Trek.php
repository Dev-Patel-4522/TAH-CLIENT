                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 01: Drive Haridwar to Joshimath</h3>
                                    <p>
                                        Haridwar is celebrated famous pilgrim site and you should reach Haridwar railway station (which is just by the 
                                        corner of the main bus depot). Here we will meet you at 6:30 am and our journey shall commence.
                                        <p>
                                            EnrouteJoshimath, we will take the course of river Ganga and cross the famous river-bank pilgrim towns of 
                                            Rishikesh and the 5 Prayags or confluences. Rishikesh, besides its ambiance of peace is also known for white-water 
                                            river rafting. Those interested could book a stay here for a few days prior to the trek and we can pick you up on the 
                                            way to Joshimath.
                                        </p>
                                        <p>
                                            At Devaprayag, You will get a stunning view of the Bhagirathi river descending into river Alakananda and fusing to form 
                                            a vivid play of different green hues in the water. The slopes of the mountains along the fiery river look picturesque from 
                                            the confluence of Devaprayag.
                                        </p>
                                        <P>
                                            Breakfast and lunch could be found at roadside inns along the Badrinath Road and this will be exclusive of charges you pay 
                                            for the trek. By 5 pm, at the hour of sundown, we will pull in on the driveway of the Joshimath guest house, in time for 
                                            refreshing hit tea followed by an opening dinner and briefing from your Trek Leader.
                                        </P>
                                        
                                        
                                    </p>
                                    <h3> Day 02: Drive Joshimath to Dhak and Trek to Gulling top</h3>
                                    <p>
                                        Next day, by 7 am we will set off for Dhak village after having breakfast at the guest-house. From Dhak the trek will begin. 
                                        The drive from Joshimath to Dhak of around 40 minutes will introduce you to the mountain kingdom surrounding the town. Enjoy the 
                                        first sights of Nanda Devi peak during your ride. Collect your refreshments and start the trek from Dhak road-head marking the 
                                        uphill bent towards the destination. Trek for 1 hour to the first village.
                                        <p>
                                            The gradually ascending trail keeps the village on the right and takes a dusty, rocky hike of gradual ascents. Without 
                                            any major difficulties, we reach Karachi village from where the trail inclines by gushing streams. Feast your senses in 
                                            the greenery of the farming steps cut across hills and the high mountains towering over from all around.The gradual gaining 
                                            of altitude here must be supported by thorough hydration, therefore, you must keep filling your bottle at village water points. 
                                            Next, you will be reaching Tugasi village, and with that, we cover about 3 km of the whole trek. Tugasi is a picture of a typical 
                                            high-altitude Garhwali Himalayan village with their wooden houses, storage places, and ranch. You will come across shepherd 
                                            clans, farmers, and other agrarian people in this village set in the backdrop of mountains. After immersing in the different 
                                            flavor of life for some time here, we move towards our next stop- the hamlet of Gulling. During the trek, you can get wonderful 
                                            sights ofDronagiriParvat towards left.

                                        </p>
                                        <p>
                                            With the turn from Tugasi towards Gulling at 8990 ft, the scenario changes from stark and dusty to green. A thickly forested area 
                                            kicks in after Gulling and traces of civilization start to blur away. The trek continues through forest cover to the Gulling Top 
                                            campsite at 9600 ft. across 0.5 km from Gulling village. Along with this stretch, finally, the great mesmerizing snow peaks of Garhwal 
                                            Himalayas pop their heads changing the whole vista to a heavenly one. Prominent in this dazzling panorama are Dronagiri, HathiParvat, 
                                            and GhoriParvat. You can see widespread Golden Oak, Birch trees on your sides. After lunch at the campsite, the sun takes a dip and the 
                                            snow peaks take a golden sheen casting a spell of pure magic—the perfect time to catch a few exquisite shots!
                                    </p>
                                    <h3>Day 03: Trek Gulling Top to Camp-I</h3>
                                    <p>
                                        The next day’s trip is a straight ascent of approx 5 km spanning 4 hours approximately. The destination Camp I is a campsite in 
                                        the clearing amid coniferous forests of blue pine, oaks, and rhododendron.
                                    <p>
                                        We set out today after breakfast prepared mentally for a moderate hike. There will be numerous water points. The entire trail will be a muddy 
                                        route. Since the trail is under dense forest cover there is no scope for big mountain sights. Greeted with sharp curves with deep ravines gaping 
                                        at one side, we move along never feeling cheated out of the beautiful vista promised. The forests seem to smile up suddenly here and there where 
                                        rhododendrons have bloomed in thickets of pink and red. From a little inside the forested trail, snow becomes part of the scene. Rich growth of 
                                        Silver Birch and Spruce hovers over the snow-covered forest floor making Pangarchulla an in-depth snow trek. Late into the day, Camp-I is reached. 
                                        Lunch tastes extra delicious today after this testing trek. The afternoon and evening can be reserved for acclimatization relaxed walks around the 
                                        clearing. The acclimatization walk is till Khullarameadows, that takes around 40 minutes to reach there. This will be one of the best walks, with 
                                        fewer efforts you will be standing in front of an ocean of Kamet, ManaParvat, Devban, HatiParvat, Garud, Trishul, Dronagiri peaks.
                                    </p>
                                    

        
                                    </p>
                                    <h3> Day 04: Trek Camp-I to Kuari pass via Khullara and back to Camp-I</h3>
                                    <p>
                                        Starting after breakfast with packed lunch from Camp I, we make our way skirting the forest lands towards the Peak and some 40 minutes into the 
                                        trail, the scenery changes from tree covered shadowy trails to open meadows. When you reach Khullara top in another 1 hour, you follow Lord 
                                        Curzon trail towards Kuari Pass. From Khullara top you can see 3 trails heading out, the right going to Tali downwards, left uphill trail 
                                        goes to Pangarchulla, left downhill to Kuari Pass. We will take the route to Kuari Pass. Keep trudging on the Lord Curzon trail for 20 min 
                                        and open Gelgad meadows will come into sight. This is another spot to capture the mighty Himalayan peaks like Chaukhamba, Neelkantha etc. 
                                        Keep walking for another 20 minutes into the trail and you will encounter the famous broken bridge. Here you can find one water point if 
                                        luckily it is not frozen. Take a few deep breaths and get ready for steep ascent until Kuari Pass.
                                        <p>
                                            Watch out for the Pangarchulla on the left as you arrive closer to it. Some of the prominent peaks visible are Chaukhamba, Mt. 
                                            Kamet, and Neelkantha, Mana, Devban, HatiParvat, Garud, Trishul, Dronagiri.

                                            Have your lunch on the wonderful pass and return back to Camp I.
                                        </P>
                                        
                                    </p>
                                    <h3>Day 05: Trek Camp-I to Pangarchulla Peak Summit via Khullara and Back to Camp-I</h3>
                                    <p>
                                        On this big day, we start early in the morning at the crack of dawn and set off on fresh snow for the summit hike. It is necessary to be 
                                        equipped with at least 2 liters of water as there are no water sources from here. Multiple steep climbing points on the way make this 
                                        stretch laborious. The trail will be same till Lord Curzon trail and you will reach here in 1 and half hour, same as the previous day.
                                        <p>
                                            The first 30 minutes of the ascent is a thorough ridge climb which you will know has ended when a vision Pangarchulla 
                                            Main and auxiliary peaks rise right up ahead. While the climb may be strenuous the fluorescent sunrise on the left will 
                                            boost your confidence. After the ridge walk, level snow grounds spread ahead giving you a scope to catch your breath. For 
                                            this region and further up, your group may require cutting through the thick carpet of snow manually—an exhaustive task 
                                            assigned to the one who leads. In May, June the snow usually melts leaving behind a carpet of boulders. Again if there a few 
                                            traces of snow, watch out while stepping on the snow. Your foot may go deep sometimes pertaining to the rocks below. Before 
                                            reaching the base of Pangarchulla Main, enjoy this level snow patch from where you will get a terrific detailed view of the 
                                            Chaukhamba massif, flanked by Mana, Kamet, Hati-Ghodiand other snow caps. We request trekkers to not to ask for sliding as it 
                                            is dangerous on these slopes
                                        </p>
                                        <P>
                                            Your trek leader will get you all set for rope up. You will be given a choice to continue or stop here. From the base of Pangarchulla 
                                            Main Peak, once again comes a steep ridge climb. At about a distance of 100 meters, the knife ridge begins. The knife-edge cut in 
                                            the middle of the ridge should be avoided as you step. Expect high squalling wind with currents. We expect to reach the summit by 10 am, 
                                            at the most. Any more delays are not allowed because the snow begins to melt, making the reverse trail slippery. On loose snow, the micro 
                                            spikes do not work efficiently.
                                        </P>
                                        <P>
                                            At the summit point, you will come face to face with the Garhwal snow peaks in all their glory, especially Nanda Ghunti on left, chaukhamba
                                             on right, which will come to fore for the first time with all the detailed ridge lines revealed. The top is marked by a small temple. From 
                                              summit, one gets a 360-degree view of the Garhwal Himalayas, with Nanda Ghunti, Trishuli, Chang Bang, Dronagiri, GarudParvat, Lampak 1,2, 
                                              Hathi, Gauri, Mana, Kamet on the right and Neelkanth, Chaukhamba, Kedardome etc. on the left side. After enjoying your victory for 15 
                                              minutes we descend and have lunch at the base.
                                        </P>
                                       
                                        
                                    </p>
                                    <h3>Day 06: Trek Camp-I to Dhak village and drive to Joshimath</h3>
                                    <p>
                                        On the way down from Camp-I, one by one the snow peaks will pop out of view and the same old village trails will remind you of the end of the journey. 
                                        From Dhak, a car ride into the dusk will take you back to life at the Joshimath guest-house.
                                        
                                        
                                    </p>
                                    <h3>Day 07: Drive Joshimath to Haridwar</h3>
                                    <p>
                                        After sleeping over the unforgettable experience of scaling Pangarchulla, today we go back city-wards. Your car will be waiting at 6 in the morning. 
                                        Bidding adieu to the gaze of the hills, you will descend down to civilization reaching Haridwar by 6 in the evening in time for your overnight 
                                        journey back.
                                        
                                    </p>
                                    

                                    
                                    
