                                <p>The pasture uplands of Uttarakhand’s alpine heights are focal points for many of our splendid treks in the region. These are the 
                                    Bugyals—fecund grassy uplands secreted away in mountain laps where shepherds take their herds in summer days before the winter 
                                    snow renders them fallow. Dayara is a relatively lesser known Bugyal, accessible from Uttarkashi by just a short, crisp trek but 
                                    it is one that pays off magnificent rewards.</p>

                                <p>Dayara in the local tongue refers to a circular plot of land, reflecting the hoop shaped grassy turf of this Bugyal, margined by 
                                    dark oak and maple woods and beyond the green reaches, a glowing snowline of the Bandarpoonch and other ranges. A rambling walk 
                                    through the undulated pastures of Dayara will bring you out of the world frames of its green ecosystem, the whispering wilderness 
                                    of the oak woods, and somewhere far away, a breathless peak view panorama gradually unveiling. Along the entire stretch, at different 
                                    junctures, you can catch glimpses of the Gangotri set of peaks, Srikanth peak, Bandarpoonch, and Black Peak.</p>
                                <p>
                                    Another high point of the journey comes on the fourth day when we are ascending to 12, 100 ft. from our Dayara campsite to Dayara Top. 
                                    A ridge walk takes you to convergences of many adjoining meadows including the Giddara Bugyal. You will also find a descent trail to 
                                    Dodital- a fresh water lake which is the origin of Assi Ganga. The summer meadows are infused on this stretch with a spray of wildflowers, 
                                    all yellow. On all of this paradisiacal effervescence, the snow thatched Bandarpoonch will cast an iridescent impression. The walk ambles 
                                    down from here to Barsu, a pretty settlement memorable for its silkenly flowing buckwheat fields and poppy beds and a warm shelter at the 
