                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1- Dehradun to Natin Village</h3>
                                    <p>
                                        Natin, is a pleasant village famous as base camp of Dayara Bugyal trek. It is around 38 km away from Uttarkashi. The village 
                                        is flourished with lush greenery. Bhagirathi River flows throughout the way to the village. The route to Raithal is like a 
                                        pathway between the mountains. You will get first glimpse of Gangotri range, Shrikanth peak from Moryana top. Here you will 
                                        halt for a small break if you wish to capture the splendid peaks.
                                        <p>
                                            As you approach closer to Natin the views of mountain peaks get bigger, Shrikanth peak is one of the prominent peaks from here. 
                                            You will spend the night in guest house or get authentic home stay.
                                        </p>
                                        
                                        
                                    </p>
                                    <h3> Day 2- Natin to Gui</h3>
                                    <p>
                                        Start early morning with energized spirit to embark on the actual trek. The trail on this is a walk through the jungle. It is filled with 
                                        silver, gold oak trees. The route to campsite is well marked and the campsite at Gui always remains in line of sight. The trail is 
                                        ascending gradually hence it will take around 4-5 hours to reach to the next resting point.
                                        <p>
                                            The camping site is unique in a way that is surrounded by alpine trees unlike mountains. You will spot quaint village huts near the 
                                            campsite. The view from Gui is almost same as from Raithal, with clear views of Gangotri range and Shrikanth peak. Under the beautiful 
                                            night sky you can relax and re-energize yourself.

                                        </p>
                                        
                                    </p>
                                    <h3>Day 3- Gui to Chelapada</h3>
                                    <p>
                                        Today is a smaller day, just like hiking in mountains. The trail rises slowly as the forest clears and the next camping site of Chelapada will come. It will be a small hike. 
                                        You can find a flowing stream next to the camping site. You might get some snow traces on the path.
                                    <p>
                                        Bandarpoonch is highly associated with Dayara Bugyal trek, but you will also encounter many other splendid peaks. After lunch or evening tea you must stroll around and explore 
                                        the area.
                                    </p>
                                    
        
                                    </p>
                                    <h3> Day 4 - Chelapada to Dayara top and back Gui</h3>
                                    <p>
                                        Summit day finally arrived. Today you will go to the highest point and return back to the campsite. With the first few steps under forest, the trail gradually opens up, 
                                        leading to vast meadows. Hereafter, you will find yourself trudging on the green meadows blossomed with colorful flowers.
                                        <p>
                                            Walking on such beautiful terrain with stunning views of Bandarpoonch and other peaks, you will fail to notice that you have finally reached closer to the top. 
                                            You might also spot a sparsely filled water stream. The final climb is steep but it is worth the physical strain because you will end up standing in front of 
                                            the Dodital range. Bandarpoonch seems a touching distance away. The trail then takes you to the Gidara pass. Pass distinguishes ways to Pichkiya and Gidara 
                                            Bugyal. Give yourself some time to inculcate the serenity of the landscape and take back your steps to return to the campsite. This day might seem hectic, 
                                            as we try to reach campsite till the lunch time.
                                        </p>
                                        
                                    </p>
                                    <h3>Day 5 - Gui to Natin</h3>
                                    <p>
                                        The small journey in the mountains comes to an end today. Wake up early and get all packed up to store the trek memories in permanent storage.
                                        <p>
                                            The trail as you might have guessed is easy descent till Raithal. Throughout the trail, you can relive the past fond moments by just 
                                            looking behind. It seems as if the mountain peaks are calling you back. Raithal is a lively village where children and villagers will 
                                            be seen meandering around. Your overnight stay will be in Natin.
                                        </p>
                                        
                                    </p>
                                    <h3>Day 6 - Natin to Dehradun</h3>
                                    <p>
                                        Have a final cup of morning tea in Natin. Say final good bye to your fellow trekkers, exchange numbers, and lots of memories. Take a wonderful 
                                        group picture and get into the cab that will ride you back to Dehradun. This will an 7 hours ride, between the mountains, valleys and Bhagirathi 
                                        river. The river which guided us throughout the trek.
                                        <p>
                                            You will reach Dehradun by 3 to 4 PM, from here you can lead your return journey as you have planned. Make sure to take back loads of adventure 
                                            filled memories and take a pledge to come back to the mountains.
                                        </p>
                                        
                                    </p>

                                    
                                    
