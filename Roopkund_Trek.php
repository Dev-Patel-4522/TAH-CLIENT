                                <h3>Overview</h3>

                                <p>Region :- Uttarakhand</p>
                                <p>Duration :- 9 Days</p>
                                <p>Grade :- Moderate To Difficult </p>
                                <p>Max Altitude :- 15,696 Ft.</p>
                                <p>Approx Trekking Km :- 53 Kms.</p>
                                <p>Roopkund, in the state of Uttarakhand in India, is a famous glacial lake. This lake is famous due to more than five hundred 
                                    skeletons of humans that are found at the edge of the lake. It is located in the Himalayas and rests at an altitude of nearly 
                                    4,800 meters. Roopkund is located in the Chamoli district of Garhwal. It is one of the best sites for Trekking in Garhwal. It is
                                     closer to hill stations like Nainital and Kathgodam. At a distance of 217 km from Nainital and 235 km from Kathgodam, Roopkund 
                                     is one of the major adventure attractions for people in the country and also for people who are travelling from abroad. 
                                     Roopkund, which is located in the lap of Trishul massif is also known as the ‘mystery lake’. You can find human skeletons 
                                     at the bank of the lake and it is believed that they belong to the Palaeolithic age.</p>

                                <p>These are believed to be the skeletons of the heroes who had fought here in the earlier times. Along with the skeletons of humans, 
                                    you can also find skeletal structure of horses and other animals. There are reports that these skeletons belong to the 12th century 
                                    to the 15th century. It is believed by specialists that the death of many people in this region was a result of landslides, blizzard 
                                    or an epidemic. The human skeletons were re-discovered in 1942 by H K Madhwal, who was a Nanda Devi Game reserve ranger.</p>
                                <p>
                                    Later in 2004, a team of Indian scientists along with some Europeans visited the area to gain more information about the nature of 
                                    the skeletons. On extensive research and findings, they found human skulls, bones and other jewellery. The DNA of the skeletons was 
                                    taken, and it appeared that the people belonged to many classes and showed different features and morphological structure. There was a 
                                    group of short people and tall people who were believed to be closely related to each other. Though the exact number of the skeletons 
                                    cannot be ascertained, but a total number of around 500 hundred skeletons were found. It is also believed that an equal number of 
                                    skeletons perished due to the natural environment.
                                </p>
                                <p>
                                    The Radiocarbon dating of the skeletons at the Oxford University Radiocarbon Accelerator Unit determines the age of the skeletons to be 
                                    somewhere around 850 AD. Later extensive research has been carried out in various parts of the country and the world. Many scientists are 
                                    of the idea that the people did not die of an epidemic but due to sudden hailstorms. The hailstorms are supposed to be as big as the size 
                                    of a cricket ball, and as there was no shelter in the open areas of the Himalayas, people might have come under its disastrous and direct 
                                    effect. It is considered as one of the strongest reasons for the extinction of the natives of this area.
                                </p>
                                <p>
                                    The cold and icy weather condition can be said to be the reason for the preservation of the skeletons for such a long time. The favourable 
                                    weather conditions prevented the skeletons to decay off and decompose into soil. With landslides that were rampant in the area, many bodies 
                                    slipped into the lakes and stayed there for a long time. The bodies that fell into the lake had a greater shelf-life as compared to the 
                                    ones which remained in the open. However it is still not determined if this group of people lived here, or if they were heading towards 
                                    a destination. Historical evidence of any trade route towards Tibet has not been established. Therefore it is hard to decipher the real 
                                    purpose of the group of people that were victims to this natural calamity. There are possibilities that the group may be heading towards 
                                    the Nanda Devi pilgrimage where Nanda Devi Raj Jat festivities take place once every 12 years.
                                </p>
                                <P>
                                    Lohajung is the starting trekking point for Roopkund. The Roopkund trek can also be called Skeleton Trek due to the presence of different 
                                    skeletons in and around the lake. The lake is surrounded by snow clad mountains and rock- strewn glaciers.
                                </P>
                                <P>
                                    Trekking in India and choosing the Roopkund trek is an adventurous journey which passes through many exotic and beautiful locations. As 
                                    you trek towards the lake, you would find lush green grassy land and conifer forest ranging towards the fall of hills. Thereafter the trek 
                                    takes its way along the Pindar River. You can enjoy the natural and lush green beauty of the surroundings with the pleasant breeze that 
                                    blows all around you, giving a soothing effect to your senses.
                                </P>
                                <P>
                                    Roopkund is a picturesque, beautiful and attractive tourist destination in the Himalayas. Located at the base of two Himalayan peaks, 
                                    Trishul and Nandghungti, it attracts people who have an adventurous side to life. There are religious festivals that take place in autumn, 
                                    held at the alpine meadow of Bedni Bugyal, where the nearby villages participate. The lake is covered with ice all the time and it adds to 
                                    its beauty. It is an enjoyable travelling destination as one is surrounded by mountains from all sides. Trekking in Uttarakhand also gives 
                                    you the chance to trek the Himalayas. You can also go for the Nanda Rajjat Yatra.
                                </P>
                                <P>
                                    There are many routes for a trek to Roopkund. One of the most common routes taken by travellers and adventurers is through Lohajung up to 
                                    where you can travel by road. Trekking begins after this point. There are also some plains or flat areas where you can camp during the 
                                    night. If the weather is not foggy, you can easily see Bedni Bugyal and Trishul. On your way to Roopkund, you would also find many grazing 
                                    lands for horses, sheep and mules. You would come across a few temples and lakes on your journey. Many waterfalls are also visible on the 
                                    slopes of the surrounding mountains. However, it should be kept in mind that the weather is generally hostile throughout the year and 
                                    travellers need to be fully equipped and prepared for these conditions
                                </P>

