                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 01: Pickup from Kathgodam in the morning and reach Lohajung (2300mts, 7500ft):</h3>
                                    <p>
                                        The journey will take not more than 10 hours, which includes one or two breaks. The serpentine road goes through the 
                                        region of Kumaon and covers deep valleys of Shivalik mountain ranges. The road will take you to Gwaldam which lies under 
                                        the administrative district of Garhwal. The road goes through coniferous forest and unrelenting curves. Though there is a 
                                        direct road from Gwaldam to Deval, the final destination of the day, it is not preferred due to damages at various places 
                                        due to the yearlong rains. Via Nandkesri the distance to be covered is less, around 11kms, but it is better to walk on a 
                                        longer, but safer trail through Tharali, which is around 36kms. Around 2 hours of the ride will take you to Deval, a village 
                                        with all provisions of purchasing food and kerosene required for the rest of the journey. The day's last stretch of the journey 
                                        is from Deval to Lohajung. The extremely curvy and winding road gains considerable height. The roads are so thrilling to drive on 
                                        that sometimes even the slightest of oncoming cars and trucks send Goosebumps. It is generally rainy and foggy during most of the 
                                        time and temperatures remain very cold. The night is spent a tourist rest house or nearby lodges.
                                        
                                    </p>
                                    <h3> Day 02: Lohargunj to Didna Village</h3>
                                    <p>
                                        The actual trek begins today. After a good home prepared breakfast, the walk starts on the level ground with slight ascent on 
                                        the jeep road; the road goes till the village of Wan. An hour and a half walk on the flat surface will take you to Kulling, which 
                                        is located at a distance of around 6km form Lohajung. Kulling is a peaceful village in the district of Garhwal, with a small pool of 
                                        the population; it is filled with the beauties of nature. Exotic birds can be spotted very easily; the air feels crisp and fresh. 
                                        After a break at Kulling, you take a right turn and gradually descend towards the village. At the start, you will notice a narrow 
                                        concrete plain which cuts through houses and fields and takes you to a small stream. This small stream is fed by Neel Ganga. You 
                                        descend further till you reach the bed of Neel Ganga. Go ahead and cross the cemented bridge and then start climbing the ridge on the 
                                        right side of the bridge. The road ahead is moderately steep and ends at the village of Didna, situated at an altitude of 2450mts, 8000ft. 
                                        The trail encompasses evergreen alpine forests and trees which are huge in size. After a climb of around two hours, you reach the top 
                                        of the ridge; the view ahead is magnificent. It is surprising to find huge open lands at such a high altitude on the lap of the mountains. 
                                        There are two villages very near to each other Didna and Kuling, basically they host the same type of people and trekkers. The first 
                                        village is habited during the summer and monsoon months while the second one is popular during winter. The days trek is over at around 
                                        3pm, after which a hot lunch is enjoyed. Total distance covered during the day is approximately around 10kms, and the night is spent in 
                                        a traditional guest house.
                                        
                                    </p>
                                    <h3>Day 03: Didna village to Ali Bugyal</h3>
                                    <p>
                                       Today's trek involves trekking through the beautiful village of Ali Bugyal, which located at an altitude of 3400mts or 11000ft, is one of 
                                       the highest inhabited locations of the country. Ali Bugyal is on top of the mountain and is clearly visible from the backdrop of Didna. The 
                                       trek will take you to climb on top of this mountain, after the start the trail will take out of the village. On the right, the trail is 
                                       slightly ascending with a very gentle gradient. On the way, temporary settlements of local shepherds known as Tolpani and their huge herds 
                                       of sheep are a site to behold. They are very friendly and warm in welcoming people; you can even share their lunch, which they think as a 
                                       friendly gesture. On the other hand, the trail on the left side is short in length but is steeper. But it doesn't really matter which 
                                       trail you take, eventually both of them will take you to the same destination. Oak, Birch, and Rhododendron trees are the only trees that 
                                       grow in these areas, and are magnificent and huge in size. According to locals, anyone visiting the place during the spring season can see 
                                       the unbelievable view of the valley covered with thousands of Rhododendron flowers in vibrant colours. After reaching the mountain top, the 
                                       tree line gradually recedes, and the plain grasslands start. The climb takes around 3 to 4 hours, after which right in front of your eyes, 
                                       the Himalaya's most attractive meadows, Ali Bugiyal welcomes you. The view is very similar to the default wallpaper of Windows XP and is a 
                                       treat for the eyes. During summer months, wild horses and cows graze the fields which are filled with vast stretches of grass and flowers 
                                       of various bright colours. On a clear weather, it is easy to spot the peaks of Trishul and Mrighthuni, the tallest mountain peaks in this 
                                       part of the Himalayas. The mighty Trishul beckons to be climbed upon and feels as there is some hidden energy which attracts towards itself. 
                                       After crossing the meadows, there comes a small face on the way, and another half an hour of trekking will take you to Bedni, the camping 
                                       spot for day 3. After lunch, visit the nearby temple in Bedni Kund, if the weather is clear you might be able to view the Garhwal Himalayan 
                                       ranges at its glory.
                                    

        
                                    </p>
                                    <h3> Day 04: Ali Bugyal to Ghora Lotani</h3>
                                    <p>
                                        Today being the 4th day of the trek you can have the choice of starting a bit late and sleeping a little bit extra. Your next camping location 
                                        is Gora Lutani, which is a perfect camping spot. Keep on walking and enjoy the picturesque locations in the adjoining area. Along the way, 
                                        you will come across Patat Nacholi, a rather unused and vest able landmass. This day you will make steep altitude gain from around 12,500ft 
                                        to 14,500ft. The high altitude and the long continuous walks will make your legs hurt, and you will also start feeling the effects of the 
                                        weather changes and the thin air will make breathing difficult. But don't worry; the wonderful surroundings will give you enough motivation 
                                        to reach your destination. The positive vibes flowing in the air lets you forget all your troubles. After a travel of around 10kms, the 
                                        campsite for the day is reached. Ghora Lotani will give you a good night's sleep, but the next morning waking up early is very crucial as 
                                        it is going to be an exciting day ahead.
                                        
                                    </p>
                                    <h3>Day 05: Ghora lotani to Bhagwabasa</h3>
                                    <p>
                                        Wake up early and brace yourself for the longest day of trekking. Have a filling breakfast and don't forget to do a few stretches. It 
                                        should be kept in mind that today; the trekking will approximately cover 10kms and that too on a steep slope. Walk towards Bagubasa, 
                                        located around 14000ft above the sea level. This road will also take you across Kalyuvinayak temple. The most thrilling part to see the 
                                        temple is to travel up 2kms, also equivalent to gaining an altitude of around 200ft. The path leading to this temple is very uneven and 
                                        zig-zag in shape. It is essential that proper safety gear and clothing is ensured before the day's trek is even started. The walk should 
                                        be done very slowly and carefully; one small slip and anyone can get hurt very badly. But since there is no gain without pain, the gifts 
                                        await you at the top. It is difficult to understand how someone could create the temple at such a height. The temple is basically 
                                        traditional in design and construction and has been there for centuries. As the altitude increases, the air gets thinner, and the quantity 
                                        of oxygen also decreases, this might lead to headache and acute stomach pain, but don't worry, take some rest for a while. The temple is 
                                        carved out of stones with ultimate precision and perfection; it installs divine intervention in your minds. From the temple, you can view 
                                        Roopkund, the final trekking destination. The view is just magnificent one with shining and sparkling Trishul on one side and bell shaped 
                                        Nandagunti on the other. You further follow up the uneven trail leading to Bhaguabasa at 1km distance from there. The region has its own 
                                        specialities, flowers that bloom only during the monsoon months. You currently stand at a very high altitude of 14000ft and the terrain 
                                        seems to look rough and not so easy to traverse.
                                       
                                        
                                    </p>
                                    <h3>Day 06: Bhagwabasa to Roopkund and back to Bedni Bugyal</h3>
                                    <p>
                                       Start early on this day to reach the mega destination – Roopkund. At an altitude of more than 16000ft, it is surely the highest point on the 
                                       trek. Though the path is not that complicated to climb, but even the 4km walk will take a breath out of you and tire you like hell. 
                                       But no trekking is easy, right? During monsoon, the snow covering this area is very less and shallow, which makes the path reveal itself 
                                       properly. From Bhaguabasa, your first checkpoint is Chhirianag which means ladder like snake. From this place, the next point lays Roopkund – 
                                       The mystery island. The lake of skeletons is one of the most mysterious places in the world. Till date, even archaeologists, and scientists 
                                       are not able to find out how there were so many skeletons on the bed of the lake. There are theories and fables which state that once a king 
                                       and his fellowmen were going to Nanda Devi, and, unfortunately, a severe hailstorm led to their deaths. If you are extremely lucky, then 
                                       from the edge of the lake you can clearly see somewhere around 300 human skeletons. This view in itself is breath taking and people go 
                                       crazy after seeing this. From this place, the trek will be along a hard and steep terrain. After the steep climb gets over, you will find 
                                       the Nanda Devi temple. This is very near to the Nanda Devi Biosphere reserve. A perfect site for taking good snaps with friends and other 
                                       fellow trekkers. Depending upon the weather and the co-climbers conditions may decide to risk on the Junargali pass which stands 16700 feet 
                                       from sea level. Without any meteorological interference, you would need about 45 minutes to climb. You will have to pass through Bhagwabasa 
                                       on the return journey. These places are filled with sheer exhilaration and thrill. The descent along the path is twice difficult that of 
                                       the ascent it is so steep that one might have the feeling of falling down from the top. Move along to discover a cave like structure called 
                                       as Bhagwabasa or the abode of the tiger. You further climb down to reach PatarNachuni about 12500 above ground level. You continue 
                                       backwards through Ghora Lautani back to Bedni Bughyal. Your day will end at the campsite amidst lush greenery. This trip is one of the 
                                       longest, covering over a distance of 15km taking about10-15 hours for completion. The legs will push you to deep sleep as they need a 
                                       lot of rest. But before going to bed, do realize the whole trek was not at all easy and requires a whole lot of determination and courage 
                                       to complete it.
                                        
                                        
                                    </p>
                                    <h3>Day 07: Bedni bugyal to Loharjung</h3>
                                    <p>
                                        Day 7 is probably the last day of the trek where you will have to walk and return to the camp site at Day01. Descending and climbing up 
                                        are two things that are very different, though the path remains the same. This time you travel on the right side of the trail and it seems 
                                        a simple path as compared to the previous day's strenuous affair. For the first time, you will see wooden huts dotted along the path. After 
                                        travelling a little more, you are now able to see the road dividing itself into two lanes. You need to take the one which leads to right. 
                                        It goes down and down, and as you descend further you can again see that you have entered the forest region with trees all along the lane. 
                                        The bird tweeting and the cool aiwin past your ears are a treat that you will rarely get to feel. Due to the varying slope, trekkers are 
                                        cautioned to take each and every step very carefully; otherwise one might end up hurting themselves. Shortcuts though may look rewarding 
                                        must be avoided as much as possible as the terrain is pretty rough. After walking a few more yards you near an opening inside the dense 
                                        forest called the Gahroli Patal. You continue our journey till you reach the banks of the river of Neel Ganga. Once you reach the river 
                                        bed, your journey continues, till you reach the wan village called Ran ka Dhar. The village of Wan is the next stop after which you descend 
                                        further. Wan feels like very much at home, a big village with hills and mountains covering the edges, a view to cherish all your lives. 
                                        As you travel a few more yards at the road and you find the car waiting to pick you up. The distance covered for the day for the descent 
                                        is nearly 18km and requires about 10 hours. It needs an hour from Wan to reach Lohargunj. The road taken by car is rocky. Once you reach
                                        back the tourist guest house you can rejoice all the memories of the trip and comfortably sleep on the warm bed.
                                        
                                    </p>
                                    <h3>Day 08: Loharjung to kathgodam</h3>
                                    <p>
                                        Wake up early in the morning; have a nice cup of tea and an awesome hillside breakfast. Today you will leave for Lohargunj, along with all 
                                        the beautiful and unforgettable memories pack your own stuffs and luggage. Your cab will drop you at the Kathgodam railway station. The car 
                                        journey will take something around 10 hours, and do make sure that you are carrying mint and tablets which prevent nausea. From Kathgodam 
                                        railway station, you will find trains to various locations. Board your respective train and take along a healthy experience with memories 
                                        that will never fade away.
                                        
                                    </p>
                                    

                                    
                                    
