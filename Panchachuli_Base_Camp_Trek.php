                                    of deodar and Bhojpatra and conifer. The five peaks of Panchachuli create the unforgettable specter of Him Khand, Yuli river 
                                    skirted by the forests of Devdaar and Bhoj Patra and conifer trees and vast meadows. Description of this trek on papers is 
                                    complex and would be injustice to the scenery that unfolds in front of the eyes of the beholder, it can only be experienced.</p>
                                
