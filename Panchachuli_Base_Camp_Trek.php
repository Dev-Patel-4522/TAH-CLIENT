                                <p>Panchachuli Base Camp is one of the exciting base camps that set out its marquee in a 40-kilometer trek 
                                    starting from Dar to Panchachuli Glacier. Nestling snugly in the lap of the snow capped peaks of Panchachuli, 
                                    the Darma valley in Pithoragarh district is endowed with abundant scenic grandeur, The route to the glacier is 
                                    abound in a splendid combination of enthralling scenery, snowcapped mountains, gushing streams, Alpine meadows 
                                    and a rich variety of flora & fauna. Situated in Eastern Kumaon Himalaya, the trekking route forms the watershed 
                                    between the Gori Ganga and Darma Valleys. People come to view the ethereal beauty of the nature, reasons don’t hold 
                                    much importance, as the Himalayas foothills are just the perfect alternative to experience adventure. Panchachuli 
                                    Glacier lying on the laps of Kumaon Himalayan range in the Darma Valley, near the borders of Nepal and Tibet an area 
                                    of outstanding beauty .The entire trek to the glacier is a walk along the river Dhauliganga .</p>

