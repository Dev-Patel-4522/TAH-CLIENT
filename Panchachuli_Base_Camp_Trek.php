                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1: Kathgodam to Dharchula 9hrs. (290km.)</h3>
                                    <p>
                                        It’s a whole day’s drive to Dharchula from Kathgodham. The picturesque ride through the Pitholagarh district of Uttarakhand 
                                        passes through the towns of Bhimtal, Almora, Dhaulchina, Sera- gat, Chaukhori, Thal, Ashkot. The lofty Himalayan ranges and 
                                        the gorges below, native villages and the sheer beauty of the forests are you company all through the drive to Dharchula. 
                                        Dharchula which is almost straddling India and Nepal across the river Kali is a picturesque isolated remote valley village 
                                        amidst the snow capped peaks. It served as an ancient trading route between India and Nepal. Overnight stay at a homestay in 
                                        the Dharchula village.
                                        
                                        
                                    </p>
                                    <h3> Day 2: Dharchula - Dar - Urthing.</h3>
                                    <p>
                                        Dar is the last road head in Darma Valley for this trek. From Dharchula it is a 3 hour drive to Dar along a windy road through 
                                        the small tribal settlements Tawaghat and New Sobla. From Dar it is a 3-4 hour walk to reach the campsite of Urthing throught 
                                        Bongling forest. The trek to Urthing is along a narrow cliff edge with the Dhauliganga river gushing below on your right. The 
                                        path winds itself on a rock cut stretch to reach the village of Bongling. It’s a level walk with short climbs in between to the 
                                        reach the campsite. Overnight stay in camp.
                                        
                                    </p>
                                    <h3>Day 3: Urthing - Naagling.</h3>
                                    <p>
                                        It’s an easy walk with short descents and climbs. The mule track from Urthing to Naagling goes through hill sides of green pastoral 
                                        land, with Dhauli Ganga flowing below cutting through the gorges. The trail passes through the green meadows of the inner Himalayan 
                                        ranges and a suspension bridge across the river to cross over to Sela campsite. The campsite is on the outskirts of Naagling on the 
                                        green carpet alongside the river. Overnight stay in Camp.
        
                                    </p>
                                    <h3> Day 4: Naagling - Son</h3>
                                    <p>
                                        The trail to Naagling goes along hill sides and is a rocky terrain for the first 3 kilometeres. We then enter the coniferous forests of 
                                        high oak and birch trees and further along we reach the table top meadow of Nagling. The view from this vantage point is a sight to behold. 
                                        Surrounded by the mighty Himalayas, the views of the distant glaciers is exciting. From the meadow Baaling is another 3 kilometre walk 
                                        through the vegetation lands of the tribals. Son is a picturesque mostly uninhabited settlement of the Darma Valley. Overnight stay in Camp.
                                        
                                    </p>
                                    <h3>Day 5: Son – Panchchuli Base camp (4260 Mts)</h3>
                                    <p>
                                        The walk to the Base camp is along a narrow trail through the vast expanse of the Dantu Bugyals. With the forest cover to reach the glacier, 
                                        on one side and the Meola river bed on the other it’s as steep descend to PCBC. The trek to PCBC itself is very picturesque with the bugyals 
                                        stretching far and wide on one side and the forests of birch and conifer on the other.
                                        <p>
                                            Wild flowers and wild strawberries grow on either side of the narrow path which has steep climbs and descends till we reach the 
                                            PCBC. With the peaks towering above and endless stretches of glaciers, the place itself provides an incredible scenery of a sprawling 
                                            meadow bordered by tall oak and birch trees with the Panch Chulli (6904 mts./22652 ft.) towering in the background. We retrace our path 
                                            back to Nagling and camp for the night. Overnight stay in Camp.
                                        </p>
                                       
                                        
                                    </p>
                                    <h3>Day 6: Panchachuli Base Camp- Naagling.</h3>
                                    <p>
                                        In the morning we will trek down to Bungling. The trek is easy to downwards. Dinner and overnight stay in tents.
                                        
                                        
                                    </p>
                                    <h3>Day 7: Naagling to Urthing 12km.</h3>
                                    <p>
                                        In the morning we will trek down to Bungling. The trek is easy to downwards. Dinner and overnight stay in tents. Back to the Naagling all 
                                        the way to same back from PCBC.
                                        
                                    </p>
                                    <h3>Day 8: Urthing to Dar drive back to Dharchula.</h3>
                                    <p>
                                        This is our last day of trekking. We will trek down 5 kms to reach Sobla and from here drive to Dharchula. Dinner and overnight stay in hotel.
                                        
                                    </p>
                                    <h3>Day 9: Dharchula to Kathgodam via Almora (8hrs.) 290kms</h3>
                                    <p>
                                        We drive down to Kathgodam and reach by evening to take the overnight train/bus to New Delhi.
                                        
                                    </p>
                                    

                                    
                                    
                                    <h4>How to Reach</h4>
                                   
                                    
                                    <h3>By Train :-</h3>
                                    <p>
                                        The Kathgodam station is one of the main stations in the district and it is an old station. It has good connectivity to many 
                                        places and these include Lucknow, Delhi, and Howrah. Tickets from Delhi to Kathgodam by train cost around Rs. 180 for a second 
                                        class sleeper ticket and Rs. 450 for a third A/C sleeper ticket. It is essential to book the tickets at least one month in advance 
                                        as this route is always in demand.
                                        <p>
                                            From Delhi take the Ranikhet Express (5014) Dep. Delhi at 10:40pm arrival Kathgodam at 5:30am (Overnight Journey) or the Uttar sampark kranti (5035) Dep. 
                                            Delhi at 4:00pm arrival Kathgodam at 10:40pm (6 hrs.40min.Journey).
                                        </p>
                                        <P>
                                            From Dehardun take the Doon Express (4120) Dep. Dehardun at 10:30 pm arrival Kathgodam at 7:10 pm (8hrs.journey).
                                        </P>
                                    </p>
                                    <h3>By Bus :-</h3>
                                    <p>
                                        Regular buses ply from ISBT Delhi Anand Vihar station to Kathgodam. Buses that go to Nainital stops at Haldwani which is a twin town of Kathgodham (8 hrs. Journey).
                                        Usually buses drop you at Haldwani bus staiton. From there you have to come to Kathgodham Railway Station which is 4km away.
                                        TAH’s Tata Sumo or similar vehicles will pick-up you from Kathgodham Railway Station
                                        We would suggest you to take only government buses from ISBT Anand Vihar. Private buses ply from outside ISBT and they are not trust worthiness.
                                        
                                    </p>
                                    
