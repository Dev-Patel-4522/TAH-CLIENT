                                    <p>
                                        A 5-hour trek to Taluka sets off this morning, climbing down by the familiar trail, stopping at familiar rest points. The car waits 
                                        at the end of the dirt road to drive back base camp-wards. Pay special attention to descending tactic. Though climbing down accounts 
                                        for lesser exhaustion, stressing out your soles with big steps is inadvisable. Sankri will arrive at the time for te
                                        
                                        
                                    </p>
                                    <h3>Day 7 - Sankri to Dehradun</h3>
                                    <p>
                                        The journey concludes with the 10-hour drive from Sankri to Dehradun. Breakfast and lunch will be on the way. Reaching time 
                                        at Dehradun station will be 6 pm, suitable for an overnight journey to Delhi.
                                        
                                    </p>
                                    

                                    
                                    
                                    <h4>How to Reach</h4>
