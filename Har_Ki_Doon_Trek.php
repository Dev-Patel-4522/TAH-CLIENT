                                <p>A winter wonderland and a trekkers delight, the Har Ki Doon Trek is a stairway to heaven that will make you fall for its beauty. 
                                    The thing that makes it an alluring trek is its accessibility both in summer as well as the winter season. Har Ki Dun valley is 
                                    situated at the western flank of Garhwal in the Uttarkashi district is very popular to the trekkers as well as that to the climbers. 
                                    It is a lovely walk to the Har Ki Dun valley with fascinating views of Swargarohini group of peaks. Har Ki Dun catchment area is a 
                                    major source of Yamuna River System and the main source of the largest tributary of Yamuna.</p>

                                <p>It takes you through some of the most charismatic alpine meadows and pine forests which is a treat to the eyes. During winters the 
                                    snow-covered trails look mesmerizing as and when the sun tries to peep behind the clouds. One of the oldest Treks in the Himalayas, 
                                    known as the 'Valley of Gods', some believe it's the same route taken by the Pandavas in the Mahabharata on their ascent to heaven. 
                                    Swargarohini—the snow peak, the vision of which dominates this trek bears a legacy of the mythical stairway to heaven.</p>
                                <p>
                                    It's a delightful experience when you're camping near the flowing river, the forest here is rich in flora and fauna which has an 
                                    engaging vibe of its own. You are able to see Swargarohini – I, II, III, Bandarpoonch, and Blackpeak from here. The Har Ki Dun trail 
                                    takes you through some of the ancient villages that are 2,000 years old. It gives you an insight into the lives of the locals who are 
                                    residing here in the valley. This trek is heaven for nature lovers, adventure seekers and also shutterbugs. You can even spot various 
                                    species of birds that thrive in this region. Capture some thrilling pictures on the trail which will last a lifetime.
                                </p>
                                <p>
                                    Other attractions include, the temple at Osla which is dedicated to Duryodhana, was built by the inhabitants of Saur. It is about 13km 
                                    from Sankri which lies on the steep mountain trail to Har Ki Doon. It is a 5,000-year formation of wooden chambers that are carved 
                                    elegantly.
                                </p>
                                <p>
                                    The challenge of Har Ki Doon, besides the long hours of trekking for 6 long hours is fighting with frequent rain. The peak time of the 
                                    year for this trek is not winter after a snowfall but in summer frequented by rain. Proceeding through green foliages dripping with pearls 
                                    of rain is a beautiful experience but trekkers must be well equipped for it. Rain trekking essential like a poncho, waterproof covers, 
                                    anti-diarrhoea medicines, and water resistant trekking boots is a must before for this trek. Whatever the conditions are, this trek surely 
                                    gives you vibes of visiting a wonderland.
