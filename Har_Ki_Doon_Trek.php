                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1 - Dehradun to Sankri</h3>
                                    <p>
                                        The first day begins with a 10-hour drive from Dehradun to Sankri. You are expected to reach Dehradun railway station 
                                        by 6:30 in the morning. To reach on time, you can book an overnight Bus/Train from Delhi to Dehradun ISBT.
                                        <p>
                                            Passing through Mussoorie- the “Queen of the Hills” on to Uttarkashi district, we will cross Naugaon, Purola, 
                                            Mori, and Netwar before finally reaching village Sankri- the first stop on the trek. The road is through and 
                                            through picturesque with pine tree forests starting on both sides of a road that catches up on a parallel to 
                                            the course of the river Tons. Stopping at a favourable hour, we will have lunch somewhere by the road at an inn. 
                                            Reaching Purola, you should get done with important calls and internet usage because hereafter, the connection 
                                            will get fuzzy. BSNL is the only efficient service available in this zone with limited providers.
                                        </p>
                                        <p>
                                            A short way before Sankri, we will cross the Govind National Park checkpoint and at about 6 in the evening, 
                                            the drive will end at Sankri. After freshening up at the guest house, you will get sufficient time to explore 
                                            the market close by. Trekking essentials, garments, and equipment are available at the bazaar in Sankri, in case 
                                            you need last minute shopping for the venture ahead.
                                        </p>
                                        
                                        
                                    </p>
                                    <h3> Day 2 - Sankri to Pauni Garaat via Taluka</h3>
                                    <p>
                                        On day 2, we start on our drive from Sankri to Taluka at 7 in the morning. This short drive through gradually thickening 
                                        forests and streams cutting through the path over boulders will introduce us to the terrain of our trek closely.
                                        <p>
                                            Reaching Taluka, another village of the same lineage found on this trek, trekkers will be invited to collect refreshments 
                                            before getting started on the hike. The first lapse is a descent to the banks of the beautiful Tons and keeping the river 
                                            on the right, the trail enters a level walk through forests of conifers until reaching the first cemented bridge.

                                        </p>
                                        <p>
                                            Crossing the stream, the trail leads up to a wooden bridge where a gentle slope goes down to the waters. Here you can 
                                            fill your bottle and move ahead. The whole way along the river is ideal for photo compositions and provides shade too. 
                                            We approach the rest point for lunch by the Supin tributary that we crossed before at around 1 in the noon. At a rocky 
                                            height from the banks of the rivulet is a food shack, one of the many that we are going to find on our trek to the valley. 
                                            These stores are with packaged food and beverage along with delightful hot snacks. In addition to our packed lunch, this 
                                            will add a different twang of delight to outdoor meals on the move.
                                        </p>
                                        <p>
                                            Progressing ahead, harvest fields of potato, millet, and maize will fall on the way giving the whole stretch a ranch house 
                                            feel. Pauni Garaat, a green clearing on the banks of Supin at its sprightly best is arrived at late in the afternoon as the 
                                            Sun begins to dip—welcome to the first camp!
                                        </p>
                                        
                                    </p>
                                    <h3>Day 3 - Pauni Garaat to Kalkattiyadhaar via Osla</h3>
                                    <p>
                                        Pauni Garaat to Kalkattiyadhar is a distance of 7 km over 6 hours which we start the early morning at 6. The trail crosses 3 “garaat” 
                                        or hydroelectricity run chaffing units which are used by the villagers for separating husks of millet and maize followed by grounding 
                                        to flour. The idyllic vibe retained in the surrounding, the trek proceeds through harvest lands, cattle grazing, and slant-roofed huts 
                                        used as granary and storages. Right between the forests of cashew nuts and walnuts lies the serene village Oslo. This village is said 
                                        to be a preacher of Duryodhana of Mahabharata. Well, apart from the stories, the village is blissful having 2000 years old Temple and 
                                        a few houses. The villagers are welcoming here. After a small chit-chat proceed further on the ascend.
                                    <p>
                                        On this incline, you will get fabulous viewpoints to steal glimpses of the Dhauladhar Range out front. Snaky trails to Borasu Pass and 
                                        Baspa Valley can also be made out from here. The high trail winds further down reaching a tabletop green stretch—Kalkattiyadhar. This 
                                        is the campsite for day 2.
                                    </p>
                                    

        
                                    </p>
                                    <h3> Day 4 - Kalkattiyadhar to Har Ki Doon & back to Kalkattiyadhar</h3>
                                    <p>
                                        Today you will start early as compared to other days. However, mesmerizing the views were until today, get ready to encounter something more. 
                                        The trail will go into the glacier basin having moraines and alpine meadows. Tall pine trees will be greeting you at every step.
                                        <p>
                                            The trail is mixed of steep and moderate ascend until the Har Ki Dun valley. Just as you rise high you can see snow patches and frozen waterfalls. 
                                            In Winter these sections are covered with thick snow. A valley is a heavenly place displaying immense beauty in one sight. 
                                            The huge peaks of Swargarohini, Hata Peak, Black Peak etc show their marvelling glimpses. The valley is an amazing amalgamation 
                                            of meadows, frozen streams, coniferous forests, glacier moraines, the backdrop of huge peaks.
                                        </p>
                                        <P>
                                            Try to engulf everything. After exploring the valley creating memories for a lifetime we return back to our last campsite at Kalkattiyadhar.
                                        </P>
                                        
                                    </p>
                                    <h3>Day 5 - Kalkattiyadhar to Pauni Garaat via Osla</h3>
                                    <p>
                                        From today the trek is actually the return journey. Today we shall retrace the steps through the Oslo village to Pauni Garaat. Here's another 
                                        chance to relive the moments with the peaceful villagers. Say goodbye to them and the stunning sights around you.
                                        <p>
                                            Follow the same trail as of earlier and reach the campsite of Pauni Garaat. Enjoy your last few moments of nightlife in the mountains.
                                        </p>
                                       
                                        
                                    </p>
