                                    <p>
                                        Today's trek will start for Tunda-Bhuj which is a steady climb out and form there you can view the Parvati River coming down from mountains left side. The track will move through long meadowland and chirping of birds will give you gleaming happiness. 
                                        The road from Khirganga is the first stream crossing start with a constant ascent for about 20 minutes. Then you can see the temporary bridge over the edge of the mountains. There in route you can see small tent where the local wholesaler sell products. 
                                        After 30 minutes you will way out from the forest area and will see meadow land and immense sight of the dell. Now you will trail the high peak of the hill. Once you arrive at the top, the follow will twirl within the hilly area and you see the huge area of the Parvati valley in front. 
                                        This will be the final overpass that you have to cross and you will reach Tunda-Bhuj situated at high altitude of 10,500 ft above sea level. There you can see huge mountains covered with snow and the sight view looks very preety.
                                    </p>
                                    <h3> Day 4:- Tunda Bhuj to Thakur Kuan</h3>
                                    <p>
                                        As walking down you will reach to pastureland through opaque mixed jungle with profusion of lovely flowers and Greenery Island. The landscape view of Tunda Bhuj is very easy but the distance is some tuff as the bridge is very long to cross and time consuming. 
                                        Crossing the bridge to the further side of the stream take time to follow up the peak. It takes 15 minutes to reach the steady climb as you pass through an inclined rocky meadow land. Trekking in Manali discover the prettiness of lovely landscape view and enhance the beautiful picturesque. 
                                        As you start for further trail you will go upto high level and the landscape view can be see very striking. After the early rise crossways the Pandu Bridge, walk down towards the Parvati River at your right takes 15 minutes passing through the field's. 
                                        Many steer can be seen with their group on the way by evening time, you will reach the Thakur Kuan which is situated at high altitude on 11,000- ft high above the sea level.
                                    </p>
                                    <h3>Day 5:- Thakur Kuan to Odi Thach</h3>
                                    <p>
                                        The next morning you will feel great after taking good rest as you have to get prepared for the adventures which lie ahead. As this is the easiest day of your trek it won't take a huge toll on your body. You will cross the terrain region and then cross multiple streams coming in your way. 
                                        Further, you will find Parvati River on left part which looks very attractive. Then you will follow the big forest area and meadow land which can be seen aside from the Parvati River and enter the Thakur Kuan region which is surrounded by the pasture of orchard flowers and small trees. 
                                        The Pandu bridge which is the most tricky patch comes after 1.5 hours of trek. It is recommended that all trekkers stay together in a group while crossing this natural bridge.
                                        <p>
                                            The trail is easy to walk as it is a falt ground which requires minimum effort. There are many water points before Pandu Bridge where you can refill your water bottles. 
                                            After reaching Odi Thach we'll be camping there for the night which is one of the windiest campsites. 
                                        </p>
                                    </p>
                                    <h3>Day 6:- Odi Thach to Mantalai Lake</h3>
                                    <p>
                                        Today, we'll be covering a distance of 12km which will take around 7-8 hours. This route is a bit muddy which at times can be slippery so it is important to be cautious. You will come across the moraine area which comes right after crossing the muddy surface. 
                                        It has a steep ascent which requires a lot of physical energy to climb. So it is important to climb at your own pace. Mantalai lake is the starting point of Parvati River and you can also visit a small temple there. Your next campsite is just 45 minutes from here. 
                                        The lake is at a higher altitude so there are chances of getting AMS. It is important to keep your body hydrated enough so that you are not hit by AMS
                                     </p>
                                    <h3>Day 7:- Mantali Lake to Summit Camp</h3>
                                    <p>
                                        After leaving the striking place of Mantalai Lake you will cross the river lane which is very hefty part of trek and then you will walk through high rocky areas and glaciers. Then you will climb towards the right side of the slope, follow the trail which is very long and takes 9-10 hours. 
                                        It takes around to climb the high mountain above at 14850 feet. Then you will walk to the composite area, steepy rocks will make you feel tired. Then you will reach to base camp which is surrounded by greenery all where and big glaciers. There you will stay in base camp and take rest for hour and take delicious dinner.
                                    </p>
                                    <h3>Day 8:- Summit Camp - Summit to Pin base/Bedu Thatch</h3>
                                    <p>
                                        The trek begins another day after long hours of rest and after long voyage you will then enter in long valley Spiti Valley. The trail now climbs for an hour amid the glacier and a rock facade. After three hours of sturdy climbing and you will arrive at the top of the pass at 17,500 feet from where you can outlook the way of Pin valley towards Srikhand Mahadev. 
                                        The follow come down over flurry and ice fields, covered with grasses and high rocks and stone will also come in your way. At this point you will need to be very careful as there you will see very hefty path which cross the dense forest lane and wild areas. The base camp is located in the centre of three vales. 
                                        Moving further you can view the ranges of Dibibokri and Kulu Makalu which looks very fascinating and eye catchy as it is covered by large mountains and ice.
                                    </p>
                                    <h3>Day 9:- Pin Base to Tiya</h3>
                                    <p>
                                        On this day we'll be trekking to Tiya which is approximately 12km. It is a gradual descent for most of the trek. It is a barren land which is surrounded by tall mountains. You will be able to see the trail of Bhaba Pass on this route. 
                                        Your campsite at Tiya has a beautiful setting but it really gets cold at night. It is important to have proper layering while you sleep in your tents.
                                    </p>
                                    <h3>Day 10:- Tiya to Mud Drive to Kaza</h3>
                                    <p>
                                        Today we'll be trekking to Mud which is the last village of Pin Valley. It is a flat walk on the road almost on the road. After reaching Mud we will be driving to Kaza. 
                                        Necessary arrangements will be made for your pick up after which we'll be driving to Kaza. Your accommodation will be in a guesthouse at Kaza.
                                    
                                    </p>
                                    <h3>Day 11:- Kaza to Manali</h3>
                                    <p>
                                        The way back to Manali will be by the familiar road, the drive starting at 4 am in the morning. Transportation will be exclusive of our charges. 
                                        You’ll reach Manali between 6:00 pm and 7:00 pm. You can book your travel any time after 7:30 pm.
