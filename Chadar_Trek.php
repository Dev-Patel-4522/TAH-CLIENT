                                    <h3>Day 4: Leh to Shingra Koma then Trek to Tsomo Paldar</h3>
                                    <p>
                                        On this day, you'll be headed to the first campsite which is Tsomo Paldar via Chilling & Shingra Koma. Shingra Koma is the most unique place of Chadar for it is placed at U bend of Zanskar. 
                                        Forming a wonderful place with lots of views. Today, will be one of the most scenic drives which lets you witness some of the top spots in Ladakh. 
                                        Gurudwara Pathar Sahib, Magnetic Hill and the confluence of the Zanskar and Indus are some of the places where you can take a pit stop for clicking pictures. After 3-4 hours drive along the Zanskar will get you to Shingra Koma. 
                                        The car will drop you right where the road ends, this will be your starting point of walking on the Zanskar. 
                                    </p>
                                    <p>
                                        The layer of Chadar may seem thin here, you can also see various rock formations. Walking with gumboots on will definitely be a challenge in the initial stage. 
                                        Understanding the ice patterns will take time, slowly but steadily you will know which surface is fragile and which is trustworthy. 
                                        There are multiple number of ice surfaces that you would come across while walking on the mighty Zanskar and as you start walking you will know more about it. 
                                        Planting your foot will be important in most of the cases, you can check for the ice for its steadiness with your walking stick before stepping on it wholeheartedly. 
                                        A small and light backpack will help as you will able to manage your body weight in an effective manner. After an hour and half of slipping, falling and trying to gather your balance you'll be reaching your first campsite at Tsomo Paldar. 
                                        This will be your first experience of camping under sub-zero temperatures.


                                     </p>
                                    <h3>Day 5: Tsomo Paldar to Tibb Cave</h3>
                                    <p>
                                        Chadar is one such trek where your not gaining altitude in quick successions. Slowly but gradually from Tsomo Paldar to Tibb Cave (6-7 hours), you will be gaining an altitude of 400ft. 
                                        (Tsomo Paldar: 11,400 to Tibb Cave: 11,800) After spending a cold night in the tent at Tsomo Paldar get done with you breakfast that will re-energize your senses in the morning. 
                                        The much needed hot cup of tea will be special in such frosty conditions.
                                        <p>
                                            As you start heading to the next campsite which is Tibb Cave, this trek route has some of the most eye-catching frozen waterfalls that you'll see. Waterfalls that stand frozen in time. 
                                            The source of some of these frozen waterfalls is still unknown and there are a lot of myths amongst the villagers. The mighty Zanskar will be flowing all along with you on the trail and the emerald green water will have a smooth soothing You will be served lunch midway to Tibb cave. 
                                            After walking for a couple of hours you'll come across different rock patterns along the way which look art looks when the sun rays fall on them making their way from behind the clouds. You will be camping near the Tibb cave which is surrounded by dry sand. 
                                        </p>
                                    </p>
                                    <h3>Day 6: Tibb Cave to Nerak</h3>
                                    <p>
                                        As you move on to the most awaited campsite of the trek which is Nerak, the 13km long trail will take you to the most iconic landmark of the Chadar trek. i.e. The frozen waterfall. 
                                        The frozen waterfall is a nature's masterpiece that attracts trekkers around the world. Hot lunch will be served on your way to Nerak.
                                    <p>
                                        By far, Nerak will be the coldest of all the campsites on the trek where the temperature goes down to as low as -30 degrees during the night. It will be important that you wear proper clothing in layers to stay safe from the biting cold. 
                                        If you manage to reach the waterfall on time, there are chances of paying a visit to the Nerak Village as well. You will be camping at a distance of 15-20 minutes from the waterfall.
                                    </p>
                                    </p>
                                    <h3>Day 7: Nerak – Tibb Cave</h3>
                                    <p>
                                        You might have heard stories of how unpredictable the weather patterns can get here on the Chadar trek. There are instances where the formation of Chadar no longer exists on your way back from Nerak. 
                                        In the same way, you will witness a new layer of Chadar formed at some. Even though it's the same route, you'll be amazed to see how different it appears while coming back. Chadar Trek is an experience of a lifetime that every trekking enthusiast should witness. 
                                        And then, what if we told you that these eight days of your life are going to be some of the best days you've lived so far and that you'll cherish these moments till the very end. 
                                    </p>
                                    <p>
                                        Today retrace your steps back to Tibb Cave. You get another last chance to cherish, relive and gather the memories. 
                                        Wave goodbye to all the locals while you make your way back. Be careful and enjoy your last stay in tents.
                                    </p>
                                    <h3>Day 8: Tibb Cave to Shingra Koma then Drive to Leh (70km)</h3>
                                    <p>
                                        Again wake up early for a long walk back. It is not really retracing steps because you may observe a lot have changed. Chadar may have melted, layer of Chadar may have gotten thin, wonderful rocks are visible now. 
                                        Wea ll, that's the beauty of nature and Chadar trek. Reach Shingra Koma and get ready to board on vehicles. Another bumpy ride and you will arrive in Leh.
                                    </p>
