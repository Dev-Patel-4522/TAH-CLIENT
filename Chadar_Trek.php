                                            It is very important that you get used to the cold temperature in Leh because the temperature will only tend to fall as you start walking on the frozen Zanskar. From the end of January to mid-February, the temperature can be anything between -10 to -15 degrees during the day. 
                                            It is also important to keep your body hydrated by increasing the intake of fluids that will help to maintain your body temperature.
                                        </p>
