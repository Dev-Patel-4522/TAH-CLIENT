                                <p>Approx Trekking Km :- 62 Kms.</p>
                                <p>The journey starts at the mountain town of Leh. The only way to reach Leh is by flight and it is surrounded by snow giving it a complete white look. 
                                   One can catch a glimpse of the snow capped mountains from the flight as it descends toward Leh which is located at 11400 feet above sea level. 
                                   The trek is over the Zanskar River which lies frozen during the winter. The most preferred time to cross the ice would be in February. 
                                   During that time, the ice tends to be at its most stable state. The Frozen River Trek begins from the small village of Chilling from where the Zanskar River begins to freeze.

                                </p>
                                  <h3>Across the Ice</h3>
                                <p>The ice can be as dangerous at times as it can be safe during others. One needs to figure out the safe paths over the precarious sheet and must always be aware of the ice conditions below the feet. 
                                    The trekkers can choose to camp in the caves along the banks like the locals or set up tents where the ice is the most stable. The setting is close to twenty degrees below freezing point and decreases further after sundown as well. 
                                    Warm and layered clothes are recommended as is being on the move as much as possible. These help invariably in keeping the body warm and prevent cold sores and also keep the blood from freezing.
                                <h3>Better Safe than Sorry</h3>
                                <p>
                                    It is always recommended to be aware of the ice conditions at all times. There are several factors to consider ensuring a safe journey. Most of these factors involve the conditions of the ice and the footing choices of the trekker. 
                                    The precautions include:
                                    <ul>
                                        <li><i class="fas fa-check"></i> The trekker must be aware of the ice sheet conditions.
                                        </li>
                                        <li><i class="fas fa-check"></i> There may be cracks in the ice which should be avoided.
                                        </li>
                                        <li><i class="fas fa-check"></i> Finding places with a coating of snow is the best place to walk normally.
                                        </li>
                                        <li><i class="fas fa-check"></i> Slippery ice can be found on several sections as well and should be walked on with utmost care.
                                        </li>
                                        <li><i class="fas fa-check"></i> Cracks may suddenly appear under one’s feet and it is required to run to more solid footing immediately.
                                        </li>
                                        <li><i class="fas fa-check"></i> The ice is not a trekker’s friend and one should always respect it and be aware of the conditions at all times especially while choosing the camping spot.
                                        </li>
                                    </ul>
                                </p>
                                 <h3>Gear and Trekking Info</h3>
                                <p>
                                    Trekking gear should be brought from home as there are not many places available at Leh during the winter where supplies can be bought. Food that can be preserved and made quickly such as instant noodles is an excellent addition. 
                                    One should always have enough before starting off as it is recommended to be moving for the better part of the day in order to keep warm as well as keeping the journey in momentum. Ice picks, trekking boots, several layers of extra 
                                    warm clothing are a compulsion in such conditions. Tents are recommended but optional as one can easily camp in caves like the locals who journey across the river. However, being a non-local, that idea may not be advisable and carrying 
                                    tents is always commendable.
