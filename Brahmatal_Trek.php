                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1:- Kathgodam to Lohajung</h3>
                                    <p>
                                        We will see you the first day at Kathgodam railway station. The pickup place is Kathgodam railway station early 
                                        in the morn. An overnight train travel from Delhi could be sufficiently convenient.
                                        <p>
                                            The journey uphill from Kathgodam to Lohajung will take you cruising through mountain alleyways connecting the 
                                            lower Himalayan belt of Kumaom hills to that of Garhwal. This is going to be a hectic 10 hour drive, despite the
                                             marvellous changing vistas of the mountains. For the first few hours, the course of our journey will be running 
                                             parallel to river Kosi, flowing softly. Soon, a blustery Pindar river will meet our path.
                                        </p>
                                        <P>
                                            Look out for a few of the best known tourist hubs nestling in Uttarakhand’s sublime hills. We will pass through 
                                            Bheemtal, Almora, and Kausani—all celebrated hill stations. Upon entering the last stretch of the drive, near Lohajung, 
                                            the mesmerizing Nanda Ghunti peak will come to sight. We will reach Lohajung guest house, our halt for the night around 
                                            5:00 pm, close to dusk and just in time for tea!
                                        </P>
                                        
                                    </p>
                                    <h3> Day 2:- Lohajung to Bekaltal</h3>
                                    <p>
                                        The first day’s trek spanning 5 hours will kick off from Lohajung up a snaky trail going into the forest. After a short while 
                                        the forest will give way to a small village named Mandoli to your left. Here you can fill your bottles and take a first major hiatus. 
                                        The trail continues in mostly gradual ascent curves. A little way higher, you will come across the valley where two mountain rivers Kali
                                         and Pindari confluence. This is the Kali valley, lying in a splendid green scrap even in the midst of wintry heights. We will trek on 
                                         further to stumble upon a huddle of mud huts. You are going to find a grove of rhododendron here that sure grows red and heavy from 
                                         February to March, but at the peak of winter, the trail will be pretty matte.
                                        <p>
                                            Crossing this point, trek on to find the next identifiable landmark—a wooden bridge over a stream. This steam is a viable water 
                                            point which you can access going down the slope. Crossing over on to the other side, you will reach Begum—a nestling rest point on 
                                            the slopes overlooking a vast expanse of bugyals at distance. From here, the path bends on to two more chattering streams, the last 
                                            being Gujrani stream. Take approximately an hour’s hike after crossing the last stream to reach the beautiful Bekaltal gleaming 
                                            peacefully in the summer while partially frozen over in the winters.

                                        </p>
                                        <p>
                                            We will be camping in the vicinity by the lake Khopdaliya. Enjoy the stillness and the quiet of this place with the thick oak forests 
                                            closing in from around.


                                        </p>
                                    </p>
                                    <h3>Day 3:- Bekaltal to Brahmatal</h3>
                                    <p>
                                        Today’s trek will take you through thickets of oaks and rhododendrons over steep slopes for quite some time before finally coming out to a meadow. 
                                        This is the meadow of Telandi. In the winter, you will find the green expanse of Telandi transform into a vast snowfield and crossing the area, a gala of thrill.
                                    <p>
                                        The meadow rolls till 11,500 ft.- the highest point, from where begins a slow descent to Brahmatal. To your left from here, you will find a small temple, bright 
                                        yellow flags flying in the air. Walk down the slope to the temple and find the trail winding towards the Brahmatal Lake, about 500 meters away from the temple now. 
                                        We will find the valleys of Almora and Nainital rise and fall around us as we go. The soaring peaks of Trishul, Nanda Ghunti, and others of the Greater Himalayas 
                                        can be spotted far across the sky. We will be camping at the mark 500 meter away from the lake, nestling in the beautiful aura of the place.
                                    </p>
                                    <P>
                                        Make sure to carry 2-3 litres of water on today’s trek since there are no water points on this trail.
                                    </P>
                                    </p>
                                    <h3> Day 4: Brahmatal to Tilbudi via Brahmatal Pass</h3>
                                    <p>
                                        Start your day early in the morning as you will be heading towards the Brahmatal lake which is an uphill climb. You will be greeted with Oak and Rhododendron 
                                        trees on the way which has a charm of their own. After a while, the zigzag ascent trail will take you Brahmatal Lake. On a bright day, the lake looks charismatic 
                                        and the sunlight makes it look even more artistic. As you walk ahead you will come across a small stream which is formed by the melting of snow. You might get 
                                        fooled by every passing top which will feel like the last ascent. After walking for a while and crossing the meadow, the final climb towards the pass begins.
                                        <p>
                                            It will be a tiring walk towards but as soon as you reach the top you will be greeted with a magnificent view of Mt. Trishul and Mt. Nanda Ghunti. Take 
                                            your time and click some jaw-dropping pictures that will last a lifetime. As soon as you start descending, you discover that the trail is different from 
                                            that which got you up. You will be able to witness a 180 degree of the entire Himalayan range and peaks such as Haukhambha, Mandi, Mana, Nilgiri, Neelkanth, 
                                            Trishul, Nanda Ghunti, Maitoli become visible. As you continue to descent, after an hour you will reach a clearing surrounded by trees on all the sides which 
                                            is the Tilbudi campsite. You will be camping here for the night.




                                        </p>
                                    </p>
                                    <h3>Day 5: Tilbudi to Lohajung via Wan</h3>
                                    <p>
                                        After having your breakfast you will start your descent to Wan which is a trail filled with Oak and Rhododendron trees. This is a very beautiful trail which allows you to 
                                        click end number of scenic pictures. After walking some distance you will come across a water source at Chota Tilbundi.
                                        <p>
                                            Half an hour later you will be able to spot Wan village which is visible below. From here onwards it will take you half an hour more to reach the road from where TAH will 
                                            arrange vehicles to take you to Lohajung.
                                        </p>
                                    </p>

                                    <h3>Day 6:- Lohajung to Kathgodam</h3>
                                    <p>
                                        After a good night’s sleep at our Lohajung guest house, we will go back to Kathgodam today. Destination will be reached by 6:00 pm. We can manage a brief stopover at Kausani to 
                                        commemorate the trek with souvenir shopping.
                                    </p>
                                    
                                    
