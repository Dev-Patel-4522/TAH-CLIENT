                                <p>Chopta Chandrashila Deoriatal is a window into Himalaya’s spring color vibrancy. A well-loved trek for mountain goers of all kinds, 
                                    this trek flourishes with blossoms, especially rhododendrons red and pink, in summer time while winters are a hazy shade with enough 
                                    scopes to have a ball in spilling snow. The whole tour covers a swoop to the Chandrashila Peak, passing by the enchanting Deoriatal 
                                    lake side, and opens up a gallery of stunning snow peaks all the way. This 5-day plan is unmatched in nature’s beauty, exhilarating 
                                    challenges, time-efficiency and convenience. The highlights of the trek range from awe-inspiring summit views to warmth of the forest 
                                    greens, piquing up interests in all sorts of mountain lovers. You could be a nature treasurer, bird watching amateur, or a shutterbug 
                                    with a dream of capturing the drama in the highlands, Chopt-Chandrashila-Deorital is a trek for all and fails none.</p>

                                <p>The first big stop on the trek comes with Deoriatal. Situated at a good height of 7, 800 feet, the lake is gripped with forests all 
                                    around. At a short distance ahead, the Chaukhamba massif glistens and the reflection of it is caught in the crystal-like waters of the 
                                    lake. The effect is striking. The site is steeped in myths. As goes the story, the five pandava brothers from the Mahabharata, once thirsty, 
                                    had come to the waters of Deoriatal and faced a test of wisdom. Camping here the first night under a stardusted sky makes the mellowest of 
                                    memories on a trek. Next up on the line of visual ecstasies are the extensive meadows that turn to snow fields along the way to Chopta. As 
                                    one furthers up the trail, great peaks jump to view—Bandarppoonch, Kedar, and Chaukhamba to name a few. These stellar features of Garhwal 
                                    Himalayas promise to get more elucidated from the zenith of Chandrashila, so the motivation is never lost and one goes on until the end of 
                                    the summit stretch at the Tunganath temple.</p>
                                
