                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1: Drive Haridwar-Sari</h3>
                                    <p>
                                        Our tour guides will expect you at Haridwar railway station at 6:30 in the morning today. You can arrange an overnight 
                                        bus journey from Delhi, reaching Haridwar in the morning or reach a couple of days earlier to the starting day of the 
                                        trek. Haridwar-Rishikesh by the Ganges is a famous site on the pilgrim trail of Uttarakhand. You can spend your time in 
                                        Rishikesh immersing in cultural activities and adventure sports in the choppy waters of Ganga. We can pick you up alternatively 
                                        from Rishikesh as well.
                                        <p>
                                            From this point to Sari village is a journey of 7-8 hours. Take in the beautiful vista of lower Garhwal Himalayas as the drive 
                                            turns uphill. The most picturesquely notable stop on the way will be at Devaprayag- a town marking the gorgeous confluence of 
                                            Alakananda and Bhagirathi. The river will keep you company throughout, reaching you at Sari by 6:00 in the evening. The village 
                                            is a pretty picture composed of little houses sketched across a mountain backdrop and lined with green patches of agricultural 
                                            fields. If you are missing any minor requisite for the journey ahead, visit the market area in Sari, just might find something 
                                            useful!

                                        </p>
                                        
                                        
                                    </p>
                                    <h3> Day 2: Trek to Deoriatal</h3>
                                    <p>
                                        The trek on the first day is easy with mixed ascent and descent. A well-defined rocky trail will show you to the end, from where the 
                                        Chandrashila summit is clearly visible and the Tunganath temple is a speck of color. After this point, it’s a steep uphill bend on the 
                                        ridge back of the mountain. Get ready for an acutely laborious climb, giving you the first taste of challenge. Soon, you will reach the 
                                        Forest Department built resting stop. Clocked timely by your Trek Leader, you can afford a 20 minutes break here. An excellent view the 
                                        way of the mountain fall waits here.
                                        <p>
                                            Post-rest, the trail will bend through a forest of rhododendrons. Spring treks here in the crimson shadow rhododendron blossoms make 
                                            it memorable more than ever. We go steadily up. At 7, 434 ft. village localities vanish from sight. Nearly 20 minutes from this point, 
                                            we will be standing with the waters of Deoriatal glistening down a lurching gentle slope.

                                        </p>
                                        <p>
                                            A grand vista of Chaukhamba massifs is unveiled in the background of the lake. It will be mid-noon by the time you reach the spot. Wait 
                                            for the morning when the cloud cover lifts and a diamond-crushed sunlight elucidate the outlines of Chaukhamba, making for the ultimate 
                                            striking frame. Having the entire day to yourself, explore the deep nooks of the forests lying out around, a paradise for bird-watcher’s 
                                            and nature lovers. Hot lunch on this day will be served on reaching our lakeside camp in Deorital.


                                        </p>
                                    </p>
                                    <h3>Day 3: Deoriatal to Baniyakund through Rohini Bugyal</h3>
                                    <p>
                                        Today’s trail will familiarize you more with the Chaukhamba massif and Mount Kedar summit, rising closely as you process. Rhododendron and maples 
                                        eclipse the forest path, spreading a cool and rejuvenating vibe. In these thickets you will find two prominent varieties of rhododendrons—red and pink, 
                                        the concentration of them thickens as you move pass the first forest stretch.
                                    <p>
                                        The trail is sprinkled with little shrines that stand out in the greenness by the red and yellow flags tied on them. After crossing a clearing in the forest 
                                        lined with scarlet blossoms, we are directly ushered up an ascending path towards Rohgini Bugyal and Chopta. The ascending trail gives way to a level land and 
                                        some leeway in the rigorous uphill hike. A resplendent view of the Chandrashila awaits on the other side of the forest margin. After the level-land, you will 
                                        have to re-enter the forest now characterized with old oaks and maple besides the rhododendrons. This forms part of the Kedarnath Sanctuary, and with some luck, 
                                        trekkers can spot red fox and other small wild things.
                                    </p>
                                    <P>
                                        More 25 minutes of trekking in the Kedarnath Sanctuary trail and you will reach the Bhagdwal temple. This is a junction point for three trek routes. One connects to Sari 
                                        village, another towards Ukhimath, and the central one descending to the meadows of Rohini Bugyal. Along this line, Chandrashila and flanked by the adjoining Kala Parvat Peak 
                                        gain prominence. You can find the way bending from here to Bisuri Tal lake and a small tarn named Nandikund, situated at the base of Kala Parvat. Further down as you progress, 
                                        a silvery rivulet coming from Chandrashila meets your way. Here, you can fill your water supplies and move on taking a small wooden bridge on the stream.
                                    </P>
                                    <p>
                                        A little way up, the meadows start. Here, you will find a cluster of huts. Shepards from local villages come here to graze their livestock. Not far from here, you will find a road head at the 
                                        margin of a jungle trail. In this area in the vicinity of Chopta village, we have our campsite at a clearing in the forests.
                                    </p>
                                    </p>
                                    <h3> Day 4: Baniyakund to Tungnath, Chandrashilla Top and back to Baniyakund</h3>
                                    <p>
                                        We commence the hike today early morning. Starting in the wee hours of dark will let us have a beautiful sunrise experience from Tunganath top. 
                                        The trek runs on a well-defined concrete-bound trail from Chopta to the temple. Today,the hike will be fraught with steep climbing challenges 
                                        with sharp turns to cross by. There will be other shrines on the way, of which a particular temple with a flagpole is notable.
                                        <p>
                                            This one comes after the 10th bend of the road and marks the opening of a splendid view of Chandrashila up ahead. From here, you will also get a 
                                            clear vision of a connecting peak to Chandrashila. This one is named Ravanshila.
                                        </p>
                                        <p>
                                            In the winters, you will find snow from here. The 11th bend marks the entryway to Tunganath above which the splendid Chandrashila peak towers at 12, 083 ft. The route to the peak bends from 
                                            behind the temple complex. Beware of thick snow on this summit stretch after December till the end of April. Crampons and gaiters are highly recommended for the snow climb and your trek lead 
                                            is expected top make the path through the cover of snow sludge with the help of an ice axe.
                                        </p>
                                        <p>
                                            At the end of a steep and arduous climb, the summit will be reached with a 360 degree view of the Garhwal and Kumaon mountains welcomingly lying ahead. Notable peaks in this panorama of grandeur 
                                            are—Nanda Devi, Trishul, Nandaghunti, Dronagiri, Chaukhamba, and Kedar. Glimpses of the Thalaysagar and Gangotri ranges too could be made out at the edges.
                                        </p>
                                        <p>
                                            The downhill journey is relatively less time consuming and smoother. It will take you about 3 hours to climb down to the Baniyakund campsite.
                                        </p>
                                    </p>
                                    <h3>Day 5: Baniyakund to Haridwar</h3>
                                    <p>
                                        A ride back from the Chopta road head to Haridwar is scheduled for day 5. After this 7 hour journey full of mellow reminisces of the hills, you will be reaching Haridwar in the evening. An overnight 
                                        journey to Delhi will hopefully work out best.
                                        
                                    </p>

                                    
                                    
