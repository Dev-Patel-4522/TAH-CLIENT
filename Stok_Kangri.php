                                            As per the J&K government, foreigners(including OCI, NRI) need to obtain a special permit to trek in J&K. This permit is issued in Gangtok and therefore, 
                                            foreigners need to come one day before in Srinagar. This process requires your signed ILP, therefore it cannot be processed in advance and can be processed only 
                                            when you enter J&K. The charges of obtaining the permit will be Rs.2, 000 per person. TAH will cooperate with you in getting the permit. You have to pay this 
                                            amount to our representative and we shall get the permit, once you have arrived. Please make a note that your expenses for the additional day are not included in 
                                            our costs.
                                        </p>
                                        <p>
                                            Citizens of China, Myanmar, and Bangladesh are not permitted to trek in Sikkim.
                                        </p>
                                    </p>
                                    <h3> Day 02: Leh (Acclimatization day)</h3>
                                    <p>
                                        On day 2nd, you can enjoy the cultural and natural beauty of Leh. This can be considered as a day to get familiar with the town and also acclimatize to the weather. 
                                        Leh is culturally a rich place. There are dozens of monasteries and ancient temples that you can go around. They are located in and around the town. 
                                        Therefore you would be getting enough time to go around the place in a day. You can have an overnight stay in Leh. The hotels are easily accessible and rates are reasonable too. You can enjoy trekking here as post monsoon trek and pre monsoon trek. 
                                    </p>
                                    <h3>Day 03: Leh (11500 ft.) to Chang Ma (13087 ft.) via Stok Village (11800 ft.)</h3>
                                    <p>
                                        It is one of the best sites that you would have once you have decided to trek this area of land. Trekking in Leh, could be considered by some people to be living their dreams. At any point of time, you might just feel that you are right in the middle of heaven. 
                                        This is a day when you would enjoy a great drive and have an enjoyable trekking experience. The day starts with a drive to Stok village. At the start of the drive from Leh, you would go through many exotic and beautiful scenes. 
                                        The drive to the Stok village is an adventurous one and if full of natural beauty and lush green trees. The weather is perfectly fine for the summer season. The Stok village lies immediately to the south of Leh and it is well known for a royal palace. 
                                        The recently crowned king of Ladakh lives here. There is also a small museum inside the palace which has beautiful and religious artifacts.
                                    <p>
                                        The day 3 is however the day when you trek to Chang Ma. This might be a difficult trek and you may experience the joy of a snow trek. You would like to spend the night here and the overnight stay would be in tents in the open lands. 
                                    </p>
                                    </p>
                                    <h3> Day 04: Chang Ma (13087 ft.) to Mankorma (14200 ft.)</h3>
                                    <p>
                                        The whole trek passes through the barren grey landscape of Ladakh. From chang ma trek up to Mankorma, This day will show you the real beauty of ladhak and gives you the answer why ladhak known as cold desert, while trekking you will cross the 2-3 times small streams, it is a fairly easy trek along with the river and you would love the overnight stay in tents.
                                    </p>
                                    <h3>Day 05: Mankorma (14200 ft.) to Base camp (16300 ft.)</h3>
                                    <p>
                                        A few hours trek takes you back to the base camp. This exotic base camp is a pleasurable place. It lies in a grassy area. This area has a number of water bodies in the form of lakes and water streams. 
                                        During the day time, you can walk on the upper areas of the campsite and enjoy the views of the surroundings. There are some beautiful peaks like Gulap Kangri, Parcha Kangri and many others. 
                                        You can also take rest in the base camp to make sure that the group is ready for the next day’s trek and touring. You would get time to acclimatize to the weather. 
                                        This is pretty necessary because in the coming days, you wuld find a drastic change in the climate and might experience thin air and lack of oxygen.
                                        <p>
                                            The overnight stay, would again be arranged in tents and make sure that you have enough warm clothes for the lowering temperature.
                                        </p>
                                    </p>
                                    <h3>Day 06: Rest day + training day</h3>
                                    <p>
                                        This day is an easy day for trekkers. You would enjoy the time for going around and knowing the place to a much better extent. It is essentially a day to train for the upcoming trek and know the possibilities and challenges that are involved in it. 
                                        Your trek leader and guide would let you know about all the different changes in the terrain. You need adequate knowledge and experience to go around the mountains. There might be situations where you have to trek in the sinking snow. 
                                        You would find your legs sinking up to your knees. Therefore it is essential that you know the weather and surrounding conditions. It is also a day when you would to take enough rest so that you are well prepared for the coming excursion. 
                                        The overnight stay would again be in tents. By now, you should be well familiar to the conditions prevailing during the day and night.
                                    
                                    </p>
                                    <h3>Day 07: Base camp (16300 ft.) to Stok Kangri (20080 ft.) back to base camp. </h3>
                                    <p>
                                        Now that you have had rest for a day i.e. the Stok Kangri trek, you should be well prepared for the big climb. You should wakeup early to start the climb. The route goes through glaciers and then comes to steep slopes to the summit ridge. 
                                        This is a long climb, so you should be mentally strong and prepared psychologically. It would take somewhere between 9 to 10 hours for average climbers. Slow climbers may take another 2 to 3 hours in addition. At the start of the trek, after a couple of hours, you should reach to the summit of the ridge. 
                                        The summit offers you great views of the area. The wide and open surroundings give you a royal feeling. Towards the north, you would get a view of the Karakoram range. The Saser Kangri dominates the sky in the north along with the Karakoram, and the Zansker ranges in the south. The overnight stay, as usual, would be in tents.
                                    </p>
                                    <h3>Day 08: Base camp to Leh</h3>
                                    <p>
                                        Day 8 would be the ultimate day of your fantastic trekking experience. The trek which included high altitude trekking mountaineering and expedition would finally come to an end after this day. But before you end the day, you would see a lot of snow clad beauty and memorable view of mountain ranges. 
                                        The joy of Ladakh trekking can be rejuvenated from this spot. As the day rises, you would have to start coming back from Base camp to Leh. The trek back could be difficult and you need to perform care and precision. On the same day, after you reach Leh, you would also have to cover up to Stok Village on your feet. 
                                        Stok village is the final stopping point of trekking. From Stok village to Leh, you would have to take a drive. The drive may make you nostalgic of all the fun that you had while trekking the mountains in the last eight days.
                                    
                                    </p>
                                    <h3>Day 09: Buffer day </h3>
                                    <p>
                                        Buffer day is an extra day that is spent at the base camp for a summit attempt in case the whether does not allow. This day is used only when the summit is not possible as per the regular itinerary. Whether to use the buffer day or not is totally team’s decision. Most of the big expeditions have 1-2 days buffer day. 
                                        Therefore, it is important to consider this day and carefully plan your travel dates too. The costing for buffer day is not included in the trek cost, buffer day cost is Rs 2,600 per day and per person. So, before using the day, make sure you and your team members discuss about it. Fortunately, if everything goes well as per the itinerary, we can plan the day for you. 
                                        We can make arrangements for other activities like Rafting, Biking, Pangong Lake visit etc.
                                    </p>
                                    
