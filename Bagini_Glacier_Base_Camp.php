                                    <h4>Brief Itinerary</h4>
                                    <h3>Day 1 :- Haridwar to Joshimath.</h3>
                                   
                                    <h3>Day 2 :- Joshimath to Jumma & trek to ruin village (3 kms, 9, 180 ft).</h3>
                                   
                                    <h3>Day 3 :- Ruing Village to Dronagiri Village (8 kms, 11, 844 ft).</h3>
                                    
                                    <h3>Day 4 :- Dronagiri village to Longatulli (14, 711 ft, 6 kms).</h3>
                                    
                                    <h3>Day 5:- Langatuli to Basecamp( 5 kms trek)</h3>
                                    
                                    <h3>Day 6 :- Base camp to Bagini Glacier and Back to Base camp (10kms, 14, 814 ft).</h3>
                                    
                                    <h3>Day 7 :- Base camp to Ruing Village (18 kms).</h3>
                                    
                                    <h3>Day 8 :- Ruing Village to Joshimath( 3 km trek, 45 km drive)</h3>
                                   
                                    <h3>Day 9 :- Joshimath to Rishikesh.</h3>
                                    
                                    
                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1- Haridwar to Joshimath (8-9hrs. Drive)</h3>
                                    <p>
                                        Drive to Joshimath 256 km. If you arrive morning Haridwar drive to Joshimath through Devprayag, Srinagar, 
                                        Rudrapryag, Karnprayag. Breakfast and lunch on the way. Stay in Guest house.
                                        
                                    </p>
                                    <h3> Day 2- Joshimath to Jumma (44km. drive) and trek to Ruing Village (3 km.trek) 2800mtr</h3>
                                    <p>
                                        The Trek starts from a non descript village called Jumma which is an hour ride from Joshimath.Trek Ration and kerosene can be picked up from Joshimath,
                                         as further up things get dearer by the metre. The ride from from Joshimath to Jumma is part bumpy and part smooth. Tapovan, Reni, Lata, Suraithota, 
                                         Phagti are many villages which fall on the way. In between Suraithota and Jumma the road is in par with the Dhauliganga River, which makes it a good 
                                         watch in autumn, but will surely play havoc during the merciless monsoon months. If you want to stack kerosene Suraithota is a good option.Jumma is 
                                         near the tibetan border so during afternoon there will be a fierce wind, blowing you off your feet as soon as you alight from your vehicle.
                                            <p>
                                                The trail starts with a steel suspension bridge over the Dhauliganga after which the ascent of Ruing begins. The road to Ruing is mildly steep through 
                                                the forest. It’s about 3 kms from Jumma to Ruing and won't take more than 2-3 hours. Staying at Ruing is a bit tricky; you can stay at the panchayat ghar. 
                                                If the village elders permit the trek team to camp there for the night, nevertheless another option is to hike further 2-3 km from Ruing and reach small 
                                                grassland just before the ascent of Dronagiri.
                                            </p>
                                            <p>
                                                The place has two distinct advantages, one you get closer to Dronagiri and it would be easy for you to negotiate the herculean climb through the landslide 
                                                and second this place has a nearby stream so a good natural place to camp.
                                            </p>
                                        </p>
                                    <h3>Day 3- Ruing Village (2800 mtr.) to Dronagiri Village (3610 mtr.) 8 Km trek</h3>
                                    <p>
                                        Starts with the same type of trail (trail from Jumma to Ruing). It’s a well laid path through the forest sometimes meandering up and sometimes down. There is no 
                                        possibility of someone to get lost here. This a solitary trail with little or negligible sideways. At a distance of around 3.5mtrs from the Campsite, you reach the 
                                        beginning of the landslide zone. You notice two distinct trails, one on the upper side and another downward. Do not take the upper one as it is a remnant of an earlier 
                                        trail that used to be the main trail to the village. But it was destroyed some years ago by massive glaciers from the Nandikund area. This has led to the making of 
                                        something of a mini Canyon which will require the help of a guide or porter to negotiate if you are not sure footed.
                                    <p>
                                        The trail is dusty, steeply inclined at an angle of 60° to 70º to the horizontal and full of mud slides, small streams crisscrossing your path, while you take your tired 
                                        body surely and slowly to the top. Once you reach there, a cemented pavement awaits in silent anticipation to take you to the Village. The scene is straight out from a 
                                        Swiss alpine landscape. The horizon is dotted with the snow laden peaks of Hathi Parvat, Ghori Parvat. If its summer, flowers will be in full blossom. For once you forget 
                                        this is not a trek but a walk in the Swiss alps.
                                    </p>
                                    </p>
                                    <h3> Day 4- Dronagiri village to Longatulli ( 6 kms trek)</h3>
                                    <p>
                                        After leaving Dronagiri we reach a meandering path which leads us to a high ridge from where the first views of the Rishi peak is visible. Bagini nala too is first visible 
                                        from here. Around 30 mintues after we left the village we reached a concrete bridge over the Bagini Nala. Do not take the left trail as it leads up to the Kanari khal and 
                                        then gradually towards Garpak village. After crossing that bridge the path again takes a meandering turn upwards. The area here is almost completely devoid of any vegetation. 
                                        Its boulders and more of boulders which fill the landscape. There is a patch of green beside a stream which we reach after an hour and 15 minutes from the bridge, This is a 
                                        good camping ground. Further an hour more we reach the camping ground of Longatulli which is again a very good campsite. Overnight stay in camp.
                                        
                                    </p>
                                    <h3>Day 5: Longatulli to Base Camp(5 kms trek)</h3>
                                    <p>
                                        After having breakfast we will start our trek for Base Camp. Bagini lower base camp is reached after a sound 3 hours hike on rocky trail from Longatulli. Bagini lower camp 
                                        site is a plain plateau. Few stunning peaks that can be observed from this place are Satminal, Hardeul and Rishi pahar. The Bagini main base camp is further up the mountains. 
                                        You can either camp here or at another better place forty five minutes from here. It is called the Advanced Bagini Base Camp. The route to this place is not very well marked. 
                                        A better vision and careful observation is required to locate and follow the trail. Few signs like cluster of pebbles positioned on top of one another are made for fellow 
                                        trekkers for better navigation.
                                    </p>
                                    <h3>Day 6- Base camp to Bagini Glacier and Back to Base camp (10 kms trek)</h3>
                                    <p>
                                        You need to begin early if we need to catch sunrise from Changabang base camp, which is around 3-4 kms from Bagini Advanced Base Camp. Leave your rucksack in the base camp 
                                        and carry only the necessary accessories. From Bagini Advanced Base camp the route to Changabang base camp again ascends. The road is full of moraine, and you will spot a 
                                        number of crevasses on the bagini glacier.
                                    <p>
                                        Changabang Base Camp is a huge amphitheatre. The peaks which are visible from here are Hathi(6507m), Ghori,Satminal(6911m), Dunagiri east(7066m),Garur peak, Trishuli(7057m), 
                                        hardeul(7323m), Rishi(6099m), Kalanka(6931m), Changabang(7174m).
                                    </p>
                                    </p>
                                    <h3>Day 7- Base camp to Dronagiri Village(9 km trek)</h3>
                                    <p>
                                        After Breakfast all the way back to Ruing village through Dronagiri Village. Overnight stay in camp at Ruing Village.
                                    
                                    
                                    </p>
                                    <h3>Day 8- Dronagiri Village to Joshimath( 11 kms trek,45 km drive)</h3>
                                    <p>
                                        Descend down to Jumma road head and it is take 3-4 hrs to reach road head and drive back to Joshimath. Overnight stay in Guest house.


                                    </p>
                                    <h3>Day 9- Joshimath to Rishikesh</h3>
                                    <p>
                                        In the morning after breakfast drive to Haridwar .
                                    </p>
