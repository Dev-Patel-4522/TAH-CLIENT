                                <h3>Overview</h3>

                                <p>Region :- Uttarakhand</p>
                                <p>Duration :- 9 Days</p>
                                <p>Grade :-  Moderate</p>
                                <p>Max Altitude :- 14,816 Ft.</p>
                                <p>Approx Trekking Km :- 43 Kms.</p>
                                <p>Bagini Glacier runs down northwards from Changabang & Trishuli peaks (in North-West rim of Nanda-Devi Sanctuary) 
                                    and then the Bagini melt river turns westwards where it is joined by Dunagiri Glacier waters running down from 
                                    Purbi-Dronagiri ridge. Further west, it is joined by the Lampak stream and finally flows into the Dhauli-Ganga, 
                                    just north of Jumma Village.</p>

                                <p>We reached Jumma Village by Max-Jeep from Joshimath on 14th June 2011 and then trekked via Ruing & Dronagiri Villages 
                                    and along the Bagini Glacier to finally stare, in awe, at the snow peaks of Rishi-Pahad, Hardeol, Kanakha, Changbang 
                                    and Trisuli Peaks. </p>
                            </div>
