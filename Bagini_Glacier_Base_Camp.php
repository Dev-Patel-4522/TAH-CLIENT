                                    <h3>By Air :-</h3>
                                    <p>
                                        Jolly Grant Airport is the airport serving Dehradun, located about 48 km from the Haridwar. There is a daily flight from Delhi for Dehradun.
                                    </p>
                                    <h3>By Train :-</h3>
                                    <p>
                                        An excellent rail network connects Haridwar to other important cities like Delhi, Mussoorie, Mumbai, Varanasi, Lucknow, and Kolkata.
                                        From Delhi -DDN NZM AC EXPRESS (2205)
                                        From Kolkata- Doon Express (13009)
                                        From Mumbai- Dehradun Express (19019)
                                        From Chennai- Dehardun express (12687) 
                                    </p>
                                    <h3>By Road :-</h3>
                                    <p>
                                        By road, Haridwar is well connected with major destinations of the country. NH72 connects Haridwar to Nahan (125 km) to the north-west, 
                                        and thereon to Panchkula, Chandi¬garh and Shimla. NH 72 terminates at Dehardun, which is at 54km from Haridwar. Haridwar is located at 
                                        a distance of 235 km from Delhi. NH72A links Haridwar to NH58 at Roorkee (35 km) and thereon south to Meerut (170 km) and Delhi (235 km). 
                                        There is regular bus service from Delhi for Haridwar from Delhi’s ISBT Kashmere Gate. 
                                    </p>
                                    
