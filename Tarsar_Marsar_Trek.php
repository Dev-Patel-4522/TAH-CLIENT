                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1:- Srinagar to Aru Base Camp</h3>
                                    <p>
                                        Our first day destination is Aru on the banks of Lidder and Aru Rivers, situated 12 km off Pahalgam. A fairly developed tourist stop, Aru is regarded the main base for two well-loved treks—the Tarsar Marsar and Kolahoi Glacier Trek, 
                                        the destination of the latter being the origination of the Lidder-Aru rivers. 
                                        <p>
                                            We shall meet you at Srinagar and make a 3-4 hours short drive to Aru via Pahalgam. Since steady telephonic network by all network providers cannot be confirmed at Aru, you need to wrap up your communication with the rest of the world at Pahalgam itself.
                                        </p>
                                        <p>
                                            At Aru, you will be welcomed at a river-side camping zone, off the touristy hub, by the sweet flowing Lidder. There are lots of activities around town at Aru—from trout fishing in the Lidder, the Aru Biosphere Reserve to horseback riding and heliskiing in the winters. 
                                            You can explore and mark these for a later visit, because we will be reaching quite late into the dusk around 6 pm. 
                                        </p>
                                        <p>
                                            As per the J&K government, foreigners(including OCI, NRI) need to obtain a special permit to trek in J&K. This permit is issued in Gangtok and therefore, 
                                            foreigners need to come one day before in Srinagar. This process requires your signed ILP, therefore it cannot be processed in advance and can be processed only 
                                            when you enter J&K. The charges of obtaining the permit will be Rs.2, 000 per person. TAH will cooperate with you in getting the permit. You have to pay this 
                                            amount to our representative and we shall get the permit, once you have arrived. Please make a note that your expenses for the additional day are not included in 
                                            our costs.
                                        </p>
                                        <p>
                                            Citizens of China, Myanmar, and Bangladesh are not permitted to trek in Sikkim.
                                        </p>
                                    </p>
                                    <h3>Day 2:- Aru to Lidderwat</h3>
                                    <p>
                                        Today we shall be following the Lidder River upstream. After a 9-10 km walk up soft slopes, the trail enters a thicket of conifers. Revealing a sweeping view of the Aru valley left down below. 
                                        We continue venturing through the forest shade and then come into a clearing filled with Gujjar huts. This is the Nandekai village inhabited by seasonal, migrating shepherd people, and agrarian, foraging Gujjar community. 
                                        Here is a slice of idyll, picture perfect and a far call from life as we know it. We won’t be passing through the heart of the village but rather get a priceless deep view of the picturesque setting as we move uphill. 
                                        A little after, we have now entered a tumbling meadow grassland, and the fir tree thickets have completely ebbed away. This is still part of the Gujjar village ecosystem and you are going to find horse -riding folks and cattle grazers. 
                                        <p>
                                            The trail cuts across a couple of little valley streams next as we go. We soon come across a crude log bridge over the second brook. This is the spot to fill up our water supplies and take some rest, 
                                            may be have the lunch carried from last camp. The Lidderwat locale is still an hour’s journey from here. 
                                        </p>
                                        <p>
                                            The trail here curves into another wooded area of gorgeous firs before coming out into the meadows of Lidderwat where the river flows clear watered. 
                                            We can find quaint little Gujjar settlements that mark the valley locale. Thanks to tourist inflow in recent times, there are also Pahalgam District Authority Huts and food places established for comfortable stays.
                                        </p>
                                        <p>
                                            Our camp would be at a quiet spot close to the stream. There should be enough time before sundown to walk around the peaceful fir tree shadowed Gujjar settlement and then lay out in the dying light on soft grass by the streamside to soak in the tranquil.
                                        </p>
                                        <p>
                                            It can be quite an exhausting day so it better to sleep early listening to the gushing waters of Perk River.
                                        </p>
                                    </p>
                                    <h3>Day 3:- Lidderwat to Shekwas</h3>
                                    <p>
                                        A trek, approx 5-hour long starts today, winding directly up from behind the PDA homes. In a matter of half an hour, you will be finding yourself inside a forest of pines, 
                                        the Lidder River is now a distant shadow, and the meadows have ebbed away. The woods are not very thick and occasionally open up to wide clearings. 
                                    <p>
                                        Soon, you will find the stretch of Lidderwat breaching to the aperture of a more vast expanse of merging valleys and snowy elevations rising straight from the rim of the valleys. 
                                        There will be multiple clear water streams cutting across our path where you can stop to refill drinking supplies.
                                    </p>
                                    <p>
                                        You will be directed towards Homwas, which is a little clearing inhabited by another cluster of Gujjar huts that falls on the other side of the now re-emerged river along our tracks. 
                                        After crossing a log bridge, we are ushered into the locality of a few homes and tea shops. You will never fail to find warm hospitality with these villagers and despite the exhaustion of travels, this makes one’s day.
                                    </p>
                                    <p>
                                        Hand in hand with the crystal waters flowing by our tracks, we now enter a different grassy landscape. As green as green can be, stretching in gentle folds to as long as the eyes can see, the meadows of Shekwas are breathtaking. 
                                        Gujjar habitation and hospitality is ubiquitous in this area too and their little low roofed, leaf-thatched huts dot the lush landscape here and there. Our trail climbs pass the huts onto a slightly higher camping ground. A sweet night, a star peppered night falls over the meadows.
                                    </p>
                                    </p>
                                    <h3>Day 4:- Shekwas to Tarsar and Explore Tarsar</h3>
                                    <p>
                                        The green moors of Shekwas are best experienced bare feet. Try this as you set out for Tarsar Lake today. The trail moves through a silken lawn of nature for sometime before finally taking a lunge over a humpbacked hill and after crossing through a few ridges, 
                                        opens to a little peek of Tarrsar’s magnetic blue waters.
                                    </p>
                                    <p>
                                        It takes a total of 5 hours to reach Tarsar from Shekwas and after the ridge, it’s another 3 hours, the view of the Lake widening as we go down succeeding smaller ridges. 
                                        You need to cross the stream, jumping from boulder to boulder adventurously before finally gliding into green veldt around the lake where we would camp the night. 
                                        Sink your feet deep into the soft grass and watch how the almond shaped waters reflects the passing hues of the sky as the day rolls on. Time seems to come to a standstill midst of such beauty and silence.
                                    </p>
                                    <h3>Day 5:- Tarsar to Sundarsar</h3>
                                    <p>
                                        Another 5 km trek commences today to the adjoining Sundersar Valley through the Tarsar Pass. We delve into a connecting stretch first which apparently joins the meadows of Shekwas and Sundersar Valley. 
                                        Trekking through the Tarsar Pass is actually a time saving but comparatively more hectic plan than descending to Shekwas first and heading to Sundarsar from there.
                                        <p>
                                            You can actually test your stamina on this day’s trek after so many days of walking on gentle undulations. On the ascent to the Pass, the trail gets steeper but then mellows down to a gentler slope to Sundarsar. 
                                            The highlight of this stretch comes with a view of Tarsar Lake from the other side of the Pass.
                                        </p>
                                        <p>
                                            Next, we go down to a valley bed to be greeted by a nomadic shepherd clan settlement, that of the Bakkerwals. Proceeding, we follow a brook that gets collected at the feet of snow-melting elevations in the form of a beautiful lake. 
                                            The entire way is wrapped in a green fold of luminescent softness. You will find an unearthly photo frame capturing sheep and horses bobbing along the green-scape as they graze lazily along the banks of this anonymous loch. 
                                            Our trail takes us through a high pass exit from this grassland to the next vale, another vast patch of extensive pampas but this one sprayed bright with wildflowers. Later, a bouldered trail over the stream ushers us into the Sundarsar Lake side, a flatland where we can camp at the end of this remarkable day. 
                                        </p>
                                    </p>
                                    <h3>Day 6:- Visit Marsar and then to Homwas</h3>
                                    <p>
                                        Today, we will ascent higher up through Marsar Ridge. Kilting our way around Sundarsar shores, we will be directed to a boulder smitten path. You can expect slippery snow patched in these rock crevasses, 
                                        just like the partially white covered mountains at the end of the valley which we witnessed through our trek in the last few days. At midday, the snow will melt and it’s going to be tricky walk.
                                    <p>
                                        After covering a partly grass covered, partly snow sheeted rock trail over on the Pass, we slightly descend to a flatland that’s cut through by occasional water flows. The top of the ridge view reveals the translucent blue Marsar Lake lying somewhere down below. It has the same kind of almond shaped outline as Tarsar and almost as big. 
                                        Snow shaded elevations happen to rise just along one fringe of the Lake, offsetting its blueness. The Lake happens to overflow on one side and go down in a sweeping glacial stream down to a valley below. The snowmelts from the white cliffs feed its waters on one side and on another, the Dachigam forests starts. 
                                    </p>
                                    <p>
                                        Now we descend back to Sundarsar camps and then to Homwas, trialing almost along the same way that we came by. The descent is full of nostalgia. Homwas is reached before nigh fall, slightly exhausted.
                                    </p>
                                    </p>
                                    <h3>Day 7:- Homwas to Aru and drive to Srinagar</h3>
                                    <p>
                                        Starting off early morning today, we descend from Homwas to Aru, sweeping over the same enchanting meadow lands we had experienced on the first days, and the same Lidder River, now to be followed downstream. 
                                    </p>
                                    <p>
                                        We will stop at Lidderwat to rest and lunch and then set off again, now Aru-wards, 10 km from Lidderwat to be covered over 4 hour approx. 
                                    </p>
                                    <p>
                                        Srinagar-bound vehicles shall be waiting for us at Aru. We can expect to reach station within 8 in the night.
                                    </p>
                                    
