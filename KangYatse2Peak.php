                                <h3>Overview</h3>

                                <p>Region :- Leh, Ladakh, J & K</p>
                                <p>Duration :- 11 Days</p>
                                <p>Grade :- Difficult</p>
                                <p>Max Altitude :- 20,500 Ft.</p>
                                <p>Approx Trekking Km :- 80 Kms.</p>
                                <p>If Stok Kangri is the most popular trek, Kang Yatse II is the less known of the 2 siblings. Both are 6000-meter peaks, almost of the same difficulty level. 
                                   Kang Yatse II must be the preferred choice of the aspiring trekkers who want to try something endearing. It is just 320ft taller than Stok adding the spice of height. 
                                   Despite that Kang Yatse II is considered the easiest 6000-meter peak.</p>

                                <p>Comprising of 11 days, the itinerary is perfect for gradual acclimatization as one gains height. The Kang Yatse peak lies in the renowned Markha Valley. 
                                   Most of the trek route coincides with the approach to Markha Valley. Therefore, this trek offers buy 1 get 1 free benefit. Starting from Leh, one gets to explore the cultural heritage, monasteries, gompa during the early acclimatization days. 
                                   Just as you gain height by traversing through small villages, you will get an insight of Ladakhi culture. At every camping site, you will be greeted by teahouses and authentic homestays. The major hamlets that pass by are Skui, Markha, Nimling, Hankar etc. 
                                   All these form the impeccable portrait of small homes sheltered by the giant mountains. From arid terrains to vibrant grazing lands, from small streams to vivacious Markha river you will encounter the contrasting nature. </p>
                                <p>
                                    The trail enters Hemis National Park, that is known as a wildlife sanctuary. Snow Leopards, arghali, mountain deer etc will be spotted throughout the route. From Umlung the Kang Yatse peak comes to visibility, motivating you henceforth.  
                                    The pastures of Thactungtse and Nimling are filled with colourful wildflowers. Mystic Mani stones and the refreshing Tigu lakes are the major revelations of the trek. Summit day is both gruelling and pleasurable. Kang Yatse summit ascend is filled with moraines and loose rocks. 
                                    The crevasses are covered with snow most of the times, hence roping up is required. Atop shows the never-ending array of Karakoram and Zanskar range. You may also see the summit of the famous Stok Kangri and K2 peak.
                                <p>
                                    Kang Yatse is the best option for those seeking a 6000-meter peak that is less crowded than Stok Kangri. 
                                    This trek is recommended only to experienced trekkers. One must be well prepared physically and mentally for this trek. </p>
                                    
                                    
