                                    <h3> Day 02- Acclimatisation at Leh</h3>
                                    <p>
                                        Yet another day to get used to the low temperatures or to discover the splendid Leh! Apart from adventures and surreal scenery, Leh is well-known for its monasteries and palaces. 
                                        Step into the history of Leh through the Shey Palace, Thiksey Monastery and ThikseyGompa. You can find thousands of adventure seekers or tourists in Leh during peak season. 
                                        Try to interact with everyone to get used to the culture. Leh is famous for its cafes and hanging out places. If you wish you can try them. Again, strictly keep hands off alcohol and smoking.
                                        <p>
                                            Apart from cultural heritage, Leh must be travelled for its passes and lakes like Nubra valley, Pangonglake. We do not allow roaming of trekkers to such places. 
                                            This is to avoid strain on the body that might hurdle the process of acclimatization. Therefore, try to avoid moving to higher altitudes and stroll to nearby places. 
                                            Enjoy your wonderful stay in the hotel. 
                                        </p>
                                        <p>
                                            It can be quite an exhausting day so it better to sleep early listening to the gushing waters of Perk River.
                                        </p>
                                    </p>
                                    <h3>Day 03- Drive Leh to Chilling and trek to Skiu</h3>
                                    <p>
                                        The drive from Leh to Chilling is like entering the ocean of barren, brownish yellow yet stunning region of the Himalayas. In the first half of the journey, major monuments Gurudwara Pathar Sahib and Spituk Monastery will quaintly welcome you. 
                                        The silent and vibrant Magnetic Hill also passes by as you make your own way. Through the views of color changing rocks, dry grasslands you will cross the confluence of Zanskar and Indus rivers. 
                                        Chilling is a popular village of Leh known as the Base camp for Chadar in winter and river rafting in summer. 
                                    <p>
                                        The trek ahead incepts with a steep ascent going through Zingchen gorge. Amidst you will also cross the river on a trolley at Kaya-Do place. The trail many times comes across Indus River. Skiu will mark the first camping in the tents. 
                                        It is located at the confluence of rivers Shgri Nala and Markha. The village has fairly 10-15 houses. They are also good for homestay and tea breaks. The habitat also has an old monastery. End your first day of the trek in Skui, looking forward to the upcoming thrills.
                                    </p>
                                    </p>
                                    <h3> Day 04- Skui to Markha</h3>
                                    <p>
                                        The advantage of Kang Yatse trek is that it seamlessly overlaps with the Markha valley. Therefore, you can boastfully complete Markha valley trek too. And today is the day to witness the Markha valley. This will be the longest day of trekking with a total of 20 km of the hike. 
                                        On this pretty long day, you will be welcomed by 3-4 villages and their rejuvenating hot tea. The first such village, Harding comes immediately after 2 hours of hiking. Keep walking ahead for another 2-3 hours, till you reach Chalak village. 
                                        It is placed beautifully amongst the barley fields with mountains in the background. Again, have some tea and get all energized for the remaining trek
                                    </p>
                                    <p>
                                        The next camping site is at Markha village, which is 5-6km from here. Despite that, it will take 2-3 hours due to the steep climb to Markha valley. The Markha river flows with a strong current and you will have to cross this furious river. 
                                        The river level might be till knee, depending on the snowfall. The Markha valley is considered the largest because it has around 30-40 houses. For people interested in history, there are ruins of a fort. You can also find an old monastery that is believed to be built in the 1800s. 
                                        Make sure to walk through the village and interact with the locals while sipping hot tea.
                                    </p>
                                    <h3>Day 05- Markha to Thachungtse</h3>
                                    <p>
                                        Wake up all fresh and be happy as today will be an easy trek. The upcoming trail meets streams and Markha river several times serving as a refreshment. Umlung village is a tiny village, which can be recognised by houses and a monastery on the route. 
                                        <p>
                                            After Umlung the Kang Yatse peak comes to visibility and keeps calling you! Hankar village will be the next short break point. You can notice the Tacha monastery, skillfully placed on such a height. Hankar is also a camping site and one can have a night stay here. 
                                        </p>
                                        <p>
                                            Trudge along the well-marked trail from Hankar towards a stone bridge. There are many routes coming out, you keep following the same path. In the next 2 hours, the pastures of Thochungtse become visible. There camping sites and tea houses here. 
                                            Find yourself comfortable with the beautiful grasslands and rest for the day. 
                                        </p>
                                    </p>
                                    <h3>Day 06 – Thachungtse to Nimaling</h3>
                                    <p>
                                        This will be a short day filled with surprises. The sight of Kang Yatse gets closer and bigger. The trail is a gradual ascent of 700 meters comprising of 3-4 hours trek. 
                                        Mystic Mani stones having prayer inscriptions will be found on the route. Recite the devotional prayers and hope for a wonderful summit. You will be greeted by twin Tigu lakes. 
                                        Though these lakes are tiny they reflect the magnificent Kang Yatse peak.
                                    <p>
                                        The trails enter into a huge plain of Nimling where sheep, mountain goats, sometimes rabbits are found grazing. Nimling is renowned as grazing pasture lands and will also be your camping site. 
                                        The entire setup is extremely pleasant and gives a contemplating experience.

                                    </p>
                                    <h3>Day 07 – Nimaling to Base Camp</h3>
                                    <p>
                                        This will be the smallest trekking day of the entire trek. Hence, chill and try to make the most of the beauty around you. The first half of the trek has loose rocks but the stunning views of Kang Yatse will ease the pain. 
                                        The latter half of the trail is a descend. After 2-3 hours you will arrive at your base camp at 5,100 meters.
                                    </p>
                                    <p>
                                        Make sure to remain out of the tent to get acclimatized. Try to not strain your body and relax at the camping site. 
                                        This is the best time to study the mountains around you, try to capture as much as you can.
                                    </p>
                                    <h3>Day 08 – Rest and Training day</h3>
                                    <p>
                                        This is a thoughtfully added day to help you get acclimatized to the high altitudes. You can consider this as a training day. Our team will be granting training on usage of technical equipment. 
                                        You will be taught how to walk in the snow, using crampons, handling of ice axe, and other techniques that are a must for the summit attempt. 
                                    <p>
                                        Try to grasp as much as you can. In doing so try not to exhaust yourself, as next will be the summit day. The delightful Kang Yatse peak will be staring at you throughout. 
                                        Additionally, you can also go nearby for height gain trek. Make sure you give enough rest to your body for the summit attempt. Pack your backpack for the summit day, keep it light and easy. 
                                        You will have dinner early to wake up midnight for the last climb. Hence, try to sleep early to get enough sleep.
                                    </p>
                                    
                                    </p>
                                    <h3>Day 09 – Summit, back to Base Camp</h3>
                                    <p>
                                        And the most exciting day rises! We will start midnight for the summit push to be at the top around 6 am for glistering sunrise. Carry packed lunch, refreshments and enough water for the strenuous climb. 
                                    </p>
                                    <p>
                                        Today you will be rising from 5100 meters to straightaway 6200 meters. Therefore, being mentally strong is the key. The route to the summit is through the famous Kang Yatse shoulder. 
                                        The trail starts with loose moraine and scree. Loose rocks make is difficult to secure the foot on the trail. Use a trekking pole for assistance. The route upwards has crevasses which are covered with snow most of the time. 
                                        Based on the level of snowfall you will have to rope up for safety. Be careful and watch every step. The route goes to the north face through the northwest ridge towards a rocky outcropping. 
                                    </p>
                                    <p>
                                        After a steep climb to the top, you will find the 360 degrees views of the Zanskar range, Karakoram peaks and mountains surrounding Kang Yatse. You might also notice the K2 peak (second highest peak in the world). 
                                        You may also spot the distant Tibet mountains. The views are flawlessly satisfactory and will bring back all the energy to climb down.
                                    </p>
                                    <p>
                                        The entire summit path is 5 km each way. When you retrace your steps, be cautious on the scree and moraines. Make sure you return from the top by 1 pm to avoid further issues due to weather. 
                                        Come back to the base camp to rest after the exhausting summit attempt.
                                    </p>
                                    <h3>Day 10 – Base Camp to Chokdo via Kongmaru La & drive to Leh</h3>
                                    <p>
                                        With satisfaction and happiness begin to return home. This day is like a bonus to enjoy the fulfilling mountains again. The terrain goes through the highest Kongmaru La pass. 
                                        After a strenuous climb of 2 hours to Kongmaru La pass, you will be pleasantly welcomed by the Karakoram range and the excellent array of peaks. 
                                        Hold your breath for the mind-blowing sight of the huge K2 Mountain, the second highest peak in the world. There are prayer flags moving along the wind. To your surprise, there is a good network connection from the pass.                              
                                    </p>
                                    <p>
                                        Further trek of 5-6 hours will bring you to Chokdo. The route has a steep descent and rocky terrain. It will present the amazing side of the cold dessert as you trudge through Indus and Ladhak valley.                              
                                    </p>
                                    <p>
                                        Today you will have mixed feelings, sadness because the trek ends today and satisfaction of completing the trek. The trail changes drastically through the scenery of Indus valley and Ladakh range. 
                                        This is the last stop of the entire trek, that also has many homestays. Look back and try to fill the salient features of the Ladakh in your eyes that you have just experienced while remembering the Kang Yatse II trek. 
                                        Your pick up vehicle will take you all the way down to Leh.                               
                                    </p>
                                    <h3>Day 11 – Buffer day</h3>
                                    <p>
                                        Buffer day is an extra day that is spent at the base camp for a summit attempt in case the whether does not allow. This day is used only when the summit is not possible as per the regular itinerary. 
                                        Whether to use the buffer day or not is totally team’s decision. Most of the big expeditions have 1-2 days buffer day. Therefore, it is important to consider this day and carefully plan your travel dates too. 
                                        The costing for buffer day is not included in the trek cost, buffer day cost is Rs 4,000 per day and per person. So, before using the day, make sure you and your team members discuss about it. Fortunately, if everything goes well as per the itinerary, we can plan the day for you. 
                                        We can make arrangements for other activities like Rafting, Biking, Pangong Lake visit etc. 
                                    
                                    </p>
