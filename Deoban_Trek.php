                                                <i class="fas fa-check"></i> The climber should be able to carry a 8 kg backpack.
                                            </li>
                                            <li>
                                                <i class="fas fa-check"></i> Heart problem, high blood pressure, asthma, and epilepsy cannot be reconciled on this mission. The climber must not have the conditions mentioned above.
                                            </li>
                                        </ul>
