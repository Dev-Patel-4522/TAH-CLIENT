                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 01: Drive Dehradun to Lokhandi and trek to Saraswati.</h3>
                                    <p>
                                        The pickup point is Dehradun railway station. Get on your vehicles that will take you on an elaborate mountain ride. The road passes through Chakarta, a famous hill station. It was a cantonment area of Britishers. It is not as popular as Nainital, though it is as beautiful. You can spot the heavily gushing waterfalls on the route. 
                                        You will arrive at your base village, Lokhandi in 4 hours. It is a scenic, peaceful village. The hiking begins with a small warm-up walk on road then goes into dense forests. The forests are filled with Deodhar, pine, oak and all types of coniferous trees. 
                                        As you walk more, you go deeper into the forest. In winters this section gets covered under the blanket of snow, far away from sunlight. 
                                        <p>
                                            As you reach towards the end of the trail, it gets steeper. The route rises and suddenly ends up at a clearing of Saraswati campsite.
                                            The serene resting place shows some glimpses of peaks you would see tomorrow. After satisfying your stomach, rest in your tents.
                                        </p>
                                    </p>
                                    <h3> Day 02: Saraswati to Vyas Point and back to Koti Kanasar via Saraswati. Drive to Dehradun</h3>
                                    <p>
                                        Since today will be the summit day and also the day to return, it will be long. Wake up early, have breakfast and get ready to leave at 5 am. 
                                        There is no water source, therefore, make sure you carry enough water. The initial section of the trail is steep which reduces gradient slowly. 
                                        <p>
                                            From dense forests, you will step into light forests to clear opening of grounds. Watch out for majestic peaks of Greater Himalayan range will start coming into visibility. 
                                            By this time the morning sunlight blossoms and shines perfectly on these mountains. Be all ready to capture the glittering mountains.
                                        </p>
                                        <p>
                                            After around 5-6 km you will find yourself on the highest point called Vyas Shikhar. This is an amphitheatre of grand peaks. At this site, you can find a stone 'Views of the Himalayas from Vyas Shikhar' 
                                            engraved with the directions to the major summits spotted from here. Get engrossed in the tallest peaks, Nanda Devi(7816 meters), Hardeol(7151 meters), Trishul(7120 meters), Chaukhamba(7138 meters), 
                                            Kedarnath(6940 meters), Dunagiri(7066 meters), Kedardome(6831 meters),  Kalanag(6373 meters), Bandarpunch(6102 meters), Srikantha(6133 meters), Swargarohini Massif( 6252 meters), Kirti Stambh(6270 meters), 
                                            Nanda Ghunti(6309 meters), Sumeru Parvat(6351 meters) and many more!
                                            Have your lunch and retrace steps to Koti Kanasar. From here board onto the vehicles that take you back to Dehradun.
                                        </p>
                                    </p>                                    
