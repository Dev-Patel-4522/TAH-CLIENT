                                <p>Approx Trekking Km :- 20 Kms.</p>
                                <p>Isn't it great if the riveting Himalayas are accounted in merely 2 days? Isn't it wonderful to witness India's highest peak without going higher? Of course, it is a lucky 
                                    opportunity for all the mountain lovers to witness yet another, a fascinating trek of 2 days Deoban trek. The essence of a weekend trek is that in less time and less effort 
                                    one gets to encounter the entire splendidness of the Himalayas. The Deoban trek is a winner in that aspect. From 3000 meters Deoban shows 55 big peaks including Nanda Devi, 
                                    Swargarohini, Bandarpoonch and the list is really big. The trek begins from Chakarta, an epic tourist place of British time. This less popular star still holds some of the 
                                    architecture of Britishers.
                                </p>

                                <p>The name Deoban means dense forests of Deodhar, therefore, no doubt the trail is flourished with Deodhar trees. Not only that, being the beauty of Uttarakhand mountains, a mix of 
                                    Pine, Oak trees can also be seen. The camping sites are flawlessly blessed with amazing meadows. Surprisingly, in winters the trail is covered with snow. Therefore, making it a 
                                    thrilling winter trek. Due to thick forest cover, it nurtures various species of birds including, babblers, thrushers etc. 
                                </p>
                                <p>
                                    The green cover magically rises into Deoban which is magnificent Coliseum showing the grandeur of huge peaks of  Nanda Devi(7816 meters), Hardeol(7151 meters), Trishul(7120 meters), 
                                    Chaukhamba(7138 meters), Kedarnath(6940 meters), Dunagiri(7066 meters), Kedardome(6831 meters),  Kalanag(6373 meters), Bandarpunch(6102 meters), Srikantha(6133 meters), Swargarohini Massif( 6252 meters), 
                                    Kirti Stambh(6270 meters), Nanda Ghunti(6309 meters), Sumeru Parvat(6351 meters) and you should visit and spot the rest!
                                </p>
