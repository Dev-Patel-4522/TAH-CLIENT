                                    <p>
                                        Here are the fitness criteria one has to match absolutely for conquering Deoban Trek with us:
                                        <ul>
                                            <li>
                                                <i class="fas fa-check"></i> First timer can apply, should have some interest in trekking and mountaineering.
                                            </li>
                                            <li>
                                                <i class="fas fa-check"></i> The climber must be fit and have sufficient stamina to cover 5 km of distance in 50 minutes without a stress.
                                            </li>
