                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 01: Arrive Srinagar, Drive to Sonamarg</h3>
                                    <p>
                                        Depart to Sonamarg in the afternoon. It is 3 hrs drive from Srinagar. 
                                        <p>
                                            As per the J&K government, foreigners(including OCI, NRI) need to obtain a special permit to trek in J&K. This permit is issued in Srinagar and therefore, 
                                            foreigners need to come one day before in Srinagar. This process requires your signed ILP, therefore it cannot be processed in advance and can be processed only 
                                            when you enter J&K. The charges of obtaining the permit will be Rs.2, 000 per person. TAH will cooperate with you in getting the permit. You have to pay this 
                                            amount to our representative and we shall get the permit, once you have arrived. Please make a note that your expenses for the additional day are not included in 
                                            our costs.
                                        </p>
                                        <p>
                                            Citizens of China, Myanmar, and Bangladesh are not permitted to trek in Sikkim.
                                        </p>
                                    </p>
                                    <h3> Day 02: Sonamarg (7,800ft) to Nichnai (11,500ft) via Shekdur</h3>
                                    <p>
                                        3 hour ascent followed by 1 hour descent and finally a gentle ascent to Nichnai (6 hours, 9kms).
                                        <p>
                                            The trek starts 3 kms out of Sonamarg, on the Srinagar road. Exactly at the 3 km mark, spot a lone Dhaba on the right. The place also sells packaged water, biscuits etc and is your last place to pick up short eats. 
                                            The next trace of dwelling civilization is only at Naranag at the end of the trek.
                                        </p>
                                        <p>
                                            A jeep track diverts to the right off the main road at the shop. The track goes down to the level of the Sindh river flows in between the mud track and the main road. 
                                            The trekking trail starts along the track but quickly diverts higher up. 10 minutes into the trek, the trail bends left and enters a green meadow. The meadow directly overlooks the Sonamarg town. 
                                            In half an hour you are at the top of the meadow where Maple and Pine trees start. At the tree line, the trail quickly descends to a tiny brook and then climbs again. What follows next is a lovely dense forest of Maple trees. 
                                            Walking on the green bed of grass amidst the Maples is an experience unique to Kashmir in India.
                                        </p>
                                        <p>
                                            For the next hour and a half, the trail winds up through the Maple trees. Stick to the trail heading uphill as the ones going down head to some of the nearby villages. The trees give way to clearings in between. 
                                            Turn around and see the view of the Sonamarg valley which gets better and fuller as you gain height. The Maple forest ends at the top of the ridge and the other side a meadow gently slopes down. Spend a few moments at the ridge taking in the views of the Sonamarg and its neighboring valley. 
                                            The streams, the meadows, the pines and the town nestled make a wonderful picture. The climb is now over and the trail slopes down into a meadow. The meadow is lined on the left by Silver Birch trees and a few shepherd huts. The carpet of green rolls down from the trees to the end of the 40ft wide meadow. 
                                            Small brooks which cross the meadow serve as water sources. On the right are small peaks with snow powdered on top of them. This is Shekdur. If you plan to have a short days trek on day one, this is where you pitch your tents for the day. It takes an average trekker about 2 1/2 hours to reach the meadow from the road head and add another half an hour if you decide to break the days trek at the end of the meadow.
                                        </p>
                                        <p>
                                            Shekdur is a beautiful campsite surrounded by Bhoj trees. This is the only campsite with trees on this trek and therefore adds a good variety to your moments and photographs. 
                                            If you have started from Srinagar in the morning, make Shekdur your first camp. The next campsite Nichnai is atleast 2 more hours away.
                                        </p>
                                        <p>
                                            If you started from Sonamarg, Shekdur, just three hours into the trek is too early to camp. Have lunch at the meadows and move ahead to Nichnai. The meadow of Shekdur stretches for half an hour. The gentle descent on the meadow ends in a forest of Bhoj or Silver Birch. Take the trail that goes in the middle of the forest and continue to descend gently. 
                                            Watch out for the sun rays making their way in between the thick foliage making it a nice show of light and dark. 
                                        </p>
                                        <p>
                                            In 45 minutes, you reach the end of the Birch trees and the trail goes down and climbs back into a river valley. The river valley is the beginning of Nichanai. The trail now is along the right bank of the river going upstream. Look behind to see snow clad peaks from Sonamarg valley. 
                                            You are now in wide valley but enclosed by mountains on either side. The river, which joins the Sindh eventually flows in speed through the valley. The first 30 minutes is over rocks and your mules will walk very slowly over here. 
                                        </p>
                                        <p>
                                            An hour into the river valley green patches devoid of rocks open up. The river valley widens and you can see the green meadow widening in between the two mountain ranges. 
                                            Far ahead lie triangular twin snow clad peaks. Choose a flat ground to pitch tents and end the first days trek.
                                        </p>
                                    </p>
                                    <h3>Day 03: Nichnai (11,500ft) to Vishansar lake (12,00ft)</h3>
                                    <p>
                                        Medium climb to Nichnai pass 13,500ft. Small descent followed by a long flat meadow walk ending near Vishansar Lake. (12 kms, 6 hours). 
                                    <p>
                                        The days trek is a long walk on meadows with the scenery changing for the better all along. Start by 8 am to give you enough time en route to enjoy the meadows. Your first destination for the day is to cross the Nichnai pass or Vishansar Berry. The pass is visible at a distance from the campsite. It lies just to the right of the twin snow clad peaks. 
                                        After half an hour of walking along the river, cross the river to move to its left bank. The next one hour, walk on the left bank. Notice that the trail slowly starts ascending as you traverse through the meadow. The following one hour is a climb to the pass. 
                                    </p>
                                    <p>
                                        As you climb, notic e a small lake at the foot of the mountains. The lake is deep blue in colour and you can sense your expectations raising of the main lakes to come on this trek. The pass is deceptive. The ridge seen from the meadow below is not the pass. The trail turns inwards twice and only then does the Nichnai pass come up. From the Nichanai pass, watch the Sonamarg valley and the trail you came from. 
                                        BSNL phone network tends to work here most times. This is the last point on the trek where you get phone network. The next sign of network is only when you move beyond Gangabal. 
                                    </p>
                                    <p>
                                        The Nichnai pass feels like a wide tunnel. On the left stand a series of snow clad peaks. There are no peaks on the right but the land raises on the right too. Nichnai pass is at an altitude of 13,500 ft. You are just into your second day of the trek and this by any standard is a very fast ascent. It is not uncommon for people to feel the altitude on the climb to Nichnai pass. 
                                        The good news is that the trail descends from here on. The rocky trail descends rapidly. In the next hour the rocks give way to grass. Red flowers spring out next to your feet. What you see ahead is a wide green meadow stretching for miles with mountains lining the sides. A new river flows down from the pass into the meadow ahead. 
                                    </p>
                                    <p>
                                        Stop here and take in the view of the peaks, the river below and the flowery meadow. Notice to your left a big waterfall splashing down the mountain cliff and joining the river. From the waterfall, the rapid descent ends and you are now walking through the flat wide meadow. On the left are the classic snow clad Himalayan mountains but notice on your right, grey and barren mountains resembling the Ladakh ranges. 
                                        Walk along in between the two ranges over the lush green carpet of grass. Two main streams flow through the valley. Stick close to the stream on the left. In half an hour, brace yourselves for another stream crossing for the day. The water is icy cold. 
                                    </p>
                                    <p>
                                        Continue in the meadow for an hour and a half until you reach the end of the valley. Another stream flows perpendicularly from your left to your right. This stream originates at the Vishansar lake which is a bit higher on your left. The lake is not seen yet and cannot be seen from the campsite. Pitch your tents anywhere beside the stream coming from the lake. There is ample camping space. 
                                        The Vishansar lake lies half a km to the left and 100ft higher from the campsite. Head left and follow the stream without crossing it. The lake is two mounds away and takes about 7 minutes to reach. The first impression you get on seeing the Vishansar lake is that it is big. It lies nestled below 4 mountains. 
                                    </p>
                                    <p>
                                        The Kishansar peak though 0.5 km away, reflects in the lake. The reflections are wonderful when the lake is still. The color of the lake depends on the time of the day and the clouds in the sky. Early in the morning, before the sun really shines, expect clear colorless water. The lake starts getting its colors when the sun shines. On a clear sunny morning, the water is absolutely blue. Clouds and the evening sun make it look greenish blue in the later half of the day. 
                                        Whatever be the color the lake looks wonderful and it is worth catching a glimpse of the lake in all its hues.  
                                    </p>
                                    <p>
                                        These lakes are full of trouts and trout fishing is popular. Obtain a permit from Srinagar for fishing. 
                                    </p>
                                    </p>
                                    <h3>Day 04: Acclimatization + Rest Day at Vishansar</h3>
                                    
                                    <h3>Day 05: Vishansar (12,000ft) to Gadsar(12,000ft) via Kishansar lake and Gadsar Pass (13,750ft)</h3>
                                    <p>
                                        TSteep 2 hour ascent followed by a steep descent followed a gentle walk in the meadows. (5 hours, 10 kms)
                                        <p>
                                            The next lake in series is the Kishansar lake. The Kishansar lake lies just about ½ km away and 500ft higher than Vishansar. The Kishansar lake lies at the base of the Kishansar peak. It takes about 45minutes to reach Kishansar lake from the campsite. Move right towards the stream and cross it where it is easy. The trail climbs up on the right side of the Vishansar lake. There are multiple tracks here. 
                                            For those keen on photography, the one going up is more attractive as you get the view of the meadow and the lake from a height. For those who prefer an easier trail, stick to the flatter trails. Though the two lakes are only 1.2 km away, there is no point where the two lakes are visible together.
                                        </p>
                                        <p>
                                            Kishansar is also big and blue. It has a big meadow stretching on its right. The lake and the meadow is bordered on the farther side by a ridge line that raises sharply. The trail climbs up to the top of the ridge and on the top of the ridge is the Gadsar pass. 
                                        </p>
                                        <p>
                                            Spend time at the Kishansar meadows photographing the lake from various angles. The next part of the trek is from Kishansar lake side to the top of the ridge. You see a pencil thin line traversing through the mountain side. That is your track to follow. Always stick to the one going higher though any them will take you to the top. It is an hour and a halfs climb to the top of the ridge a moderate pace. 
                                            45 minutes into the climb, you will be treated to one of the loveliest views you can ever imagine. Both the Kishansar and Vishansar lakes are in full view together. The view of the twin lakes lasts till you reach the ridge top or the Pass. This is called as the Gadsar pass ? altitude 13,800ft ? the highest point on this trek.
                                        </p>
                                        <p>
                                            On the other side of the Gadsar pass stretches a long valley with 2-3 small lakes visible. Far in the distance lie a series of snow clad peaks. The peaks lie outside our Line of Control.
                                        </p>
                                        <p>
                                            It is not uncommon to find snow at the Gadsar pass and also in the initial parts of the descent to the other side. The descent is straightforward. The first small lake that is seen on the left is the Yamsar lake. The locals tend to believe that lord Yama has some association with the lake! Beyond Yamsar is another nameless lake. An hours descent leads you into flat meadows again. 
                                            This valley is narrower with two ranges running on your either sides. Notice the multicolored flowers growing up out of the green grass. It starts with red and moves on to blue and purple Iris flowers. 
                                        </p>
                                        <p>
                                            When you are in the blue Iris area, you are almost at Gadsar ? one of the prettiest and most pristine of the lakes on this trek. Gadsar is at the base of snow clad cliffs. Blue flowers spring up one one side and snow slabs fall into the lake from the mountain on the other side. Look ahead to see the green blue valley gently slope down. The Yamsir, the nameless lake and Gadsar are all connected by a stream as water flows from the higher lake to the lower. Gadsar is again at 12,500ft. 
                                        </p>
                                        <p>
                                            Choose to make Gadsar you campsite for its sheer beauty but only if you can vow to leave the ground as neat as you found it to be. No one camps at Gadsar. There is a small abandoned army shelter near Gadsar.
                                        </p>
                                        <p>
                                            If you choose not to camp next to Gadsar, the next place to camp would be the Gadsar army camp. Continue on the downward trail from Gadsar and in half an hour the valley widens up. Spot another blue lake on the left of the valley. Notice the snow clad mountains now give way to lower barren mountains. You have lost considerable altitude again. In another half an hour down the trail, a few Shepherd huts come up. Continue downhill and at the end of the third half hour, the army camp comes up. 
                                            Gadsar army camp is just a small hut housing 5-8 army men. To move beyond the army camp, one needs permission from their Head quarters. The Head quarters is 3 miles further away down into the tree line near a village. The Gadsar camp communicates with the HQ through walkie talkies. The HQ is equipped with a satellite phone of the army. 
                                        </p>
                                        <p>
                                            If you are near the Gadsar army camp, it is best to report to them as soon as possible. It takes 2-3 hours for their green signal to come through. All details of the trekkers and staff including original id cards are recorded, collected and checked. 
                                        </p>
                                    </p>
                                    <h3>Day 06: Gadsar (12,000ft) to Satsar (12,000ft) </h3>
                                    <p>
                                        Gentle descent for 1 hour followed by a steep ascent for 3 hours followed a flat meadow walk. (6 hours, 12 kms) 
                                    <p>
                                        From the army camp move left towards the stream. The trail crosses the stream and heads up the mountain. The altitude at the stream is 10,600ft. The Satsar camp is at 12,000ft and almost 10 kms away. Crossing the stream is the easiest way to trek. 
                                        If one really wants to avoid getting their feet wet in the icy cold water, then head to the bridge which a kilometer away and cross the stream. The climb up is steeper if one goes to the bridge.
                                    </p>
                                    <p>
                                        After crossing the stream, take the trail that goes up the mountain. The trail now looks like a typical western ghat trail. The terrain feels very similar. You are just above the tree line. Trees and the river valley are visible below you. The hour and a half long climb takes you up by 1100ft. Once beyond the 11,500ft altitude, the climb graduates to a traverse. 
                                        The trail bends round to the left and leaves the river valley. You are now walking on a flat trail surrounded by mountains on a meadow. To you right are a few deep craters. This place is called as Maengandob.
                                    </p>
                                    <p>
                                        The landscape ahead is captivating. Isolated mountains stand in front. Towards the right is a small ridge. In between is a flat green bed with a stream flowing in between. Choose to camp here if Gadsar lake was your last camp. If the army camp was your start, it makes sense to cover a little more distance today and camp near the first of the Satsar lakes. 
                                        Satsar is actually a collection of 7 lakes. You can actually find 4 or 5 lakes with water, depending on the season you choose to trek.
                                    </p>
                                    <p>
                                        Beyond the ridge on the right is the Satsar army check post. It is the 3rd line of defence from the LOC. The same process of ID checking, collection and questioning repeats here too. Finish this today so that tomorrow is a clean day of trekking.
                                    </p>
                                    <p>
                                        Ten minutes out of the army camp is the first of the Satsar lakes. The lake is pretty big and looks picturesque in its green setting with mountains in front. Choose a place to camp here for the day.
                                    </p>
                                    </p>
                                    <h3>Day 07: Satsar (12,000ft) to Gangabal twin lakes (11,500ft)</h3>
                                    <p>
                                        Mild ascent followed by gradual descent followed by long steep ascent and long steep descent followed by gradual up and down walk. (6 hours, 9 kms)
                                    </p>
                                    <p>
                                        The days trek goes up and down replicating the trek as a whole which mostly goes up and down. Trek up half an hour out of camp, to reach the biggest Satsar lake. The terrain is bouldery and it is more of a boulder hopping exercise than anything else. The biggest of the Satsar lakes is also the last in the sequence. 
                                        After the last lake the trail starts to descend. Continue for half an hour on the main trail until you see the forest line ahead to your left. The right side is a ridgeline about 1000ft higher. It is time to gain height again. The altitude at the base of the climb is 11,800ft. Take the zig zag pony track to climb up to the top of the first ridge. 
                                        A 45 minute trek with limited breaks will see you on top of the first ridge. Once on top, you see two more ridges to climb. The trail from the base to top is barren and rocky. Looking behind at the opposite mountains, spot the Gujjar huts amidst the tall pines. You will not fail to notice the bareness of the mountains here and the greenery on the other side.
                                    </p>
                                    <p>
                                        A total two hour ascent brings you to the top of the 3rd ridge. The altimeter reads about 13,400ft. The best part however is the surprise view you get from the ridge top. Two lakes lie next to one another. A stream takes water from the higher lake to the lower. If you look keenly, two more blue lakes are seen one on either side at a distance. 
                                        The smaller lakes are nameless but the biggest amongst the set is the Gangabal. Its companion by the side is the Nundkol. You now know your destination. It is 1400ft below you. The route descends a bit more and ascends again to the lake. Take pictures of both sides and brace yourself for a steep stony descent. Your destination is either the Gangabal or the Nundkol. 
                                        The ascent is dry but on the descent you cross a stream mid way. Fill your empty bottles with the cold water and move on.
                                    </p>
                                    <p>
                                        An hour and a half steep descent brings you to green meadows again. The meadow is not too wide but stretches from your right to left. A kilo meter down left, you see the tree line again. Once at the base, the lake shore is still a good distance. The destination is seen but seems far away.
                                    </p>
                                    <p>
                                        A quick climb, a shorter descent and a stream crossing over a wooden log bridge brings you to the shores of Nundkol lake. Notice that this lake is not as pristine as the other lakes you saw on the trek. Remnants of camping are there all around. Lot of people trek up from Naranag to Gangabal and go back as a weekend outing. 
                                        You will wish they spared time and thought to clean up the mess they created by these beautiful lakes.
                                    </p>
                                    <p>
                                        The Nundkhol lake lies at the base of the Harmukh peak. The Harmukh glacier hangs on the the sides of the rocky edges of the mountain. Both the Gangabal and Nundkhol are famous for trout fishing.
                                    </p>
                                    <p>
                                        The Gangabal lake is about 20 minutes away from Nundkhol. A fiery stream flows on the right of the two lakes connecting them. The stream has to be crossed to go to Gangabal from Nandkol. Do not try to cross the stream at the lower levels but go all the way to the bank of Gangabal and on the right you find a good man made bridge laid out. 
                                        Gangabal is huge. A parikrama of either of the lakes will easily take an hour.
                                    </p>
                                    <h3>Day 08: Gangabal (11,500ft) to Naranag (7,450ft) Drive to Srinagar</h3>
                                    <p>
                                        Gradual descent followed by steep descent. (6 hours, 11kms).
                                    <p>
                                        The days trek is a killer on your toes and knees. From the Gangabal campsite head down along the stream towards the tree line. Don't walk beside the stream but walk along the ridge on the right. 30 minutes into the trail, your aircel phone could get glimpses of network just enough to tell home that you are alive. 
                                        The ridge ends and you descend to a green flat meadow on the right. The Harmukh peak looks impressive when you look back. The green meadow has little yellow flowers growing all over and you start walking gingerly to avoid stepping over them.
                                    </p>
                                    <p>
                                        An hour out of the campsite, you hit the fringes of the tree line. Pine trees line the meadows and you also see traces of civilization with the presence of a log hut. Do not walk into the forest directly but stick to its fringes and walk towards the right. 
                                        As a trademark of this trek, you don't lose altitude now but start to climb up a bit. You drop to 10,800ft at the tree line but climb again to 11,000ft. For about 6 kms you never really lose altitude and you are forever around the 10,800ft mark making your way in and out of the Pine forest and finally entering it fully. 
                                        Only after walking for 2/3 of the distance does the true descent begin. The descent is now really steep. The trail is a well trodden muddy one through the thick of the pines. The last 4 kms sees you dropping more than 3000ft. It is not rare to spot lot of people trekking up here from Naranag headed only to Gangabal.
                                    </p>
                                    <p>
                                        Naranag slowly comes in sight at around 8500ft but there is quite a bit more to go. The last stretch of the last day does become an endurance test but soon the stone paved village track comes up and in no time you enter the main road of Narnag.
                                    </p>
                                    </p>
                                    
