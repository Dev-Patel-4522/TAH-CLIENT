                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day1:- Dharamshala to Liyoti via Kareri Village:</h3>
                                    <p>
                                        Pick up will be arranged from Dharamshala at 10:00 in the morning from where we'll drive to Kareri village which will approximately be a 2-hour drive. After reaching, your trek leader will 
                                        brief you about the trek to Liyoti. You can keep your extra luggage at Kareri Villageso that there is no extra load to carry ahead. It is approximately a 6km trek which passes through forest 
                                        and grassland which is mostly an uphill climb. You will be provided with pack lunch on the way and hot lunch will be served at the campsite. We will be camping at the riverside for the night, 
                                        where you'll be served evening snacks and dinner. 
                                    </p>
                                    <h3> Day2:- Liyoti to Kareri Lake Trek:</h3>
                                    <p>
                                        On the second day of the trek, you will be moving to the Kareri Lake Trek which is approximately at a height of 10,800 ft. Hot breakfast will be served along with tea before you start your trek. 
                                        This route will take 4-5 hours as it has some steep climbs. You will walk through some Oak, Pine and Rhododendron trees on your way to Liyoti. The trail passes along the river which you will cross 
                                        a number of times. When you reach the lakeside, enjoy the view and the stunning beauty that of the Dhauladhar range. We'll be camping here for tonight and your dinner will be served at your campsite.
                                    </p>  
                                    <h3> Day3:- Kareri Lake Trek to Kareri Village and drive to Dharmshala :</h3>
                                    <p>
                                        After having your breakfast, spend some time exploring areas around Kareri Lake Trek and then Back to Kareri Village (Breakfast, Lunch) and drive to Dharmshala.
                                    </p>                                        
                                    <h4>How to Reach</h4>
                                    <h3>By Bus:-</h3>
                                    <p>
                                        Dharamshala is well-connected to Delhi and other parts of North India via a network of state operated buses as well as private tour operators. 
                                        The journey is almost 520 kilometres from Delhi.
                                        <p>
                                            Most buses stop at the main bus terminal in Lower Dharamshala, but there are also some public Haryana Road Transport Corporation (HRTC) buses that go all the way to the main square of McLeod Ganj.
                                             An overnight journey from Delhi takes about 13 hours
                                        </p>
                                    </p>
                                    <h3>By Train :-</h3>
                                    <p>
                                        An overnight train journey is a good option to reach Dharamshala. The nearest major railway station is at Pathankot, 85 kilometres away. There are numerous trains that go to Jammu and Kashmir that stop 
                                        by in Pathankot. You can take a taxi or bus from Pathankot to reach Dharamshala. There is also a smaller railway station, Kangra Mandir, just 22 kilometres from Dharamshala but none of the important trains 
                                        halt here.
                                    </p>
                                    <h3>By Air :-</h3>
                                    <p>
                                        The nearest airport is at Gaggal, about 13 kilometres away from Dharamshala. Gaggal airport connects Dharamshala to Delhi via Air India and Spice Jet flights. Tourists from other parts of India would find it easier 
                                        to take a flight till Chandigarh and book a taxi for their onward journey to Dharamshala, which is about 275 kilometres away.
                                    </p>

