                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1: From Manali Drive to Gulaba and Trek to Gulaba Meadows Camp</h3>
                                    <p>
                                        Journey commences from Manali, on the Rohtang Pass Road toward the little hill nook hamlet of Gulaba. Three hill towns—Kolang, Palchan, and Kothi marks the way, and it gets more and more beautiful
                                         with pine forests as we go. Snow covered elevations surround the little locale picturesquely. Sweet grasslands washed over by the watercourse of Beas would make fantastic frames at Gulaba , only if 
                                         the Rohtang transit crowd wasn’t there.
                                    </p>
                                    <p>
                                        After lunch at the village, we follow a short trek, near about 1.5 hours to our first camp in the grasslands nearing a forest line. The trek is a complete pleasure and no hardships, navigating through
                                        the conifers shade dark and deep, and eventually coming to an open clearing.
                                    </p>
                                    <h3> Day 2: Gulaba to Rola Kholi</h3>
                                    <p>
                                        Today we start for an altitude of 12,500 ft. spanning 6 km in 3-4 hours. It’s going to be steep, steep walk, so you need to go equipped with 2 liters water and little snack bites to unwind between the 
                                        testing laps.
                                    </p> 
                                    <p>
                                        For the first few miles we are still trailing under a forest cover and bathing in the shade of rustling conifers. You can easily spot oaks, cedar, and birches. It’s a beautiful sight to catch a glimpse 
                                        of blue through these leaves.
                                    </p> 
                                    <p>
                                        Walk on 20 minutes into the trek, and the forest has now ebbed behind, unfolding only rolling green meadows before. This is the shepherds’ zone where cattle grazers are brought from the local villages all through summer. 
                                        You could find a few mud huts and camp poles lying here. Keep the meadow behind and start heading south. You will find the peak of Hanuman Tibba rising in sharp focus at this point and a little below, the Seven sisters Peaks. 
                                        Lying somewhere far below is the green patch of Beas Kund valley.
                                    </p>
                                    <p>
                                        Next comes another slight jungle cover, passing which the trail falls on to the rugged meadows again. The thrill climaxes when the path gets narrower and steeper just before entering the grasslands of Rola Kholi. 
                                        At about 15 to 20 minutes far from reaching the campsite, we pass two nallahs or small streams which are also our water sources. Our camp is set near chor nallah at the Rola Kholi campsite.
                                        This night is the night of wonder under a sky breaking into a zillion stars!
                                    </p>
                                    <h3> Day 3: Rola Kholi to Bhrigu Lake back to Rola Kholi</h3>
                                    <p>
                                        Today’s is an out and out landscape change from all of what we have experienced in the days before! Starting off way early in the morn we traverse a distance of 10 kilometers in approx 7-8 hours on a steep and massively stony path. 
                                        The verdant meadows of the last two days feel like a dream but this landscape is no less captivating, and gets all the more refreshing in the cool mountain climate.
                                    </p>           
                                    <p>
                                        Till June, you can expect this course post Rola Khuli to be covered in snow. From mid-July on, the gradients of brown, green, and white gets prominent as the snow melts off. On the last leg of the trail, it’s highly advisable to go slow 
                                        and not hurry at all as the path is ridden with scree and slippery boulders.
                                    </p>        
                                    <p>
                                        The alpine lake becomes visible between the two ridges that we reach at the end of the last boulder-bound lap. Be it high winters or the beginning of summer, the lake is never entirely frozen. This is an incidence attributed to the power of 
                                        Sage Bhrigu of the ancient myths to whom this lake is named.
                                    </p>          
                                    <p>
                                        From the high belt surrounding the lake, you can catch a istant glimpse of the Solang Valley nestling far away and on a clear day, the sky lights up with the splendid peaks of Pir Panjal and Dhauladhar range.
                                    </p>   
                                    <h3>Day 4: Rola Kholi to Gulaba drive to Manali</h3>   
                                    <p>
                                        Today we trek back the same treaded path through the delicious green meadows in one long sweep to Gulaba which will take approx 2 to 3 hours, and back from there to Manali by car, we will reach Manali in between 2 to 3 pm.
                                    </p>  
                                    <p>
                                        Note: There is an alternate return route to Manali via Vashisht and Pandu Ropa from the Bhrigu Lake site, but we find this path more relaxing and sustainable for a soothing green trek.
                                    </p>  
                                    <h4>How to Reach</h4>
                                    <h3>By Bus:-</h3>
                                    <p>
                                        Manali is well connected to Delhi. Take an overnight bus from Delhi to Manali. Tickets can be booked online at www.redbus.in or www.hrtc.gov.in, Tip: Since Delhi Manali is a long journey most Volvo private buses leave Delhi between 5 pm and 6 pm. 
                                        The last govt bus leaves at 8.30 pm from ISBT Kashmere Gate.
                                        <p>
                                            Manali to Delhi Like the onward journey, buses leave from Manali to Delhi between 4 and 5 p.m. Your tentative arrival at Delhi may be anywhere between 6 a.m and 10 a.m. Plan your onward journeys only post noon giving enough buffer for bus delays.
                                        </p>
                                        <p>
                                            Note:- Trekkers need to take only those VOVO buses which reach Manali till 11.00am on first day. Our reporting time is 12.00pm at Manali but in-case you will not able to reach at meeting /briefing time, either we will transfer your seat to our any next batch or we will cancel your
                                             trek, so please double check your bus time with your bus transporter.
                                        </p>
                                    </p>
                                    <h3>By Air :-</h3>
                                    <p>
                                        Bhuntar, which is 52 km away from Manali, is the nearest airport. Taxi services are available from Bhuntar to Manali, which costs about Rs. 700- Rs1000. Bhuntar is well connected to Delhi by air. The airliners 
                                        that take you to this Airport are Indian Airlines, Kingfisher airlines, MDLR Airlines and Jagson Airlines, operating flights from Delhi and Chandigarh to Kullu.
                                    </p>

