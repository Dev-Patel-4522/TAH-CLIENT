                                <p>Approx Trekking Km :- 25 Kms.</p>
                                <p>Trekking to high altitude glacial Lakes in itself is quite adventurous and manages to catch the interest of every hiker. Usually these high 
                                    altitudes lakes are difficult to reach and needs days of hiking and crossing difficult terrains, before the sight of the lake can be 
                                    cherished. But all these tough complications can be kept at bay with the beautiful Bhrigu Lake Trek in the upper Kullu Valley based in the 
                                    eastern side of the famous Rohtang pass in Himachal Pradesh. This is ideal weekend hiking trip promising loads of entertainment for both 
                                    beginners as well as experienced hikers. Nestled at an altitude of 14,000 ft the Bhrigu Lake Trek offers fascinating natural vistas and 
                                    breathtaking views of snow clapped mountains, lush meadows ad verdant rich forests.
                                </p>

                                <p>One of the popular trekking expeditions, The Bhrigu Lake Trek has gain preference due to its unconditional display of natural extravaganza 
                                    and being one of the easy The Adventurers Himalayan. People with competent physical fitness can easily enjoy the privilege of hiking to the 
                                    exclusive lake region even without previous hiking experience.
                                </p>
                                <p>
                                    The scenic Bhrigu lake has significant historical and religious implications. As per informations provided by the locals, there is an association 
                                    of the Lake to Sage Bhrig since the pious soul is known to have perform strong meditation and rituals in the banks of the lake hundreds and 
                                    hundreds of years ago. Even the locals believe that many Gods of the Kullu valley have dipped in the holy waters of the lake making it 
                                    sacred for the devotees. The location of Bhrigu Lake is about 6km from Gulaba which is a pictorial village about 22 kms from Manali.
                                </p>
                                <p>
                                    Be it the unparallel breathtaking views of Seven Sister Peaks or Deo Tibba and Hanuman Tibba, the outstretched Pir Panjal ranges, the rich line of 
                                    oak and cedar forests, fascinating green lush meadows, enthralling carpeted alpine flooring or the gushing streams along the meandering trails, the 
                                    scenic vistas of the region is absolutely captivating which also makes the trekking experience overwhelming. With good luck one can also rejoice the 
                                    sight of Himalayan Monal, Snow Cocks and the beautiful pheasants.
                                </p>
                                <h3>Best Season to visit</h3>
                                <p>
                                    Starting from Mid May, till the month of October, trekking to Bhrigu Lake is absolutely allowed and travelers can plan accordingly. However the ideal 
                                    time is before the month of June and after August since this is the monsoon time and there remains the probability of landslides in the region. It must 
                                    also be noted that the temperature of this place during summers hovers around 20-25C in the daytime and falls to 5-7C at night. In winter the temperature 
                                    gets lower with 10-15C around daytime and nearly 0 to -7C at night. Almost half a year the lake remains frozen, and travelers can enjoy the sight of snow 
                                    even during the early summer time.
