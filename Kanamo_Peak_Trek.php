                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1:-(Manali to Kaza)</h3>
                                    <p>
                                        You will take you by the road to Spiti from Manali. There is always a possibility of getting caught up in traffic snags on this otherwise beautiful road especially around Rohtang Pass from where a spectacular view of could be found. 
                                        The Manali-Leh highway offers interesting vistas of arid mountains throughout the journey but disappoints travelers with ill-maintained, rutted roads. Our advice—reach one day early in Manali and start off at dawn to enjoy the scenic route devoid of traffic. 
                                        On reaching Gramphu, you would be directed towards Kaza road leading to our first stop.
                                    </p>
                                    <p>
                                        Crossing Batal, you will be ushered into Spiti Valley. Picturesque villages surrounded by green patches of agricultural fields are a treat to the eyes and this view will remain with you till Kaza at the height of 11,980 feet. 
                                        Here we will stop for the night at a guest house. Dinner will be served from the TAH house. 
                                    </p>
                                    <p>
                                        As per the Himanchal Pradesh government, foreigners(including OCI, NRI) need to obtain a special permit to trek in Himanchal Pradesh. This permit is issued in Shimla and therefore, 
                                        foreigners need to come one day before in Manali. This process requires your signed ILP, therefore it cannot be processed in advance and can be processed only 
                                        when you enter Himanchal Pradesh. The charges of obtaining the permit will be Rs.2, 000 per person. TAH will cooperate with you in getting the permit. You have to pay this 
                                        amount to our representative and we shall get the permit, once you have arrived. Please make a note that your expenses for the additional day are not included in 
                                        our costs.
                                    </p>
                                    <p>
                                        Citizens of China, Myanmar, and Bangladesh are not permitted to trek in Himanchal Pradesh.
                                    </p>
                                    
                                    <h3> Day 2:- Kaza to Kibber</h3>
                                    <p>
                                        We will take you on an hour’s drive from Kaza to Kibber situated at an altitude of 14,108 feet. Boarding here will be provided at a guest house in the village neighborhood. 
                                        Zigzagging through the cold deserts you will reach the village by lunchtime. The rest of the day could be spent exploring cultural sites in and around. 
                                        The ancient Key Gompa is situated in Kaza. 
                                    </p>
                                    <p>
                                        The Monastery’s high reaches offer you a great view of the surrounding valley and the Spiti River flows by silently many feet below.
                                    </p>
                                     <p>
                                        Spend your hours in the shadowy alcoves of the monastery in company of the monks, going over prayer verses hidden in olden artifacts and manuscripts. Giving you the first taste of the rocky terrain of the mountain desert, an acclimatization walk will be conducted in the afternoon and the evening reserved for exploring village life. 
                                    </p>
                                    
                                    <h3>Day 3:- Acclimatization Stay</h3>
                                    <p>
                                        To prepare you for the heights, this day will be kept for acclimatization in Kibber situated at an altitude of 14,108 ft.
                                    </p>
                                    <h3> Day 4:-Kibber to Kanamo Base Camp</h3>
                                    <p>
                                        The dry grass and desert valley of Kibber offers a challenging trek on this day. Continuous ascent for the first 3 hours will be followed by a slow descent on a total 6-7 hour trek. The trail goes through a number of small villages with flourishing harvest fields and a couple of mountain lakes which are the main source of water in Kaza town. 
                                        The path is filled with a variety of flowers among which the beautiful Brahma Kamal deserves a special mention.
                                    </p>
                                    <p>
                                        Pay attention to your water supply on this day, making sure to carry at least 2 liters. The lakes on passage are the only drinkable water source that you could find.
                                    </p>
                                    <p>
                                        At the height of 15,748 feet in the Kanamo Peak base camp, close to the Kanamo Lake, you will be resting for the night.
                                    </p>
                                    <h3>Day 5:- Kanamo Base Camp to Kanamo Peak and back to Base Camp</h3>
                                    <p>
                                        Your ascent summit-ward will begin at the break of dawn as the sky slowly comes to light and the mountain ranges all around emerge from the darkness. The cold is something to be aware of in these wee hours of the morning, so, you must layer up wisely. 
                                        To guard yourself from the bitter wind, wear bacalavas. Today, you will be given packed breakfast and lunch to be had stopping on the way at a favorable hour.
                                        <p>
                                            Loose stone chips fill up the whole trail and in the last phase snow will be found. In this scenario, stepping with a firm toehold is very important; the trick is to take large and careful steps. Avoid falling with support from your trek guide. The trek to summit is mixed with parts of sheer ascent and relaxing flat walk. 
                                            In the ascent of 7 hour, the last 3 hour stretch will be a snow climb, be prepared for this with crampons. You will find a surface of scree and snow and as you go higher up, the snow will deepen.
                                        </p>
                                        <p>
                                            Get ready for a spellbinding view of the Greater Himalayas from Kanamo top. An interesting bird’s eye view of the entire Kinnaur-Lahaul-Spiti territory will welcome you. The renowned passes—Parang La and Pin Parvati can be seen clearly at a distance. To the back of Kanamo you will find a massive glacier and snow on the nearby peaks.
                                        </p>
                                        <p>
                                            The Kanamo summit is expected to remain free of extreme snow, but you must be careful to prepare adequately for the last phase of snow mixed scree.
                                        </p>
                                        <p>
                                            The descent to base camp will be relatively easy and take lesser time, approximately 5 hour.
                                        </p>
                                    </p>
                                    <h3>Day 6:- Base Camp to Kaza via Kibber</h3>
                                    <p>
                                        This day, after breakfast you will be trekking your way back to Kibber, stopping for lunch here followed by a drive back to Kaza. 
                                        The path is full of scenic brilliance with a magnetic backdrop of the mountains left behind.                                    
                                    </p>
                                    <h3>Day 7:- Kaza to Manali</h3>
                                    <p>
                                        The way back to Manali will be by the familiar road, the drive starting at 4 am in the morning. Transportation will be exclusive of our charges. 
                                        You’ll reach Manali between 6:00 pm and 7:00 pm. You can book your travel any time after 7:30 pm.
                                    </p>
                                   
