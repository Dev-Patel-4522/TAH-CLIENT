                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 01: Haridwar to Joshimath</h3>
                                    <p>
                                        You will begin your journey with us from Haridwar. Starting the drive from Haridwar from the appointed pickup point 
                                        (Haridwar railway station), we will take the fabled Badrinath Road. We will be crossing Rishikesh and conveniently arrange 
                                        pickup from there too. Alternatively, you can arrange your stay at these awesome places on your way back after the trek as well.
                                        <p>
                                            The nine-hour drive to Joshimath will surely be long and arduous, but you are never going to run out of mesmerizing views. 
                                            The great river running with us on parallel will get wilder and more fascinating as we trudge uphill. We will be passing by the 
                                            4 of the Panchprayag or the five holy confluence of Ganga—Devaprayag, Rudraprayag, Karnaprayag, and Nandaprayag. So, keep your cameras 
                                            ready and remain awake for most of the journey!
                                        </p>
                                        
                                        
                                    </p>
                                    <h3>Day 02: Joshimath - Ghangaria Trek</h3>
                                    <p>
                                        From Joshimath to Govindghat the distance is 22km. En route, you can spot the final Vishnuprayag. We will start off on the 
                                        drive in the morning after breakfast and reach Govindghat by 8 am and then 4 km drive to Pulna which is the starting point. 
                                        It will be a 9 km uphill route along the river, Pushpawati. The entire trail will be crowded most of the time. Many snack shops 
                                        selling the beverage, hot noodles, omelets with bread, paratha, and the like can be found on this trail. So, you will never go hungry. 
                                        The trek will be on a clearly defined stone-paved path, conveniently built with stairs. After 5 km walk, you will arrive at Bhyundar 
                                        village. This village marks an amazing viewpoint for clear Hathi Parvat sights. Enjoy the wonderful sights as much as you can, because 
                                        the strainful steep ascent will commence henceforth. You will reach Gangharia before nightfall, attaining an altitude of 10, 000 feet. 
                                        The stay will be at a guest-house.
                                        
                                    </p>
                                    <h3>Day 03: Ghangaria - Valley of Flower - Ghangaria.</h3>
                                    <p>
                                        The journey to Valley of Flowers is a distance of 4 km. The valley is 6-7 km long; the distance travelled in the valley depends on 
                                        how the trekkers want to explore the valley. Another, factor to be considered is the stamina of the trekkers.
                                    <p>
                                        Right, when you start from Ghangaria in 30 minutes forest check post arrives. The check post might not be an attraction but right before check 
                                        post, there is a gushing waterfall called Laxman waterfall. Another 20 minutes into the greenery and next furious attraction comes. At 
                                        this point, the Pushpavati river flows furiously below a wooden bridge. It is quite a common yet a unique and mesmerizing capture. The 
                                        trail is steep and stone paved like the previous day. You can find many bridges and landslide areas when you enter the valley.
                                    </p>
                                    <P>
                                        The valley is thousand times stupendous than you might have imagined. It truly makes the Himalayan peaks lively and cherishing. Blue Poppy, 
                                        Snake Foil, White Leaf Hog Foot, Himalayan Rose, Meadow Geranium, Dog Flower, Hooked Stick Seed are the native flowers. These astounding 
                                        colorful blossoms end after 5 km stretch at a glacier. If the weather is clear the mighty Himalayan peaks of Nilgiri Parvat, Bhyundar Khal, 
                                        Rattaban, Gauri Parvat etc. in the background. Today’s journey will conclude with your coming back to Ghangaria before nightfall.
                                    </P>
                                    

        
                                    </p>
                                    <h3> Day 04: Ghangaria - Hemkund - Ghangaria</h3>
                                    <p>
                                        Get readyearly in the morning to start trekking (6 Km) for "HEMKUND SAHIB". One of the most revered of all Sikh Shrines, The Hemkund Sahib, 
                                        the World’s Highest Gurudwara, is situated at an altitude of 4,329 meters. There will be porters and mules if you wish to offload your 
                                        load. The trail today will be similar to previous days stone paved and continuous ascend. There will be shops on the way where you can 
                                        energize yourself. Watch out for the rare flower Brahmakamal that will be spotted beside pathways.
                                        <p>
                                            The Gurudwara is placed magically beside the mirror like glacial lake Hemkund. During clear skies, one can witness the flawless reflections 
                                            of the adjoining Saptrishi peaks. The lake is fed by melting glaciers of Hathi Parvat and Saptrishi peaks. Religious Laxman temple is also 
                                            found near the lake. In the afternoon trek down to reach Ghangariya for an overnight stay. The evening is free for rest & relaxation.
                                        </p>
                                        
                                        
                                    </p>
                                    <h3>Day 5 : Ghangaria to Govindghat and drive to Joshimath</h3>
                                    <p>
                                        After your breakfast in the morning we descend to Govindghat and then drive to Joshimath. From this place Badrinath is 25 km away and take 
                                        1 hrs to reach. You can visit Badrinath only if road and weather condition permits us.
                                        <p>
                                            Govindghat to Badrinath is landslide prone area in monsoon time so if our leaders find that your safety is in risk due to weather 
                                            condition/landslide or road blockage we shall skip Badrinath altogether and drive to Joshimath directly.
                                        </p>
                                       
                                        
                                    </p>
                                    <h3>Day 06: Joshimath to Haridwar</h3>
                                    <p>
                                        The ride will commence at 6 am and you will reach Haridwar around 6 - 7 pm. Your Breakfast and Lunch will be on the way at a roadside inn, 
                                        exclusive of charges, after having your morning tea/coffee you can leave for your next destination. We advise you to book further travel 
                                        after 8 pm.
                                        
                                        
                                    </p>
                                    
                                    

                                    
                                    
