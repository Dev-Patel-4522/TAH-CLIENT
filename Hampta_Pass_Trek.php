                                    Generally, it takes 4 days to complete the Hampta pass trek, but one more day is further added to the itinerary so as to visit the well-known and striking Chandrataal, a high altitude alpine lake few hours from Chatru. The best months to trek are May and June and August through October. 
                                    The reason why Hampta pass is famous trekking zone is snow; the pass has lump sum snow which draws more tourists.                                   
                                </p>
                                
