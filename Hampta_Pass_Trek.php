                                    The nearest station is Manali. Therefore, the trip starts from Manali to Chika via Jobra. From Jobra the trek is undertaken by walk. This route comprises the first day. Balu ka Ghera is reached on the second day. You would cross amazing boulders, Gendrame and Rani Nallah on this day. 
                                    The third day destination is Shea Goru from Balu ka Ghera via Hampta Pass. You will come across mind boggling and stupendous glaciers, cornice formation and snows all the way along. The maximum altitude will be reached on this day i.e. 14, 100 ft. Day four will start from Shea Goru and end up in Chatru. 
                                    It is a steady descent on this day. You will witness the valleys of Lahual and Spiti and also the mountain ranges of Pir panjal and Spiti. On the same day you can also head to Chandra tal camp site and camp there for the night. The final day of the trek's route is from Chandra tal to Manali. 
                                    From Chandra tal campsite you can visit the infamous chandratal lake, deepest amongst blue shades.
