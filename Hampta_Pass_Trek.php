                                <p>Himachal Pradesh is one of the most toured states in India. The state has beautiful pristine snow clad mountains which are a major source of tourist attraction. 
                                   Tourists from all over the world come to this glorious, unpolluted and serene place to nurture their mind and soul and cherish an unfathomable nature's beauty. 
                                   The word 'Hima' means snow in Sanskrit and hence the name Himachal Pradesh which means 'In the lap of Himalayas'. The mountains of the state are used for many adventure sports. 
