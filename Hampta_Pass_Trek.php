                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1: Manali to Chika via Jobra</h3>
                                    <p>
                                        The trekking expedition starts from Manali to Jobra in vehicle. Manali is already at an elevation of 6, 700 ft above sea level. So, it is a good place to acclimatize your body before beginning the trek. 
                                        It takes two hour and forty hairpin bends to arrive at Jobra. Jobra is at an altitude of 9, 800 ft. On the first day you will reach a height of 10, 100ft and cover up two to three hours of trek.
                                    </p>
                                        <p>
                                            The trek on foot initiates from Allain Guhugal Hydel Project junction. After crossing the main road, take the small path that leads to the forest of pine trees. 
                                            You will also find other variety of trees like maple, deodar, toss and oak. The sight is refreshing. The slope that is supposed to be climbed is an easy one and not very steep. 
                                            Twenty minutes into the trek, you will reach a bend that overlooks a lush green meadow strewed with small rocks. Enjoy the surrounding for a couple of minutes and head towards the meadow. 
                                            The Rani nallah comes into view which flows directly in between the meadow. You will also see few sheep and cows grazing in the field. The sight looks exactly as if taken out from fairy tale. 
                                            You can pass through the meadow or climb up the hill on the left hand side. Either way you will reach Chika which is at the end of the vast meadow. But, climbing the hill and descending down to Chika is a better and easier route. 
                                            Chika camp site is a huge, spacious patch of green. The Rani River flowing in the middle makes this place perfect for setting up the tents.
                                        </p>
                                        <p>
                                            As per the Himachal Pradesh government, foreigners(including OCI, NRI) need to obtain a special permit to trek in Himachal Pradesh. This permit is issued in Shimla and therefore, 
                                            foreigners need to come one day before in Manali. This process requires your signed ILP, therefore it cannot be processed in advance and can be processed only 
                                            when you enter Himachal Pradesh. The charges of obtaining the permit will be Rs.2, 000 per person. TAH will cooperate with you in getting the permit. You have to pay this 
                                            amount to our representative and we shall get the permit, once you have arrived. Please make a note that your expenses for the additional day are not included in 
                                            our costs.
                                        </p>
                                        <p>
                                            Citizens of China, Myanmar, and Bangladesh are not permitted to trek in Himachal Pradesh.
                                        </p>
                                    
                                    <h3> Day 2: Chika to Balu ka Ghera</h3>
                                    <p>
                                        The next destination is Balu ka Ghera which is not visible from the Chika camp site, but you can make an idea as it falls to the right. Follow the left bank of the river that will appear at your right hand side. 
                                        The trail leads the way to a slope of boulders and rough terrain. The path is an easy one and can be accomplished without any difficulty. You will find an entire stretch of the river's right bank lined up with amazing dwarf Rhododendron trees. 
                                        These trees will cover the lower and the central region and cluster of silver birch trees cover up the upper region. Once you have reached the end of silver birch trees, wait a moment and look back to marvel the entire range of Dhauladhar snow clad peaks in front of you. 
                                        The view is very picturesque. You will reach a clear stream waterfall to the left at a distant. Best place to fill in your bottles.
                                    </p>
                                    <p>
                                        After crossing the waterfall, the next route is towards the river to your left. You can go to the other side only by crossing the rivulet by wetting your feet. The water is super cold and will definitely prick your feet. 
                                        You can also jump from one rock to another in the stream and get to the opposite side. You must have finished half of the day's trek when you enter Jwara. Jwara is a beautiful scenic sheltered valley; huge snow clad rocks at one side and the valley drops down to the river on the other side. 
                                        You will feel like immersing yourself in the beauty of God's nature. You will find snows melting into the river and cattle grazing the land. In front of the picture is snow clad mountains and behind them lies the Hampta Pass. 
                                        From here, the trail would be by the river side which will lead you to another rectangular valley. The path is covered with multi colored flowers. The last of the tree lines vanishes out of the sight and is left behind. It will take another half an hour or so to reach the end of the valley, which is the finishing part of Jwara. 
                                        After Jwara, the path is a slope covered with snows and rocks. The end of the path is Balu ka Ghera, the name has nothing to do with the place. The place is covered with snow and greens whereas the name imply mound of sand. But it is said that this place was formed with the sands and dust deposited by the river. 
                                        Balu ka Ghera campsite is a flat ground amidst the mountains that you will cross the next day to reach the pass. Balu ka Ghera is at an elevation of 11, 900 ft.
                                    </p>                                       
                                    <h3>Day 3: Balu Ka Ghera to Siagoru crossing Hampta Pass</h3>
                                    <p>
                                        The day's trek encompasses 19 hours. You can split the journey in two halves, a steady ascent on moderate steep slope to Hampta Pass, thence a sharp descent to Sia Goru. The highest altitude will be achieved on this day at Hampta Pass (14, 100 ft). 
                                        The day starts by heading towards the mountains you spotted the previous day. The trail is along the river side. The view is amazing, tiny yellow and orange flowers give a pleasant picture to the river. The river side trails takes you further to the base and then the actual climb to the Hampta pass takes place. 
                                        It will take an hour to reach here. Another hour of walk will take you to the first plateau. In summer, the place is completely covered with thick fresh snow. Otherwise, it is just patches of snow everywhere. The Deo Tibet peak will fall right in front of you.
                                    </p>
                                    <p>
                                        You will reach another plateau and then to a ridge. It will take another half an hour to reach the ridge's end. Then from the first ridge trek further up to get on the second ridge. Hampta Pass is just a few meters and a bend away from the second ridge. The climb to Hampta Pass is a vertical one and is a hard mount. 
                                        It will take about 15 minutes to reach the pass. Yes, by the time you reach there, you would be extremely tired but the site which enfolds in front of you is stunning. Enjoy for sometime in the pass, relish the beauty around, have your lunch and then get ready for the next descent to Siagoru.
                                    </p>
                                    <p>
                                        The descent is trickier than the journey to the pass. The decline is so steep that you would not be able to locate the base of the mountain, not even by craning your neck. The best possible way to get down is either by sliding down the slope or with the use of ropes for support. The route is a little meandering too. 
                                        You will also spot Spiti from this point. In an hour and a half you will reach the base of the valley that will lead you to Siagoru. The valley is secured by snow clad mountains, and the side which is open is Siagoru's way. The trek to Siagoru is easy on flat land.
                                    </p>
                                    <p>
                                        The Siagoru camp site is also by the river side. Set your camps on the grounds and retire for the night with a feeling of contentment at the heart.
                                    </p>
                                    
                                    <h3> Day 4: Siagoru to Chatru</h3>
                                    <p>
                                        You will cover Sia goru to Chatru by foot and it will take approx 5 hours of easy downhill trek. Siagoru is placed at an altitude of 12, 900ft and Chatru at 11, 000ft. It is a gentle trek downhill from Siagoru. The trail here again is by the side of the river valley between mountain ranges. 
                                        Chatru can be located at a distance. No vegetations can be seen in the barren mountains. The tricky part comes next.
                                    </p>
                                    <p>
                                        You will have to be careful as the trail might sometime lead you to edges of mountains and ridges. The route has slippery parts too. So, sure footed and careful navigation is required to go further. Nevertheless, the trek is not that difficult as it sounds. You will for sure enjoy the sliding and heart pumping mounting. 
                                        Getting down the mountains will consume another two hours of your time. You will notice the Chandra River flowing below and a road that goes by its side. More than two Glaciers comes next which are to be crossed. The other side of the Glacier is Chatru, the next camping zone. Choose a location which has the river running around.
                                    </p>
                                    <p>
                                        Chatru is a magnificent camping site; it is a consensus of the three passes, Hampta, Rohtang and Spiti. The location is just about another two hours.
                                    </p>
                                    <h3>Day 5: Chatru to Manali (drive).</h3>
                                    <p>
                                        A ride back from the Chatru camp site to Manali is scheduled for day 5. After this 7 hour journey full of mellow reminisces of the hills, you will be reaching Manali in the evening. An overnight journey after 06:00pm to Delhi will hopefully work out best.
                                    </p>
                                    <h3>Chandratal Lake</h3>
                                        <p>
                                            A visit of Chandratal will be either on Day 4 or Day 5. You can hike up to the marvellous Chandratal Lake (known as the moon lake). The sheer blue color of the lake has an amazing effect to the surroundings. Chandratal Lake is 45 km from Chatru camp site. 
                                            You can visit Chandratal only in the summer months, if road and weather condition permits us. if not, you will have to drive directly from Chatru to Manali and skip Chandratal all together. 
                                        </p>                                   
                                    <h4>How to Reach</h4>
                                    <h3>By Air :-</h3>
                                    <p>
                                        Bhuntar, which is 52 km away from Manali, is the nearest airport. Taxi services are available from Bhuntar to Manali, which costs about Rs. 700- Rs1000. Bhuntar is well connected to Delhi by air. 
                                        The airliners that take you to this Airport are Indian Airlines, Kingfisher airlines, MDLR Airlines and Jagson Airlines, operating flights from Delhi and Chandigarh to Kullu.
                                        <ul>
                                            <li>
                                                TAH’s Tata Sumo or similar vehicles will pick-up you from Kullu-Manali Airport at 9:30am.
                                            </li>
                                            <li>
                                                Pick-up is included in the cost of the trek.
                                            </li>
                                            <li>
                                                You will reach Bhuntar by 11:00 am.
                                            </li>
                                            <li>
                                                Food from Manali to Bhuntar is not included in the trek cost.
                                            </li>

                                        </ul>
                                    </p>
                                    <h3>By Road :-</h3>
                                    <p>
                                        Manali is well connected to Delhi. Take an overnight bus from Delhi to Manali. Tickets can be booked online at www.redbus.in or www.hrtc.gov.in, Tip: Since Delhi Manali is a long journey most Volvo private buses leave Delhi between 5 pm and 6 pm. 
                                        The last govt bus leaves at 8.30 pm from ISBT Kashmere Gate. Manali to Delhi Like the onward journey, buses leave from Manali to Delhi between 4 and 5 p.m. Your tentative arrival at Delhi may be anywhere between 6 a.m and 10 a.m. 
                                        Plan your onward journeys only post noon giving enough buffer for bus delays.
                                    </p>
                                    <h3>Note:-</h3>
                                    <p>
                                        Trekkers need to take only those VOVO buses which reach Manali till 11.00am on first day. Our reporting time is 12.00pm at Manali but in-case you will not able to reach at meeting /briefing time, either we will transfer your seat to our any next batch or we will cancel your trek, so please double check your bus time with your bus transporter.
