                                <h4>About the trek</h4>
                                <p> 
                                    DeoTibba of 6000 meters height is the second highest peak of the Pir-Panja lMountains. It is in the Kullu valley and starts from Manali. 
                                    There is a local belief that Gods assemble on the top hence the name DeoTibba. The peak is climbed during many Mountaineering Courses expeditions by ABVIMAS institute because it has technical importance. 
                                    However, the peak is suitable for trekkers starting out in the mountaineering field.
                                </p>

                                <p>
                                    The peak is exceptional in a way that the summit is not a pointed ridge but a snow dome just like an ice cap. The trail to the summit goes through the beauty of Himachal Pradesh. 
                                    First comes crossing the beautiful Jagatsukh village near Manali. Right from the beginning each day the trail keeps changing, starting with the dense forests to Birch trees to meadows to the glacier. 
                                    The trek shows every element of the Himalayas. It also never shies from showing lake and waterfalls including rockfall!
                                </p>
                                
                                <p>
                                    Even though it is in 6000 meters like StokKangri the peak requires mountaineering style climbing. It requires a load ferry, crossing technical terrain difficulties, using fixed ropes, crampons, ice axe etc. 
                                    Therefore, DeoTibba is a proper mountain expedition. It is recommended only to experienced trekkers having technical knowledge.
                                </p>
                                
