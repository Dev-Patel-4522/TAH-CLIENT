                                    <h4> Detailed Itinerary</h4>
                                    <h3> Day 1: Arrival at Manali</h3>
                                    <p>
                                        Arrive with excitement in the adventure hub, Manali. Get fresh and be ready to explore Manali. Manali is a town located on the banks of Beas river. It is completely covered with tall Pine trees; the weather is pleasant throughout the year. 
                                        On a clear day, you can also see the DeoTibba peak. You can roam around on the Mall Road or visit Hadimba temple, VashishtKund. Try not to go far or gain height it may hurdle the acclimatization process. 
                                        Delay your plans of visiting Rohtang pass after the expedition.
                                        
                                        <p>
                                            As per the Himachal Pradesh government, foreigners(including OCI, NRI) need to obtain a special permit to trek in Himachal Pradesh. This permit is issued in Shimla and therefore, 
                                            foreigners need to come one day before in Manali. This process requires your signed ILP, therefore it cannot be processed in advance and can be processed only 
                                            when you enter Himachal Pradesh. The charges of obtaining the permit will be Rs.2, 000 per person. TAH will cooperate with you in getting the permit. You have to pay this 
                                            amount to our representative and we shall get the permit, once you have arrived. Please make a note that your expenses for the additional day are not included in 
                                            our costs.
                                        </p>
                                        <p>
                                            Citizens of China, Myanmar, and Bangladesh are not permitted to trek in Himachal Pradesh.
                                        </p>
                                    </p>
                                    <h3> Day 2: Manali to Chika</h3>
                                    <p>
                                        Get all ready to begin the first day of your trek. First, you start with a ride till Sethan and then followed by a small trek to Chika. Make sure you actively observe the sceneries during the ride. 
                                        It is wonderful climbing up passing through Jagatsukh. It is one of the beautiful villages with colourful wooden houses. Jagatsukh was old Kullu capital. The road ahead is not that good with bumpy curves. 
                                        Look the greenery around as you slowly move away from Manali.
                                    </p>
                                    <p>
                                        Right before Chika, you will cross Sethan. It is another popular adventure site, it is famous for rock climbing and snowboarding. You can see big tunnel doors of the Hydro Power project at even distances. 
                                        After you have crossed the Khanual dam get ready to start your trek. The trek route is through dense forests and uphill. First 20 minutes is steep then it is a gradual walk through the valley. 
                                        You will arrive at your campsite in 2-3 hours of the trek. Enjoy your blissful stay in tents.
                                    </p>
                                    <h3>Day 3: Chhikka to Seri</h3>
                                    <p>
                                        Wake up fresh to start your hike to Seri. The trail starts with a gradual ascent through dense forest. As you move up slowly you may notice the forest cover gets sparse. 
                                        The trail is throughout on the right of the JagatsukhNala. You may also encounter freshwater springs at Panduropa and DuduPatthar. 
                                    <p>
                                        The Birch also starts welcoming us through the trail followed by bushes. All of which vanishes to open grounds of meadows. These are the grasslands of Seri but the campsite is away from here. 
                                        You can see Tainta cliffs at the left and waterfalls on these rocks. About an hour's walk through the meadows and you reach the Seri campsite. This campsite is also pleasantly located on flat grounds. 
                                    </p>
                                    </p>
                                    <h3>Day 4: Seri to DeoTibba Base Camp</h3>
                                    <p>
                                        Today will be a little tough day compared to the previous two. The trek starts with a flat trail. The climb is on the zig-zag trail till Tainta cliff. Once you cross the Nala or stream moraines start. 
                                        You will overcome the boulder field. Another hour's walk on the ridge and you will be on the Tainta camp which is the base of the glacier. 
                                    </p>
                                    <p>
                                        As you can imagine henceforth the trail is through loose scree. Take your steps carefully. The climb through such trail is for two hours until you reach ChotaChandratal. It is a tiny lake in mountain depression. 
                                        Your camp will be towards the downside of the lake. Unlike the previous two camps, this camp is in the closed valley. 
                                    </p>
                                    <h3>Day 5: Rest / Acclimatization day</h3>
                                    <p>
                                        Wake up today to chill and rest. Since we have reached at 4200 meters height it is important to give time for acclimatization. You can roam here and there and explore around the campsite. 
                                        Technical equipment will be distributed and tested by our team. You can try them and clear your doubts.
                                        <p>
                                            You can visit ChotaChandratal after lunch. In the evening there will be briefing sessions on the summit process and further plans.
                                        </p>
                                    </p>
                                    <h3>Day 6: Load ferry to Camp 1 (Duhangan Col)</h3>
                                    <p>
                                        Today will mark your journey into mountaineering world. In Mountaineering it is important to do load ferry. It is a term that works on work high and sleep low, where climbers first carry their equipment and gear to the higher camp and rest for the night at base camp. 
                                        This helps for acclimatization and avoids AMS even at higher heights. Today you will be carrying your gear to camp 1 and will come back for the night at the base camp. 
                                    </p>
                                    <p>
                                        The trail is difficult that starts with the moderate ascent for first 200 m then steep for 300 m followed by 100 m flat slope. Camp 1 is situated on Duhangan Pass. Uniquely the location of the campsite can be seen from Base Camp also due to saffron-coloured rocks. 
                                        The slope is between 40-65 degrees and filled snow and rocks. You can see that the glacier starts. There are many crevasses on the glacier so watch your steps. The trail is known for rock fall, therefore, follow your guide and team.
                                    </p>
                                    <p>
                                        Once you have loaded all the gear have your lunch at camp 1 and get ready to go down. Stay will be in the tents at Base Camp.
                                    </p>
                                    
                                    <h3>Day 7: DeoTibba Base Camp to Camp 1</h3>
                                    <p>
                                        Today will be the actual day when you will shift to camp 1 completely. Follow the same route as you did the day before. Be cautious on the rock fall area, glacier and while crossing rocks. 
                                        The climb will take almost 5-6 hours. Make sure you not exhaust your body and take enough rest. Stay in tents at camp 1.
                                    </p>
                                    <h3>Day 8: Load ferry to Summit Camp</h3>
                                    <p>
                                        It is time to move to the summit camp. But again it will only be load ferry and coming back to camp 1. The entire trail is filled with snow. Therefore, wearing all the snow gear and clothing is necessary. 
                                        Along with the snow, the trail is difficult. 
                                    <p>
                                        For the first one hour, the trail is of moderate climb later on it is all steep climb. Be careful and follow your team. Follow the footsteps of your guide as he breaks route open for you. 
                                        Spend little time on the summit camp, have lunch so that your body starts acclimatization. Again retrace your steps back to camp 1. 
                                    </p>
                                    
                                    </p>
                                    <h3>Day 9: Camp 1 to Summit Camp</h3>
                                    <p>
                                        The entire team will now move to summit camp. Carry only minimal items that will be needed and leave the rest at camp 1 only. Follow the same as the day before. Once you reach the camp, your team will brief you about the summit attempt. 
                                        The night will be exciting as it will be the summit night. Eat full and rest properly for the summit attempt.
                                    </p>
                                    <h3>Day 10: Summit Attempt and back to Camp 1</h3>
                                    <p>
                                        The summit attempt will start at 2 am in the early morning. Wake up accordingly to get all geared up for the summit push. Recheck all your equipment and be prepared. 
                                        The route starts by crossing a gulley using fixed ropes. First 300 m is steep and technical ascent.
                                    </p>
                                    <p>
                                        Keep climbing up on the snow slopes. You can see Indrasen peak as you gain height. The slope gets steeper right before the summit. The summit is actually big snow dome, open and flat. 
                                        Hence, the last 100 m to the summit is almost a flat walk. There are also many crevasses towards the end. 
                                    </p>
                                    <p>
                                        The summit is wide open that can accommodate all the team members. When you arrive at the summit you can clearly see Indrasen peaks and a mountain lake. The views are absolutely stunning.
                                    </p>
                                    <p>
                                        If for any cases summit push gets halted then buffer days will be used. After the summit attempt come back to camp 1.
                                    </p>
                                    <h3>Day 11: Camp 1 to DeoTibba Base Camp</h3>
                                    <p>
                                        After the summit attempt is now time to start the return journey. Follow the same trail to DeoTibba Base Camp. It is a steep descent, therefore, watch your steps. 
                                        Try to reach the Base Camp till lunchtime. Return all your technical equipment to your Trek Leader. Rest after the strenuous days in your tents.
                                    </p>
                                    <h3>Day 12: Base Camp to Chika</h3>
                                    <p>
                                        Start your return journey to back home. Retrace steps through steep trail and moraines. The path is gradual and flat in between. 
                                        The path will be 14 km long hence carry your packed lunch. Enjoy your last tent stay at Chika.
                                    </p>
                                    <h3>Day 13: Chikka to Manali</h3>
                                    <p>
                                        The last day of the trek has arrived. Collect all the memories and say goodbye to the mountains. 
                                        Follow the same trekking trail and then ride back to Manali. You will reach Manali till lunchtime.
                                    </p>
                                    <h3>Day 14&15: Buffer Day</h3>
                                    <p>
                                        Every expedition plan has buffer days for flexible planning. This day is used in case of unfavourable conditions when the summit attempt gets halted. 
                                        So to increase the chances of the summit, 2 days are included in the itinerary. If due to any situation summit is not possible then the team will wait at camp 1 and try attempting summit again.
                                    </p>
                                    <p>
                                        Using the buffer day will be a team decision. Please note that the cost of the buffer day is not included in the trek cost. In case the team decides to use the buffer day the said cost must be paid. 
                                        Make sure to already discuss the buffer costs with your team. If everything goes as planned and buffer days are never used then you can utilize them to travel around in Manali.
                                    </p>
